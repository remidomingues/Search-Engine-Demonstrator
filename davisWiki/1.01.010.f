 GENERAL PROVISIONS  
 1.01.010 Code designation and citation. 

The ordinances embraced in the following chapters and sections shall constitute and be designated The Code of the City of Davis, 1971 and may be so cited, or may be cited as the Davis Municipal Code. Either such citation shall be sufficient to designate any ordinance adding to, amending or repealing such Code or portions thereof and such citation shall be sufficient citation in the prosecution of any violation of the Code. Any sign or notice within the city which refers to this Code may be abbreviated DMC. (Ord. No. 1313, ยง 1.)

