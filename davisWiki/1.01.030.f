 1.01.030 Acts by deputy. 

Whenever a power is granted to or a duty is imposed upon a public officer, or employee, the power may be exercised, or the duty may be performed by a deputy of such officer or employee or by any person otherwise duly authorized pursuant to law or ordinance unless this Code expressly provides otherwise. (Code 1964, ยง 13.05.)

