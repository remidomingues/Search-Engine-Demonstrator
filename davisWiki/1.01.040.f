 GENERAL PROVISIONS 
 1.01.040 Provisions considered as continuations of existing ordinances. 

The provisions appearing in this Code, so far as they are the same in substance as those ordinances existing at the time of the effective date of this Code, shall be considered as continuations thereof and not as new enactments. (Code 1964, ยง 13.02.)
