 GENERAL PROVISIONS 
 1.01.060 Effect on matters of record. 

The provisions of this Code shall not in any manner affect deposits or other matters of record which refer to, or are otherwise connected with ordinances which are therein specifically designated by number or otherwise and which are included within this Code, but such reference shall be construed to apply to the corresponding provisions contained within this Code. (Code 1964, ยง 11.04.)

