 GENERAL PROVISIONS 
 1.01.090 Territorial application of code. 

This Code shall refer only to the omission or commission of acts within the city and to that territory outside of the city over which the city has jurisdiction or control by virtue of the Constitution, or any law, or by reason of ownership or control of property. (Code 1964, ยง 13.10.)

