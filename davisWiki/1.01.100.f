 1.01.100 Code severability. 

If any section, subsection, sentence, clause or phrase of this Code is for any reason held to be unconstitutional, such decision shall not affect the validity of the remaining portions of this Code. The city council hereby declares that it would have passed this Code and each section, subsection, sentence, clause or phrase thereof irrespective of the fact that any one or more sections, subsections, sentences, clauses or phrases be declared unconstitutional. (Code 1964, ยง 11.05.)

