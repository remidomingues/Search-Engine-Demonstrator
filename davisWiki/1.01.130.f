 1.01.130 Punishment determination. 

Whenever in this Code or other city ordinances, the punishment for a crime is left undetermined between certain limits, the punishment to be inflicted in a particular case shall be determined by the court authorized to pass sentence, within such limits as may be prescribed by this Code. (Code 1964, ยง 14.04.)

