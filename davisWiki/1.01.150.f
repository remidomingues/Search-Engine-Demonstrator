 GENERAL PROVISIONS 
 1.01.150 Notice to appear in court for violations. 

If any person is arrested for the violation of any provision of this Code or any ordinance of the city and such person is not immediately taken before a magistrate as prescribed in the Penal Code of the state, the arresting officer shall prepare in duplicate a written notice to appear in court, containing the name and address of such person, the offense charged, and the time and place where and when such person shall appear in court. (Code 1964, ยง 1 5.01.)

