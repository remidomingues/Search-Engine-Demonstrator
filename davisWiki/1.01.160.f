 GENERAL PROVISIONS 
 1.01.160 Violation of written promise to appear in court. 

Any person wilfully violating his/her written promise to appear in court is guilty of a separate violation of this Code, regardless of the disposition of the charge upon which he/she was originally arrested.

When a person signs a written promise to appear at the time and place specified in the written promise to appear and has not posted bail as provided in section 853.1 of the Penal Code of the state, the magistrate shall issue and have delivered for execution a warrant for his/her arrest within twenty days after his/her failure to appear as promised. (Code 1964, ยง 15.02, 15.03.)

