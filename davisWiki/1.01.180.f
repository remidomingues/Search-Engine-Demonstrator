 1.01.180 Notice and proof. 

Proof of giving any notice may be made by the certificate of any officer or employee of the city, or by affidavit of any person over the age of eighteen years, which shows service in conformity with this Code, or other provisions of law applicable to the subject matter concerned. (Code 1964, ยง 1 3.07.)

