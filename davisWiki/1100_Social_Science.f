
1100 Social Science is a large Campus Lecture Halls lecture hall in the Social Sciences and Humanities Building. It seats 369 students, making it the third largest lecture hall on campus.

The seats within this hall are very comfortable and spacious. Furthermore, the lighting inside is not very bright. These two factors provide for a perfect nap time if you happen to have a lecture in this hall.

At one point the wide screen projectors used in this hall were of the same model used by the US Navy on their aircraft carriers. Those were sold through Bargain Barn.

