  State census for Yolo County lists:
   Whites, males: 1,085
   Whites, females: 189
   Negros, males: 11
   Negros, females: 3
   Indians, males: 109
   Indians, females: 43
 There is a note suggesting that the census only recorded Indians who were permanent residence of the county.

  The census lists four towns: Washington (now the Broderick section of wiki:westsac:Broderick West Sacramento), Fremont (was located north east of Knights Landing), Putah, Cottonwood, and Merritt.
