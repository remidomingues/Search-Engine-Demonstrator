  August 1, Miss Stella Huntington, county librarian, is charged with starting branch stations of the Yolo County Library. One is established in Davis consisting of fifty or more books that shall be entirely or partly changed every three months.
  December 10: The ASUCD Associated Students of the University Farm was chartered by students at an student assembly.  The ASUF subsumed the Athletic Association founded in the previous year, but was founded to expand student activities beyond athletics.

