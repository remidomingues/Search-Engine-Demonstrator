The 1991 Davis Bike Plan set the stage for many improvements, including:
  The Dave Pelz Bike Overpass Dave Pelz overpass over Interstate 80
  The Putah Parkway underpass under I80 and the RR tracks
  Bike Lanes on Covell west of 113
and many others.

Each capital improvement (big project that would cost a lot of money) was planned years in advance, and funding sources were identified.

Compare to 2001 http://www.city.davis.ca.us/pw/pdfs/01bikeplan.pdf Bike Plan.

