This is an annual soup cookoff sponsored by the Davis Food Coop. It was held in the MultiPurpose room of the Davis Senior Center.  Competitors are local restaurants that make soup, and the chefs representing them.

Osteria Fasulos Roasted Tomato Soup took first place.  Adam Walsworth, the executive chef, shares his recipe on their Osteria Fasulo wikipage.

Profits from the event benefit the Elderly Nutrition Program.
