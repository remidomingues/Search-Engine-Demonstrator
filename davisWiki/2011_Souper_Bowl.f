The fifth annual Souper Bowl was held in 2011 on February 27, 4pm to 6pm at the Veterans Memorial Center.

A $10 ticket ($3 for those 12 and under) nabbed you soups and bread and a vote in the peoples choice award. Beer and wine was available for purchase from the Soroptomist International Soroptomists.

As the Coops event notice read, profits from the event benefit our countys Elderly Nutrition Program, offering services like Meals on Wheels. Theyre an important part of our safety net, and a fun afternoon full of food and friends to raise money for them Just Feels Good!
