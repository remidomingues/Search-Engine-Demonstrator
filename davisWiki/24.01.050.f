 24.01.050 Severability. 

If any provision, clause, sentence or paragraph of this chapter, or the application thereof to any person or circumstance, shall be held invalid, such invalidity shall not affect the other provisions or application of the provisions of this chapter which can be given effect without the invalid provisions or application and, to this end, the provisions of this chapter are declared to be inseverable. (Ord. No. 1700, ยง 1 (part), Ord. 1955 (part).)

