Davis Municipal Code Davis Municipal Code/24 Chapter 24
 NOISE REGULATIONS 
 24.07.010 Interrelationship of provisions. 

It is the purpose of this chapter to provide maximum noise level limitations for otherwise lawful activities. Nothing contained in this chapter shall be deemed to authorize any otherwise prohibited activity nor to supersede otherwise existing noise limitations. In the event of a conflict between the standards contained in this chapter and any other provision of law, the more restrictive shall govern. (Ord. No. 1700, ยง 1(part); Ord. No. 1955 (part).)

