 PARKS AND OPEN SPACE AREAS 
 27.02.130 Behavior in parks. 

No person using a park shall:

(a)Have brought or have in his or her possession, or set off or otherwise cause to explode or discharge or burn, any firecrackers, torpedo, rocket, or other fireworks, explosives of inflammable material, or discharge them or throw them into any such area from land or highway adjacent thereto unless allowed by permit. This prohibition includes any substance, compound, mixture, or article that in conjunction with any other substance or compound would be dangerous from any of the foregoing standpoints;

(b)Make or kindle a fire for any purpose except at places provided for such purpose unless a permit has been obtained therefor;

(c)Enter an area posted as Closed to the Public, nor shall any person use any area in violation of posted notices. (Ord. No. 1011, ยง 2 (part); Ord. No. 1877, ยง 1 (part).)

