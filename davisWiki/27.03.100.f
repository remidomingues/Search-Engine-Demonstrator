Davis Municipal Code Davis Municipal Code/27 Chapter 27
 PARKS AND OPEN SPACE AREAS 
 27.03.100 Fires, smoking and fireworks. 

No person using a city open space area shall make or kindle a fire for any purpose except at places provided for such purpose, and/or if granted a special permit by the director, to do so.

Smoking is prohibited in any city open space area. Fireworks are prohibited in any city open space areas. (Ord. No. 1877, ยง 1 (part).)

