The Biological Systems Engineering Biological & Agricultural Engineering Computer Lab, or the EBS Lab, as the students call it, is the computer lab/study lounge area for Biological Systems Engineering students; for both graduate and undergraduate students alike. It can be buzzing with students to dead during the day depending on the time of the quarter. This lab is also where some students live out of during Finals week. You cant get into the lab unless you have the door code, a valid Kerberos Login kerberos username and password, and are an EBS Student.


