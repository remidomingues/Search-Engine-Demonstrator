 SMOKING CONTROL 
 34.02.020 Regulation of smoking in places of employment. 

(a)It shall be the responsibility of employers to provide a smokefree workplace for all employees.

(b)Within ninety days of the effective date of the ordinance codified in this article, each employer having an enclosed place of employment located within the city shall adopt, implement, make known and maintain a written smoking policy which shall contain the following requirement:

Smoking shall be prohibited in all enclosed facilities within a place of employment without exception. This includes common work areas, auditoriums, classrooms, conference and meeting rooms, private offices, elevators, hallways, medical facilities, cafeterias, employee lounges, stairs, restrooms, vehicles, and all other enclosed facilities.

(c)The smoking policy shall be communicated to all employees within three weeks of its adoption.

(d)All employers shall supply a written copy of the smoking policy upon request to any existing or prospective employee. (Ord. No. 1682, ยง 2 (part).)

