
8th Streets Street runs East/West and consists of two streets: East 8th Street East 8th and West 8th Street West 8th. 8th changes names and address numbers at A Street.  In addition, East 8th Street curves / turns into Tulip Lane. It intersects Sycamore Lane and Pole Line Road.

The addresses are not continuous which is a bane to new Pizza pizza delivery drivers. So if you live on 8th and are ordering a delivery, be very specific about which part of 8th you live on. 8th is also a good road to avoid police as most police concentrate on Russell Blvd.

While the Speed limits speed limit is only 25mph for the length of 8th St, the traffic lights are timed quite well, making it an (often quicker) alternate to Russell Russell Blvd.

8th St, with Sycamore Lane, was added in 1967 as one of the few new postWWII streets with bike lanes.  Then UCD Professor Bob Sommer brought the idea to city planners in the mid 60s.  The initial design was a success, and became the standard bike lane design for the rest of Davis, California.  At first, bike lanes only went from Sycamore Lane to B Street, then later from J Street to Pole Line Road.  The gap from B Street to L Street was too narrow for bike lanes and parking.  Eventually, daytime parking was banned, then parking was permanently banned, and now 8th Street is easily the best street to go across town.  Had the parkers won a victory in 1970, it would be much more dangerous for bicyclists.  8th Street now has bike lanes for its entire length, and specific bicyclistoperated traffic signals at the intersections with L Street and B Street.

Be careful bicycling through the F Street intersection on 8th Street.  There are no left turn lanes for cars, so a bicyclist can be hidden behind a samedirectioncar while approaching the light, and then hit by a left turner in the opposite direction after the samedirection car has passed.  Or, if you are passing a car that is waiting to turn left, you are hidden from view and may have opposing left turners turn in front of you or hit you.  Several serious, nearly fatal accidents have occurred this way in recent years.

Before East 8th St. was built, Cemetery Road extended from G Street east across the railroad tracks to the Davis Cemetery.  Sweet Briar Road and Pennsylvania Place are remnants of Cemetery Road, and the section from J Street to the cemetery is now part of 8th Street.  Part of old Cemetery Road can be seen if you walk into the bushes from the corner of Sweet Briar and H Street  there is some concrete and a yellow center line on an abandoned piece of street where it once crossed the railroad tracks.  8th Street ended just east of the cemetery until the mid 1990s when it was connected to Tulip Lane.

