95618 is one of the three zip codes in town. At one time it only served El Macero, but it was readjusted to take some of 95616.
95618, when searched on many websites, will get you El Macero. For instance, for looking up a weather forecast, it says El Macero and not Davis. It is ironic, because most of this zip code is residential and actually in the city limits of Davis AND that of all of the post offices in Davis, none are currently located in 95618.

The third zip code, 95617, is only for post office boxes.

