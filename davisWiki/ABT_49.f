ABT 49, Field Equipment Operation (aka Tractor Driving), is a Classic UC Davis Courses classic UC Davis course offered every fall and spring quarter.  It is taught at the Western Center for Agricultural Equipment (WCAE) and is 2 P/NP units.


During the course, youll get to drive quite a few tractors, from rustic John Deeres to modern models with air conditioning.  Youll learn how to hitch up and operate various implements such as:
 Springtooth harrow
 Moldboard plow
 Disc
 Lister
 Bedder
 Ringroller
 Land plane
 Mower

Youll also learn how to calibrate seeders/fertilizers, perform basic tractor maintenance, and move large piles of dirt with a front loader.
