The ASUCD Campus Safety Unit no longer exists; it was disbanded and its funding withdrawn in mid2005.

Campus Safety, was located at 349 Memorial Union, served as an advocacy unit of ASUCD. The ASUCD unit addressed student concerns of safety with the use of workshops, programs, events and educational material. This unit was responsible for informing the ASUCD President and Vice President of student concerns regarding safety and to work, at the campus administration level, to address safetyrelated concerns of UCD students.

There is also the Campus Safety Escort Service for safe, onCampus transportation.

