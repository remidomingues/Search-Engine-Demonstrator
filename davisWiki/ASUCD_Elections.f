 ASUCD Elections  Now Celebrating 75 Years of Scandal 

ASUCD Elections occur in the seventh week of Fall and Winter Quarters. In Fall Quarter six ASUCD Senate ASUCD Senators are elected and in the winter six more ASUCD Senate ASUCD Senators as well as the Presidential ticket  the ASUCD President and ASUCD Vice President.

The elections are administered by the ASUCD Elections Committee, and Choice Voting is used in ASUCD Elections.

