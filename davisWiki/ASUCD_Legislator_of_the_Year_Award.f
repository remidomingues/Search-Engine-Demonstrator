


The ASUCD Legislator of the Year Award was created in 2008 to recognize a legislator who has worked hard on behalf of students student issues. The award was conceived by ASUCD Lobby Corps Director Derick Lennox. The ASUCD Awards award winner is selected by ASUCD Lobby Corps.

The 2008 Legislator of the Year was California State Senator Leland Yee.
