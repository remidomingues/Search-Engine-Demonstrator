

The ASUCD Scholarship are for all continuing undergraduate students that are interested in applying.  There are four $500 scholarships and three $1000 scholarships.

In a bygone era of ASUCD History it typically served as a LEAD Retirement Fund, or Student Focus Focus Retirement Fund, depending on which of the now defunct slates was in power.  These prizes were not given out during the 20092010 and 20102011 school years, but have returned for the 20112012 school year.



