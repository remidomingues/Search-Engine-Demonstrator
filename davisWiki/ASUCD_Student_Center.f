The ASUCD Student Center was created  in either 2003 or 2004 as a way to make ASUCD more visible to the average student by putting it directly across from the UCD Bookstore in the Memorial Union. Staff from the ASUCD Student Government Administrative Office would hold hours in here as well.

It was the office for the Campus Outreach and Organizing Office from 2005 Spring 2007.

Prior to its conversion into the Campus Outreach and Organizing Office COOO it appeared to be merely a computer graveyard with monitors and iMacs placed haphazardly throughout.  There was also discussion of ASUCD Senators holding their office hours here, prior to its transformation.

The office is currently being used by ASUCDs Pathfinder.
