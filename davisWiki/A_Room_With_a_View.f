A Room With a View was a student built wiki:wikipedia:camera obscura on exhibit in the UC Davis arboretum in the winter of 2007/2008.  It was essentially a giant walkin camera and created an impressionistic photographic image inside.  The lens could be rotated 360 degrees from within.

The exhibit received a writeup in the Davis Enterprise on December 19th 2007.  It was free and open to the public but was removed at the end of January.  Its now gone!


