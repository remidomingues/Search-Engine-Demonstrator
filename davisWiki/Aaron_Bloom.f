Aaron Bloom was a political science (and philosophy minor) undergraduate at UC Davis between 1993 and 1997. During his time at UC Davis he was a member of the Phi Alpha Delta PreLaw Fraternity, the 199596 ASUCD Vice President, an ASUCD Court ASUCD Supreme Court Associate Justice (19967), a Resident Advisor (19967),  a member of the UCSA Board of Directors (19956), and member of the Alpha Epsilon Pi Fraternity .

Upon his graduation in 1997,  he was awarded The Chancellors Award of Merit.

He went on to attend the University of Pennsylvania Law School and is an attorney in Los Angeles.
