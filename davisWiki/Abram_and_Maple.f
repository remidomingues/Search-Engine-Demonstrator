Date of Birth: September 1, 2004

Names: Maple is the darker, smaller, and sweeter one. Abram is the bigger and lighter one and looks like an M1A1 Abram tank.

Diet: Vegetable, grass, fruits, and just about anything edible!

Exercise: We wander around freely in daddys apartment at The Colleges at La Rue The Colleges because weve outgrown our tank and go for occasional walks outside when its warm.

Environment: Pebble filled tank with a heat log we sleep in or when were outside of the tank, underneath the couch, inside of slippers, and in the bathroom... hey, its home.

Hobbies: Sleeping, eating, exploring, cuddling, sniffing and looking at anything we come across (turtles are quite curious critters).

Dislikes: Bathing, which is done with warm water, a toothbrush, and baby shampoo. Being leashed with a rubber band and yarn so we dont wander too far.

Characters: I, Maple is outgoing and fond of humans whereas Abram, my hubby is the shyer one and has fits when daddy turns him over and tickles him (you gotta see it to know what i mean, its hilarious).

Life expectancy: ~120 years.

Full size: Well be full grown in 50 years, at about 32 inches head to toe and 200 lbs.

Memorable moment: I had a plate of salad for lunch outside of UCDs food courts, received a phone call to meet up with some friends, came back, and the two had climbed onto my plate and started eating away!
