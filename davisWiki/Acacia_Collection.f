


The Eric E. Conn Acacia Grove displays over 50 species of acacias from Australia, Africa, and the Americas. In early spring, visitors can walk through clouds of fragrant yellow blossoms amid meadows of native California bunchgrasses. We are testing these attractive heat and droughttolerant plants, which range from prostrate, lowgrowing species to tall shade trees, for use in Central Valley gardens. The grove is named for Dr. Eric E. Conn, professor emeritus of biochemistry at UC Davis and an internationallyrecognized expert on acacias.





