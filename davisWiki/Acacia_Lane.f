History
Acacia Lane was the 102nd street built in Davis. It was built in 1952 with the expansion of the UCDavis campus. At least two nearfatal accidents have happened on this street, which some have called accursed. And the semiinfamous Breitbach murder of 1954 happened at the intersection of Acacia and Plum Lane.

1033 Acacia Lane is said to be haunted by the ghost of the Naked Lady of Riverside, usually late night to early mornings, who died in 1973 after getting hit by a pickup truck.

