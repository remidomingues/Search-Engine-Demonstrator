

UC Davis Advising Services maintains an academic advising location in each of the dorm areas for all students living in the Residence Halls. They feature Academic Peer Advising peer advisors who offer help with planning course schedules, and understanding Majors Major and General Education requirements. Also, they have peer tutors who tutoring tutor with calculus and precalculus types of math, workload and regular chemistry two series, and linguistics series classes. They offer paper writing help as well. Come in early, since the place often fills up.

During finals week they offer live review sections for the main (most common) intro chemistry and calculus classes being taught that quarter.


