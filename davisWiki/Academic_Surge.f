
address(Academic Surge, 38.535306, 121.752887) (ACADSR) has the good luck of having one of the weirdest building names on Campus campus. That being said, the building itself is fairly plain, aside from the architect having an odd industrial fetish.  Inside are numerous computer labs that highly used by civil engineering students.

Academic Surge is not to be confused with Surge I, Surge II, Surge III or Surge IV.

Academic Surge is located on the south side of EU III Engineering III (which is near Kemper Hall, for those not familiar with Eng. III which is now called Ghausi Hall).

This building hosts (at least) the following:

   Institute for Data Analysis and Visualization
   Institute for Transportation Studies
   School of Education
   Bohart Museum of Entomology
   Energy Efficiency Center
   Museum of Wildlife and Fisheries Biology
   Engineering Student Startup Center
   The Ragle Center, used by the Department of Nutrition Nutrition Department for clinical studies
   One very odd (now only occasionally broken) ice cream vending machine.
   A very high concentration of late night grad students
