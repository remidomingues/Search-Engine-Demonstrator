The http://ahmct.ucdavis.edu/ Advanced Highway Maintenance Construction Technology group is hosted by the Mechanical and Aeronautical Engineering Department of Mechanical and Aeronautical Engineering.

They develop new technologies for Caltrans to use in maintaining the states highways and roads. If you look for orange painted vehicles around either Bainer Hall or Academic Surge you may see some of their work.

Projects have included:

  video targeted spraying for weeds (which sprays much less pesticide)
  automatic crack sealing
  automated road cone deployment and retrieval system

