
The Advanced Materials Research Laboratory finished University Construction construction in the spring of 2008. The site is across the arboretum from the Putah Creek Lodge. The labs main purpose is to provide permanent laboratory and support space for students and faculty engaged in HVOF (high velocity oxygen fuel) research. A total of 4,625 assignable square feet will be provided including the HVOF lab, four fuel cell research labs, a machine shop and related research offices and support space.


