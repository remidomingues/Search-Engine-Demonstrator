
African Americans and Africans Cultivating Education, or ACE, is a student organizations student organization that is part of the Student Recruitment and Retention Center.

ACE works to guide and empower students early in their school careers (starting in 8th and 9th) to prepare themselves for the pursuit of higher education. ACE will provide current UC Davis students will holistic support and tools to complete their undergraduate careers.

Services include a mentorship program, campus visits, parent workshops, study halls and a book exchange.

ACE is located in the basement of South Hall.
