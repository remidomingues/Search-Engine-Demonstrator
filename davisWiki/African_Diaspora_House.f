Tandem Properties  Russell Park


The African Diaspora House or more fondly known to its occupants as The ADH, was a coed coop on campus that housed mostly transfer students. The program was overseen by the African and African American Studies department, while lease management was handled by Tandem Properties. With the close of the 2004 academic year, Tandem and UC Davis agreed to terminate the program and establish an agreement with a campus fraternity. It is now the Delta Sigma Phi house.

A few years back, a gun was fired at a party, and while such stains remain part of ADHs history, most of its former residents have fond memories of their time spent there.

