Aggie Jam is a yearly event hosted by the Aggie Pack to help promote the Mens and Womens basketball programs. The event is free and takes place at night. There are free tshirts and a fair amount of burritos, pizzas, candy, tube socks, etc. thrown, fired, tossed, blasted into the crowd. It is probably an undergraduate students undergrad only thing, but were not sure. For the 20032004 Aggie Jam, they gave out obscenely yellow tshirts.

Other basketball promotions include:
  Break the Record Night
  Black Out Night
