The sweet taste of Aggie Pride!

Its sold at Nugget.  In late summer, 2006, the Aggie Pack Fire Engine was parked right in front of the North Davis Nugget, on the actual shopping center (not on the street).  This was probably a promotional event for the drink.  Soon thereafter, Nugget started to stock Aggie Root Beer at $1.49 a bottle.  This root beer tastes slightly different from most other root beers, slightly sweeter.  Maybe its due to the honey. The label is gold and blue (as expected for UCD colors) and says that it is the Official Root Beer of UC DAVIS Athletics.

Despite having a brewery on campus, the drink is brewed and bottled at Coast Range Brewing Co, in Gilroy, California.

