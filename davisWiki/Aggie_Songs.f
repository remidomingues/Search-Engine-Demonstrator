Traditions Traditional Aggie Songs include:

  Aggie Fight Song Aggie Fight
  Big C
  Hail to California
  Roll, Big CA, Roll
  Sons of California
  Ragtime Cowboy


With the exception of Aggie Fight Song Aggie Fight and Roll, Big CA, Roll, these songs are shared throughout the UC system.  Hail to California is UC Davis Alma Mater.
