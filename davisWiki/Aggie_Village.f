Aggie Village is a housing development designed to provide a unique neighborhood for UC Davis faculty and staff.  It is found at the northeast end of the Arboretum and was developed around 1997.  The houses are small and many of them have even smaller Aggie Village Cottages cottages in their backyards. It is located at the southern end of C Street in the southern part of downtown. The Village consists of two streets Aggie Lane and Cottage Circle. The Davis Commons shopping center is to the east. The arboretum is just to the south of the village and its bike paths tie directly in.

In some ways this project has served as a testbed for ideas that are planned for the West Village.



 Land History 

This section of land has served as a location for college dorms, the bus depot backed by a vacant field, and other uses over time.
