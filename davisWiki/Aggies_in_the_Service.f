Aggies in the Service is a registered UC Davis Student Organizations student organization with the following stated goals:
  Regardless of whether you support the war, support our troops
  Service members, family of service members, or just people that want to support our troops
  Provides a forum for improving relations between the community and UCD Army ROTC
  Joining the club entails NO MILITARY OBLIGATION

