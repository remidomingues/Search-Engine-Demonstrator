
The majority of the time the air is fairly clear in Davis. There are times when the air quality in the Sacramento Valley valley gets bad, especially when there are Grass Fires fires in the area. Air pollution caused by human activities is also a concern. The YSAQMD is the local governing body that concerns itself with the air.

You can get more information about air quality, including realtime data for many counties at http://www.airquality.org.

See also Spare the Air Day Spare the Air alerts.

