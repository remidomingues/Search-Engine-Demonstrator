
 AlWasilah is an AS Papers publication, which aims to enlighten and evoke thought and discussion among the Muslim community. We aspire to critically analyze the diverse human response to Islam and the full range of perspectives that develop as a result, as well as tackle controversial issues relevant to our Ummah. We invite nonMuslims to discover Islam in the context of politics, social issues and the arts.

