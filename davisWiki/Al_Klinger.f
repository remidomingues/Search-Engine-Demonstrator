Al Klinger was an art Teachers in the Davis Joint Unified School District teacher at Holmes Junior High School for over 30 years from the 70s90s. He retired in the late 90s and now thoroughly enjoys being retired. Over the years he taught thousands of kids, and is still remembered by many in Davis and the surrounding areas for his unique way of teaching, and friendly personality.

He still works on various art projects and had his first art show at the Davis Art Center in 2007, which included a number of his interesting coffee grounds and glue canvases made over the past few decades.




