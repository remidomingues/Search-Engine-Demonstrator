Alan Fernandes is a current Trustee of the Davis JUSD School Board, having been appointed in May 2014 to complete the term of Nancy Peterson, who resigned in March, until the November 2014 election.  Fernandes came in third in the 2012 election for school board, behind Susan Lovenburg and Peterson in which two seats were filled.

