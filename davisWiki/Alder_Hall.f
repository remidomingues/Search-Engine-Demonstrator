
Alder Hall is the westernmost unit of the threebuilding UC Davis residence hall complex known as Segundo North. Built in 2003, Segundo North is located in the northwest quadrant of campus.  In 2004, Alder 2 became home to the Academic Theme Programs AfricanAfrican American Academic Theme Program.  As of 2010, the entire building houses the Asian Pacific American Themed House.  The building houses approximately 180 students with 20 triple occupancy rooms on floors two, three, and four.  Rooms are grouped in clusters of four with a shared bathroom.

