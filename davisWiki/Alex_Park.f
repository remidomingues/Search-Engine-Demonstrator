
Alex Park is the director of ASUCD ASUCDs Creative Media unit, the only nonstudent working there.  Alex seriously has too much to do, essentially being in charge of all of the ASUCD websites, all of ASUCDs computers (tons and tons of them!). He does all this with less resources than an IT unit usually has available. He has lots of great toppick student programmers and graphic designers.

Park has worked in ASUCD for a long time.  He used to work for the Coffee House as a manager.  After that, he moved up to being their technical guy, and when Creative Media was formed he was the logical guy to take over and run the show. You can call him Park or A Dawg or Shady.
Alex also helped start AGTV, now AggieTV, the premiere student video and media production studio.

He can be often found at the 3rd and U Cafe for lunch with his good looking programmers, all of whom love him dearly. He even has his own Alex Park Special.

Hes a pretty nice guy. He does a good job and helping out everyone in ASUCD and hooking up the students who need help. You should be nice to him, because if not, its bad karma.

