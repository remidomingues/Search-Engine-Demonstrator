Alison Keenan is a Graduate Students graduate student at UC Davis, starting in her PhD in the Nutritional Biology program. She moved here from New York City, via Amtrak train.
Her undergraduate degree is in Forensic Science, specifically toxicology. Not only is she a scientist, but also a writer and artist of sorts. Currently she lives in a studio with her two cats Miller and Sage.

Alison is a GSA representative for the Executive Council of the Academic Senate, as well as the Educational Policy Committee. She has responsibilities as a member of the Student Advisory Committee for the Graduate Group in Nutritional Biology.

