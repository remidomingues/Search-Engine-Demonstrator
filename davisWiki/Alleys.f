

 Interesting things to see & do in Davis alleys 


Granny Flats
Davis alleys are the perfect environment for granny flats. Standalone buildings originally intended for your elderly parent or inlaw, granny flats are minihouses designed to give the resident privacy & independence, while still being close to the main house. They usually have their own kitchen (though minimal), living area, & bedroom, although often all in the same room. Granny flats often open onto alleys for maximum privacy and minimal disturbance from the street.

Once upon a time, renting a granny flat was Davis answer to cheap housing. But now that granny flats are popping up all over the place, they are often more expensive than an apartment.


