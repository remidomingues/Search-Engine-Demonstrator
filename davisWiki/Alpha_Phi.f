Alpha Phi at UC Davis boasts a group of incredibly talented and ambitious girls who are not only members, but leaders in various areas of campus and community life. From  Relay for Life to CaretoCure to Student Fashion Association, as well as athletics, academic organizations, studying abroad, and of course social events, our diverse sisters can be seen all over town and the world!

The powerful bond Alpha Phis share is hard to describe, but evident in the support and care our sisters show for each other. We sought out friendship, and found sisterhood. We are a united house, brought together by not only our similarities but also our differences. Our friendships last a lifetime!


