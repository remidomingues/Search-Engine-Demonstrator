 Food 

 Growing your own food 


  Davis Community Garden
  EC Gardens Experimental College Gardens
  Cooperative Housing Coop Gardens  Many of the Davis housing cooperatives have garden areas for their residents uses.  Make some friends and maybe you can plant something there...
  Hydroponic Gardens  Good for growing plants inside but probably not cost effective unless growing very valuable plants.
  Gardening Your backyard  Davis tends to have good earth for growing.
  Brew your own beer  This can be very fun.
  Get Free Food from around town.

Beware:  the startup costs in growing your own food, depending on what you grow, can sometimes exceed whatever you might save during your first harvest.  Either plant a lot and collaborate with friends, or expect to recoup your investment over time.

 Complimentary Continental Breakfasts 

  Some Apartments give away a free continental breakfast on the weekends.  These usually consist of a few apples, bananas, baked goodies, cereals, and juices.  They dont really check for residents, its just a walk in and grab deal.  Greystone definitely has the brunch.   (Some might consider this morally questionable)

 Condiments 

  Ask for extra ketchup, hot sauce, and mustard packets on those occasions you do go out.  Also ask for extra napkins.
    Hot Sauce is free when you get it from the MU on the tables outside.  This is stealing.
  Sugar, Honey, Lemon Juice, Sweet and Low, Straws, and plastic silverware can be found at The Silo.
  Sugar, Silverware, Splenda, Equal can be found at the Memorial Union.

If youre going to take some extra, its probably wise not to take a whole ton and to ask for permission.  A few extra packets at a time will not offend anyone.  But taking a whole lot at once will likely lead to tighter control of the wonderful sauce packets and sporks. Be mindful that condiment packets involve a lot of packaging for very little product, and contribute to our Landfill landfill problems.

 For UCDavis Students 

  There is a theory that since there is always something going on on the UCD campus, free food can be found just by joining lots of organizations and participating in their events.  Food is also offered during guest lectures.  While the quality of the food can range from Sodexho grade to Pizza, there is always some way to get food on campus if youre willing to put your time into it.

 Disposable products 

  Buy and use cloth napkins and towels instead of napkins and paper towels .  However, if you do not wash your laundry by hand this could ultimate cost more than a roll of paper towels.  Remember, the environmental impact of a load of laundry can be softened by using ecologically friendly soaps.
  Take extra toilet paper from various places like the Dorms, the bathrooms in Everson Hall, the second floor hallway of Sproul Hall.  This is illegal.

