 Furnish your Apartment 

  Moving Day means free furniture throughout Davis.  It feels like looting, but it is legal.  There are also certain apartment complexes notorious for random appearances of fairly good furniture yearround.  Check out: Renaissance Park Apartments, Parkside Apartments, Almondwood and just about any apartment complex big enough to have several dumpsters.
  Dumpster Diving is a useful way to find furniture and all sorts of treasures.
  Freshmen Move Out Day is different than Moving Day. It is the day that Freshmen have to move out of the Dorms. They throw away INSANE amounts of perfectly good stuff.  There are a LOT of clothes that are perfectly wearable that are tossed out along with blankets, lamps, and other small furniture items.  Pick them up before R4 picks them up and sells them.
  The Freecycle Network Davis Freecycle  a grassroots movement of people who are giving (and getting) stuff for free in their own towns.

 Media 
  Normal TV and Radio is free (you dont need to have cable, etc).  The downside to watching most TV or Radio is the extreme amount of commercials and otherwise corporate messages.  This can be usually avoided by tuning in to nonprofit community radio like KDVS or KDRT, or free TV like PBS or DCTV. Another caveat is that TV reception in Davis is mediocre at best. Only a large rooftop antenna or poweramplified bunny ears will work. However, once either of these is installed most network channels come in clear (with the exception of NBC 3).
  Internet access can be had for free at WiFi Hot Spots

