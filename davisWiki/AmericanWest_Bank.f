
AmericanWest Bank is a banks bank that is headquartered in Spokane, Washington.

This branch location was previously a Premier West Bank. Before that it was a Wachovia branch until Wells Fargo sold the branch to PremierWest Bank. Originally this location was a World Savings Bank branch until October 11, 2007.

AmericanWest Bank took over PremierWest Bank and rebranded the bank branches in 2013.

