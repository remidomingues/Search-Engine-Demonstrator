AIRR, now known as (Natives Empowering Through Unity and Education), was created by the Native American Student Union (NASU) to address the needs of the UC Davis American Indian population. AIRR is dedicated to reversing low graduation, recruitment and retention rates by utilizing a holistic approach to student development.

Efforts will include cultural identity workshops, support groups, community dialogue circles, retreats, and leadership development skills. 

AIRR is located in the basement of South Hall and is a part of the Student Recruitment and Retention Center.

