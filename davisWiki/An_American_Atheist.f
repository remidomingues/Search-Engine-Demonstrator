
An American Atheist is a radio show, podcast and blog featuring news and discussions about atheism and religious criticism. The show attempts to promote the concepts of critical thinking applied toward all subjects, especially the often taboo subject of religion, in an intellectual and meaningful way. Notably, panelists are largely UC Davis graduates, graduate students, or Sacramento Valley residents.

The show is also available as a podcast via http://anamericanatheist.org/category/broadcasts/ iTunes and http://anamericanatheist.org/?feedpodcast plain RSS.

