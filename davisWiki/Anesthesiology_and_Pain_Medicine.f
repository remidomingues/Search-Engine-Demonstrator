

Anesthesiology and Pain Medicine is a department of the School of Medicine.  They have research labs in Surge III as well as in Tupper Hall.

They provide anesthesia and pain management treatment for patients in all areas of the UC Davis Medical Center in Sacramento, which is where the bulk of the department is housed.
The department has an array of educational opportunities, including a threeyear residency program, several fellowships, and a clerkship for medical students.
