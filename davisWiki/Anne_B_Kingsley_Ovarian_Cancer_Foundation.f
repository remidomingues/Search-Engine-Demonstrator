
The Anne B. Kingsley Ovarian Cancer Foundation was founded in 2005 by Anne Kingsleys son, Robert Kingsley, to support research that helps in the early detection of ovarian cancer. The Davis Davisbased NonProfit Organizations nonprofit also supports graduate students working in gynecological oncology. Family and friends help with fundraising and managing the small foundation, in order to limit overhead costs.

