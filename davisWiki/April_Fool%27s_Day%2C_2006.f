 Super WalMart Coming soon 
In Davis on April 1st, 2006 (April Fools Day) some clever individual(s) placed a Super WalMart Coming Soon banner on the corner of Pole Line Road Pole Line and East Covell Boulevard Covell in the farmland across from Longs.  The sign was very professionally rendered and was in location of the proposed Covell Village development, which probably caused more than one Davisite severe distress.



