


AptMap, founded in Summer 2013 by UC Davis Senior Thomas So, provides apartment navigation directly on your mobile device, getting you where you need to go faster. AptMap gives you the ability to navigate around unfamiliar apartment complexes straight from your smartphone.The app is now available in the Apple Store since the start of February 2014.


AptMap expansion: Spring 2014

AptMap is currently looking into adding more apartment complexes through the entire city of Davis.








