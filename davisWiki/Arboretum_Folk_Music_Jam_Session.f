

The FOLK MUSIC JAM SESSION is a recurring events board event in the Arboretum at UC Davis.  Folk music musicians are invited to bring their acoustic instruments—fiddles, guitars, banjos, mandolins, squeeze boxes, you name it—and play together informally over the Restaurants lunch hour. All skill levels are welcome, and listeners are invited.  Check out the Arboretums website to find out if the jam session is happening this Friday or next!

