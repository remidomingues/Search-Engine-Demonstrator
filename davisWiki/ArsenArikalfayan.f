



A native of San Carlos, California, Arsen Ari Kalfayan earned his Bachelor of Arts degree in Political Science with minors in Communication and Global and International Studies in 2006 from University of California at Davis.

Upon graduation, he began his career with Fisher Investments as an Account Executive. After serving in positions of increasing responsibility, in 2008, Kalfayan was promoted to the Institutional Group where he served as a Materials Production Associate.

In January 2009, Okko Grippando and Kalfayan cofounded Reason to Party, dedicated to fulfilling the financial needs of charities in the San Francisco Bay Area benefiting underserved populations.

