

This gift shop sold highquality gifts, antiques, fine art, and did custom framing. Many of their art pieces and crafts were original works by established artists, including several international/world artists. They had rotating art shows and participated in the second Friday ArtAbout.

Some items were a bit pricey but it was definitely worth stopping by to check the store out if you were looking for a nice gift.  The employees were very nice and helpful. Who knows, they may have even offered you hot apple cider while you were there.
