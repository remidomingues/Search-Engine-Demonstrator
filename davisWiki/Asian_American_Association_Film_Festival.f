 2014 AAA Film Fest is Coming Up Soon! 



The Asian American Film Festival is a twoweek Film Festivals festival of films involving Asian Americans and Asian American issues, held annually in May. The Film Festival was launched in 2004 to showcase Asian and Asian American media.  It aims to break stereotypes and to provide an alternative view of Asians and Asian Americans not generally seen in the American mainstream.  Admission is free and open to the public.

