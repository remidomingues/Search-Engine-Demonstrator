This section of the The Greenbelt Davis Greenbelt connects to the Evergreen Greenbelt. It is located in West Davis north from Patwin School Patwin Elementary School and Arroyo Park. It goes from Arroyo Park through a tunnel under Shasta Drive, to Glacier Drive.  It is home to several benches, including the Lucille Youmans and Dixie Bench.

Features

  Lighted Bike Paths
  Town Wildlife Wildlife Viewing
  Dinosaur Park
