Audioversity in the Little Tomato (Six Decades of Music Associated with Davis, Ca) is the title of the mix CD given to some of the donors to the 2010 http://localwiki.org LocalWiki.org fundraiser. It was created by Rob Roy, who also created the Davis Wiki Dot Org Slash Music mix CD for the Davis Wiki Wacky Woo 1 2006 Davis Wiki Fundraiser. It contains music from artists who are either from Davis, lived in Davis, or attended UC Davis.

The Mix CD can be heard for free at http://soundcloud.com/davisca soundcloud.com/DavisCa


