Automated External Defibrillators (AEDs)  are life saving devices. They can be found in many public places throughout Davis and are in nearly every large building on campus.FootNote(http://www.dateline.ucdavis.edu/dl_detail.lasso?id13171) Nearly all CPR courses train you how to use them.

On December 22, 2010 a Tragic events tragic event was averted when a publicly accessible AED in the ARC was used to successfully defibrillate a man who collapsed while exercising.FootNote(http://www.dateline.ucdavis.edu/dl_detail.lasso?id13169)

