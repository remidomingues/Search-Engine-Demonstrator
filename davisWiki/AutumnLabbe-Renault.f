Autumn LabbeRenault is a Davis resident and longtime staff member at Davis Media Access, where she currently serves as executive director.

She helped found local radio station KDRTLP. 95.7 FM and is a community media activist & advocate. She hosts a radio show Wednesday mornings at 10 a.m. KDRT called Speakeasy, which profiles people doing good work in our community. She also writes a monthly newspaper column for The Davis Enterprise on community media & telecommunications issues.

Autumn is involved with Soroptimist International of Davis, the Unitarian Universalist Church of Davis, Birch Lane School Birch Lane Elementary School, and several other community organizations.

A mixedmedia artist and jewelry designer, she blogs under the name Autumn Light Designs.
