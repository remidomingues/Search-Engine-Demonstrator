
B.J. Kline was a former school board Davis School Board trustee who served from 2001 to 2005.  He lived in Southern California from 2005 to 2013, after which he returned to Davis.  In April of 2014 Kline announced his interest and availability to serve out the term of Nancy Peterson who resigned the month before.  He stated that he would not run for school board office in the November 2014 election.

