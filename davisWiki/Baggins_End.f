Baggins End is the name of the area where The Domes are located.  It is a reference to wiki:WikiPedia:Bag_End Bag End, home of Bilbo and Frodo Baggins in The Hobbit and The Lord of the Rings by J.R.R. Tolkien.  Elsewhere in town, (e.g., in West Davis,) there are several other Tolkien references in the street names.

