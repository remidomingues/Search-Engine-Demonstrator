
Baháí Grove is a stand of Mediterranean cypress trees just to the west of the Mrak Hall parking parking lot and south east of Physics and Geology.  Donated by the Bahai Club at UC Davis and ASUCDCFC in 1988.

The dedication plaque reads:

  The earth is but
  one country and
  mankind its citizens.
