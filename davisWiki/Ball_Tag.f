
The rules are simple:
  Define a playing area. Everyone stays in the playing area.
  Hand out the balls to a few people. Make sure you have more people than you have balls.
  If you have a ball, try to throw it at someone who doesnt have a ball. If you hit them, they have to take that ball as their own.
  If you dont have a ball, try not to get hit by one.
  Play for as long as you like.

Variations:
  Have only one person without a ball. (Imagine the chaos!)
  Allow people with balls to get hit. (So someone could have more than one ball at a time.)

The best place to play this game in Davis is at the Death Star.
