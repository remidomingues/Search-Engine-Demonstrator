
Beckett Hall Circle is a small campus road that juts off of California Avenue to the west.  It is named for the no longer existing Beckett Hall.

It provides an entrance to the Primero Grove complex and rear access to the old Cowell Student Health Center. The road ends not in a proper circle like Regan Hall Circle, but in parking lot 22 behind the Regan Residence Halls Regan Dormitories.

Beckett Hall Circle is the address residents of the Regan region would use if receiving mail during the school year.
