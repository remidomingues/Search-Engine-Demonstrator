A sugar filled plant usually grown for the edible, earthy root bulb.  Typically dark red in color, giving rise to the term beet red.  One variety is grown for the leaves rather than the root and is known as chard. Fresh beets are full of nutrients, and can be bought at the Davis Food Coop, the Farmers Market, and at some Supermarkets.

If you buy a bunch of beet leaves and theyre still fresh and crisp, try this recipe. Dont try this recipe if the leaves have wilted or yellowed beet leaves supposedly give off cyanide as they decay:

3 tbs. unsalted butter
1 medium red onion
1 tsp. sugar
1/2 lb. beet leaves, stems removed
1 tbs. balsamic vinegar
1/2 tsp. kosher salt
1/2 tsp. white pepper

melt butter over medium heat. add onion, increase heat, and saute until it browns. add sugar and increase heat to hight. saute until onions caramelize, about 2 minutes. add beet leaves, and stir over high heat, 12 minutes. add balsamic vinegar, salt, and white pepper, cook for 1 minute to reduce the sauce.




