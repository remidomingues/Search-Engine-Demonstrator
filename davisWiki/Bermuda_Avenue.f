Address(Bermuda Avenue) is a residential Street in West Davis. If you drive to the end you arrive at a large field that stretches the entire distance between Covell and Russel. There are views to the Coastal Range and this can be an excellent place to watch the Sunset. In recent years the farm in view has grown Sunflowers.

