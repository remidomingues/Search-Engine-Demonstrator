Bernard Rogers died on July 1^st^ 1909 at his home. He was 57 years, 5 months and 21 days old. He had arrived in Davis about 31 years prior from Ireland.

He was married to Miss Mary Ruane 31 years. Prior to her death in San Francisco and they had seven children:

John T. Rogers was a ranch foreman at the University farm
Bernard Rogers Jr. was a clerk for J. B. Anderson in his general merchandise store
Miss Loretta Rogers was a stenographer for the Schmeiser Manufacturing Company.
