
Beverages and More!, better known as BevMo, is your source for any and all things alcohol.  Large beer selection, larger wine selection, and a good selection of other alcoholic beverages.  They also have brewing kits if you want to make your own.  Throw in a bunch of cheeses, chips, and other snacks and accessories, and you have what the name of the store describes.  If you like cigars, they have a few of those as well.

There is another location in Arden, but the Natomas location (linked above) is closest to Davis, just off the Truxel exit of I80.


