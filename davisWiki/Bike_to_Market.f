Ride Bikes to the Farmers Market on May 9th: Bike Basket Installation ($5) + Free Sale

Ride your bicycle to the Davis Farmers Market to participate in Bike to the Market day, as part of Mayisbikemonth festivities. Bike baskets will be offered while supplies last ($5 donation). There will also be a Free Sale, so bring along unwanted goods to offer as gifts to your fellow community members. All items/gifts welcome all untaken items will be donated. Contact Rainbow at sevacycle@gmail.com if you would like any assistance transporting goods to the market.


Event sponsored by Davis Bicycles! to encourage cycling in Davis
