
Bill Kendrick first lived in Davis from 1998 to 2004. In that time, he helped cofound and run the LUGOD Linux Users Group of Davis, founded and ran the shortlived Davis Game Design Club. He moved to the Bay Area for a while, and returned in 2007 after his son, William, was born.  He can often be found chasing William around the Davis Food CoOp, Alphabet Moon, and Mishkas.

Along with his day job (currently the CTO of http://www.smashwords.com/ Smashwords) and being a parent, he also writes open source software (most notably, http://www.tuxpaint.org/ Tux Paint).


 
