

Bill Emlen was hired officially as the city manager of Davis in mid April of 2006. He served as interim city manager after Jim Antonen was let go in January 2006. Before that, Emlen served as assistant city manager.

During his stint as Director of the Community Development Department, he http://www.davisenterprise.com/articles/2006/03/10/news/2020/2020part1davis.txt was quoted as saying: We have a pretty good idea that the citys not going to change that much by 2020.

On August 25, 2010, Emlen announced that he is http://www.davisvoice.com/2010/08/anexitinterviewwithcitymanagerbillemlen/ resigning to take a new job in Solano County.  His last day in office will be September 24th.

