This program is offered under the umbrella of the Molecular and Cellular Biology department at UC Davis. MCB itself is in the College of Biological Sciences. Its courses overlap with the Genetics and Cell Biology programs. Many people who are interested in computational or theoretical molecular and cellular biology minor in Quantitative Biology and Bioinformatics. The advising center for the major is located in 156 Briggs Hall Briggs. Phone: (5307529032)

 From the 20042005, 20052006 General Catalog 

The Biochemistry and Molecular Biology major introduces students to the chemistry of living organisms and the experimental techniques that are used to probe the structures and functions of biologically important molecules. Students who both enjoy both chemistry and biology and who are comfortable with quantitative approaches to problem solving will find this major a rewarding field of study.

