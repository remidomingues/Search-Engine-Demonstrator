
The Bird Path Zen Community of Davis is a spiritual organizations spiritual organization that meets for meditation weekly, and also meets informally for study groups and social events. The style of practice is patterned after that of the Diamond Sangha in Hawaii and one of its affiliates, the Ring of Bone Zendo located near Nevada City. This style of Zen Buddhist practice has its roots in both the Rinzai and Soto schools. Although this describes the groups primary affinities, the doors are open to all.

