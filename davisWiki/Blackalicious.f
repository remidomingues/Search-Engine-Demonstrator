
Blackalicious and other members of the Quannum Crew graduated from UC Davis. DJ Shadow a member of Quannum actually graduated from Davis Senior High in the early nineties. Before they became famous, they had radio shows on KDVS where they perfected their style.  Quannum Projects was formerly known as SoleSides.


