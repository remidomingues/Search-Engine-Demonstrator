The Bloody Mary is one of the finest morning after drinks ever invented. It restores precious bodily fluids and purity of essence sapped away by the previous nights encounters with cheap Forties forties and vodka drinks made with less artistry. Of all the drinks served at all the Bars bars, the Bloody Mary deserves an ode.


Here are two popular ways to make a Bloody Mary:

 Way #1 
Ingredients

   1.5 oz vodka
   3 oz tomato juice
   0.5 oz lemon juice
   7 drops Worcestershire sauce
   3 drops Hot Sauce Tabasco sauce
   freshly ground pepper
   1 dash celery salt, freshly grated (how is any kind of salt freshly grated?)

Serving Instructions

Combine ingredients in glass filled with ice. Stir gently. Serve the Bloody Mary in a pint glass. Garnish with a stalk of celery, pickled asparagus.

 Way #2 
Ingredients

   1 quart Beefamato or Clamato juice
   Juice of one lemon
   1 teaspoon Worcestershire sauce
   1 teaspoon Tabasco brand hot sauce
   1 tablespoon prepared horseradish
   1/4 teaspoon garlic salt
   1/4 teaspoon celery salt
   Freshly ground white pepper
   1 and 1/2 cups pepperflavored or plain vodka
   Lemon or lime slices for garnish

Serving Instructions

Mix all ingredients in a 2quart pitcher. Stir until well blended. Pour into icefilled glasses, and garnish with lemon or lime. Or a stalk of celery!

Makes 6 to 8 servings.


