Bob Buckley served as Trustee on the Davis JUSD School Board from 1985 to 1991.  He was originally appointed to fill out the remainder of Ernie Pfanner Ernie Pfanners term when the latter was elected to the Yolo County Board of Supervisors in March of 1985.  He was elected in his own right in 1989.  During his tenure on the school board, he was a lecturer in computer science.


