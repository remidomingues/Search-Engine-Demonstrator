
Bob Gonzalez taught mostly band classes in the Davis schools from 1971 to 2003.  He was born and grew up in San Antonio, Texas and arrived in California in the 1960s.  From 1980 to 1986 he was band director of Davis Senior High School Davis High School and taught band at Holmes Junior High School for the latter part of his career.  He received the Harmony in Our Lives Award in 2004.  He plays trombone in various local groups.

