
Bob Hagedorn served on the Davis Planning Commission, and served as Planning Commission Chair in 2003. In 2006 he announced a run for 2006 City Council Election City Council but pulled out of the race on March 8th.

Since 1992, Bob has been representing California consumers at the California Department of Insurance, where he is currently an Assistant Chief Counsel.

Bob has a long history in environmental and consumer protection. He was a consulting attorney to Earth Day 1990. He was an attorney with, and later the acting Executive Director of, Earth Island Law Center, which was founded and run by David Brower, former Executive Director of the Sierra Club.

