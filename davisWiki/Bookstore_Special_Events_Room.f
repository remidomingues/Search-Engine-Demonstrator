

The Bookstore Special Events Room is an area in the Memorial Union that the UC Davis Bookstore uses to buy back textbooks and process textbook returns at the start of each quarter. During other times, special sales may take place to sell excess lowselling inventory at discount prices. The room goes unused the rest of the time.

The area used to be a small lounging area with soft seats to sit on before it was converted into an actual room for the bookstore to use.
