Botany is the study of plants.  Davis not only has a wide variety of town flora for casual fans of our green friends, theres also the Plant Biology department at UC Davis and the Botany Club.  The university is host to the Center for Plant Diversity, and there is the Davis Botanical Society who support Botanical Conservatory and Herbarium.


Regionally, the Sagehen Creek Field Station is used for botanical studies within the UC System.
