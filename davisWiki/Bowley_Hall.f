

Address(Bowley Hall, 38.538476850150,121.763931984009) was completed in October 2003 and is used for many classes offered by the plant biology and plant sciences departments. The hall is a bit remote from the center of the UC Davis campus proper, and is located in the University Extension Center as apart of the Plant sciences Teaching Facility

The hall was named after the late D. Gould Bowley and his widow, Virginia Bowley.

It has free parking it seems, though not terribly useful to the average student.


