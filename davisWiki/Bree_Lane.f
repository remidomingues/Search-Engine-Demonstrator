Bree Lane is one of the many Navigating Davis streets in Davis Village Homes neighborhood that are named after places or characters in J.R.R. Tolkiens books, The Hobbit and The Lord of the Rings.

Bree Lane branches off from Creekhollow Lane.

Bree is the town of the Hobbits where Frodo, Sam, Pippin, and Merry first saw the Nazgul (the dark riders of Sauron), and where Frodo accidentally slipped the ring on his finger and became invisible, revealing his identity.
