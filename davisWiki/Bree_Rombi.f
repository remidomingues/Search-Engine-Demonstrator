PLATFORMS:

UPDATE THE ASUCD WEBSITE TO INCLUDE A STUDENT CALENDAR
We need to make the ASUCD website more user friendly and create easier access. This student calendar will be the goto spot for all students to find out what is going on for each and every day. It will have pictures, each item will be colorcoded, and you can click to filter out information.
Every election people come in with good ideas like this, and little follow through. This is a good example of how to waste money, but this is also very easy to fix. I am tired of seeing little resultsI want to be the one to change this.

BRING CLASS READERS TO CAMPUS COPIES
Everyone knows what a pain it is to go off campus to buy yet another book for class and how expensive it can get. It would be much cheaper to buy it on campus from our very own Campus Copies. This would be easier for students and it would create more student jobs. I have a lot of experience with this project because I helped previous senator Rebecca Schwartz start this project. I plan on working with the professors and I want to make the move to permanently have all readers made at Campus Copies.


KEEP UC DAVIS COMPLETELY ENVIRONMENTALLY FRIENDLY
We all see the different recycling trashcans on campus, but what about composting? We do very little composting on campus, and we actually ship huge amounts every day. I want to work to keep our carbon footprint as low as possible and to bring composting back to campus.



I am also advocating a stance against student fees and our classes being cut. I think it is ridiculous to increase our tuition, and I will fight against it!


