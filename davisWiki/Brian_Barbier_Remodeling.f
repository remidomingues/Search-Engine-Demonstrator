


Brian Barbier Remodeling is a small, fullservice residential Home Improvement & Repair remodeling company that specializes in Sustainability Green/Energyefficient housing modifications. They specialize in Kitchens and bathrooms but also do whole house renovations and small additions. They have been servicing the Davis community for a bit over 6 years.

They are a member in good standing with the Better Business Bureau.  They tout that they work with their clients from design through completion to create a space that meets all of the clients needs, and in doing so, achieve the latest standards in Green Building and Energy Performance.

Brian is no longer doing remodels (as of 7/2012).

