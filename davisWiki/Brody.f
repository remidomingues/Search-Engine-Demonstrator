

Brody was born in wiki:wikipedia:Chico,_California Chico, California. He went by the name Tubby, Sprinkles, and etc, before he finally received the name Brody. Brody enjoys posing for the camera and lounging in front of the fan. He also likes to chew on Apple Inc. Apple cords and hide under mattresses. He lives in a beautiful, two story house that he occasionally chews as well. He likes to be petted but if he doesnt want to be held, he will try to escape and maul your chest. Give him a raisin and he will love you. Brody needs a friend.

