

Brownies are a younger scouting division for grades 13 or ages 68.  They (like Daisy Scouts) are a distinct group within and part of Girl Scouts, with their own ranks, uniforms and leadership.

Promise
  On my honor, I will try:
  To serve GodFootnote(A member may choose any word they wish to replace God) and my country,
  To help people at all times,
  And to live by the Girl Scout Law.
