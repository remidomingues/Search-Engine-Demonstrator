Address(Bryce Lane) is a residential street West Davis. At the west end of the street you can access the Bike Paths bike path that runs along the West Pond.  At the east end youll find the Aspen Greenbelt which runs from Glacier Drive to the north down to Patwin Elementary School in the south after crossing under Shasta Drive.

  Connects to Isle Royale Lane
  Crosses Denali Drive

