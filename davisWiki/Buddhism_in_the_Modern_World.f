
The mission of this club is to bring Buddhist perspectives into contemporary issues through openminded dialogue and daily application. Open to all students, nonstudents, religious, and nonreligious. Regular oncampus activities include studies of texts from both Buddhist and nonBuddhist scholars and discussions on the effects of actively seeking and applying their teachings. With these activities, we aim to promote harmony at the individual, campus, and larger community levels.

Come join our weekly meetings and feel free to ask any questions through our Facebook site or email!

