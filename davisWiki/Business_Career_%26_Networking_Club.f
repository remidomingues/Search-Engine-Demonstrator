


The Business Career and Networking Club (BCNC), founded in Fall 2013, provides professional development and careernetworking opportunities to UC Davis students. By offering a careernetworking platform for the businessinterest groups around our campus and community, we can provide our members with the resources to develop the skills and knowledge necessary to prepare for employment in their respective professional industries upon graduation.

Throughout the academic year, BCNC hosts several events each quarter. Students of all majors and organization affiliations are invited to attend.

