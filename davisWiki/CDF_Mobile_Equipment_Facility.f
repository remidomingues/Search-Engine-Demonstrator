

This is the huge yard of big red fire trucks that can be seen from Interstate 80 along Chiles Road.  This facility is responsible for the design, acquisition, maintenance, major repairs, replacement and disposal of all 3,000 vehicles owned by the California Department of Forestry and Fire Protection.

A couple times a year they hold a big auction at this facility where you can buy retired fire equipment. For information contact the Office of Fleet Administration at (530) 7571063.

This facility is located next to the L.A. Moran Reforestation Center.

