This page contains Cascading Style Sheets (CSS) that control the look of your wiki.  You should be familiar with CSS before changing these files.  To change the look of your wiki, change the following CSS files on this page:

   File(style.css)  The CSS file for controlling colors, fonts, and the like.
   File(layout.css)  The CSS file which controls the margins, paddings and the like.
   File(common.css)  The somewhat general CSS file that you most likely wont need to change.

See the wikispot page on wiki:wikispot:customizing wikis for more information.
