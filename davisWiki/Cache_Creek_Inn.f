
The Cache Creek Inn is a hotels bed & breakfast near Davis (in Rumsey). It is a lovely craftsman style inn with full wraparound porch, surrounded by large grassy yards with a hot tub and towering valley oaks.

Bedrooms are large and sunny with private bathrooms.

In the winter, fireplaces make the lobby cheerful. In the summer, rafting, kayaking and swimming holes and picnic spots are nearby on Cache Creek.

Hiking trails are just up the road, leading to frog ponds along creeks. Birders will like the bluebirds, several species of woodpecker, and others. The coastal hills surround the property.

The Cache Creek Inn accepts Davis Dollars community currency.

