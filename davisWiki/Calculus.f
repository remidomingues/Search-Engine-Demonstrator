 Calculus at UC Davis 
The UC Davis Mathematics math department offers three calculus series to its undergraduates.

  The MAT 16ABC series is general calculus meant for people who will take no more math.
  The MAT 17ABC series is general calculus with applications aimed at biology and premed students.
  The MAT 21ABCD series is general calculus designed for scientists, engineers, mathematicians, and people who will deal with lots more math in their education.
  MAT 25 is Advanced Calculus. Its a lower division real analysis course that replaced MAT 127A. This covers all the hows and whys that were handwaved away in your other calculus classes.
