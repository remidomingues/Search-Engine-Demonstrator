

California Calisthenics  Davis is a free exercise workout group that meets at Cesar Chavez Elementary School Mondays and Wednesdays from 57. They welcome all skill levels  from people that are fighting for their first pull up to people that can hold advanced skills  everyone is welcome!

https://www.facebook.com/groups/617816148253369/ is their facebook group, and it is recommended to join if you are interested, as they post what times and exact locations they will be meeting.

Their website is also a great place to check out cool videos, routines, exercises and personal training options

Youtube Channel: https://www.youtube.com/user/DavisCalisthenics




