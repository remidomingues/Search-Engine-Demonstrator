

Though mapping programs will suggest taking Interstate 80 to get to Wine Country wine country, if you have the time, the brakes, the tires, and the driving skill, take Russell Blvd. all the way west. Its quite the mountainous drive, but its a pretty Adventures Outside of Davis adventure to take.

Calistoga has a variety of B&B establishments for wine country tourists.  Its a historical spa town, and you can still soak in mud baths there.  Downtown Calistoga has little touristy artsy shops, restaurants, and an ACE Hardware.  Theres also reasonably tasty sushi restaurantbe prepared to spend Wine Country Prices (2x Davis) for items.

If youre looking for food in Calistoga, try the BBQ shop at Lincoln Ave/Highway 29.
