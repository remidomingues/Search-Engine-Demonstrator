

Calling Morocco is a Music Scene local band that has been described as alteverything, pulling influences from rocknroll, altcountry, pop and punk rock. Their debut album Outside Providence was released in May 2011 to excellent http://callingmorocco.wordpress.com/press reviews. The group is composed of:

 Kyle Olson  Vocals/Guitar
 Jason Chase  Drums
 Zak Norlyn  Bass
 Ziad Younis  Guitar

Occasional members:

 Max Hart  Pedal Steel/Keys
 Users/JesseMiller Jesse Miller  Keys

To hear the bands album, Outside Providence, you can stream it on http://callingmorocco.bandcamp.com their bandcamp page.
