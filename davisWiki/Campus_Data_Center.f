
Directly south of Kemper Hall lies the Campus Data Center.  It houses many of the important servers used by students, staff, and faculty at UCD, supporting the infrastructure for the administrative, academic, and research computing.  This includes the UC Davis Email email servers, web servers, MyUCDavis, Course Management (Smartsite or Sakai), DNS, a ntp.ucdavis.edu time service, and a bunch of other services that are the core service infrastructure supporting all computing at UCD.  Theyre a part of the Information and Educational Technology department at UC Davis.

As of Jan 2010, smartsite, the campus student portal for classroom materials, has been outsourced to an outside vendors.

They used to house the public isun.ucdavis.edu unix service and Usenet news on campus.  Both of those services have been retired.

Beginning June 2009, the Data Center began offering virtual server colocation to campus departments.  This service runs in a highly available environment using VMWare vsphere .  Pricing and other information is available at http://virtualization.ucdavis.edu

The Data Center uses approximately 350kw of power and has 90 tons of cooling to dissipate the heat load generated by the equipment inside.  It has 60GBit of network connectivity to the campus backbone.



