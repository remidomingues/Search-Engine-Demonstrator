Campus Parking is orchestrated by TAPS. There are several parking permit types:


http://taps.ucdavis.edu/parking/permits/rates.cfm Official TAPS site: Parking Rates

 Primero Grove parking permits are only 3 dollars if you are a resident.
 An M permit can be used with your car during night parking.
  All quarterly permits went up three dollars a day at the beginning of FY 200506.
 UCD Parking permits are valid at other campuses as well.  Although I havent tried it on a weekday, evening and weekend parking on other UC campuses is free with the UCD permit.  I learned this years ago, and havent tried it recently, but it should be valid. Users/RogerClark RC
