We got a tour of the campus winery today in Classic UC Davis Courses Introduction to Winemaking.

 The Teaching Winery 
The teaching winery is used to teach volume production of wines. Its still low volume, but much bigger than the few gallons produced at a time in the research winery.


 The Research Winery 
The research winery is used for smaller scale research production of wines. A lot of small (23 gallon) samples of wine can be produced in parallel here.

