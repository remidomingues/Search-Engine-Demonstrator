
The Career and Graduate Study Resource Center (CGSRC) is a joint venture between Advising Services and Internship and Career Center. The ICC maintains a modest, though very useful, library of career and jobrelated book titles and print resources. Additionally, the CGSRC has numerous volumes with a goldmine of information about graduate, professional, and health science schools and programs, and test preparation materials for MCATS, GREs and other standarized tests.

At the end of Spring quarter 2006 the CGSRC received a bit of a makeover, and now has ten internetenabled computers to aid students and alumni in their pursuit of career and postgraduate study data.

Its also a very popular place to study.

