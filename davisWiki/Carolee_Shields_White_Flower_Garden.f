Image(photo.jpg, thumbnail, 300)The Carolee Shields White Flower Garden and Gazebo is a theme garden based on medieval moonviewing gardens of India and Japan. With its curving paths framing a vinecovered gazebo, the garden is a popular site for weddings and other events. Many of the plants here are fragrant, and their pale flowers are particularly luminous by moonlight. The garden is named for Carolee Shields, an avid gardener and the wife of Judge Peter J. Shields, one of the founders of the UC Davis campus. The Carolee Shields White Flower Garden is located in the arboretum and surrounds the Arboretum Gazebo gazebo.





