
For years people have moved the Address(painted rocks, 38.562892, 121.638409) at the south side of Interstate 80 where the levee for the Yolo Bypass provides a sloping message surface. When the weeds are growing high along the freeway it is harder to see the messages or initials that are left there.  Fraternities and Sororities are the usual rockarrangers, though Banduh other groups frequently interject their own formations.

