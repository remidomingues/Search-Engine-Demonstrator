
Cedar waxwings are the species B. cedrorum and live all over the United States and sometimes in South America. They can be seen yearround in Davis. They eat berries, fruit, and insects. According to Wikipedia, they make very highpitched whistles and buzzy trills often represented as see or sree.

A tree between the Wells Fargo parking lot and the Davis Enterprise lot is full of these cute lil birdies.



