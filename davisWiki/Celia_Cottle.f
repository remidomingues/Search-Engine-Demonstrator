Celia Cottle is director of a number of student jazz groups in the community, including the Jazz Cats, Sax by Popular Demand, and Jazzmatazz.  She began teaching clarinet and saxophone lessons locally when she was an undergraduate at UC Davis.  She is also the 2003 recipient of the Harmony in Our Lives Award.

Source:

Ryen, Maddy; 2003 Oct. 28, Music as her way of life, Davis Enterprise, pg. A11.
