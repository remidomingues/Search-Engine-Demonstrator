The Central Heating & Cooling Plant provides steam and chilled water to the campus. The plant is located north of Tercero along Dairy Road. It is one of the facilities that is staffed 24 hours a day 365 days a year on campus.

 Steam 
Steam is used for heating, making hot water, and autoclaves in many core campus buildings.

 Chilled Water 
Chilled water is also produced and stored at the Chilled Water Facility. Much of central campus cooling takes advantage of the chilled water loop. Some operations on campus also use it for other process cooling.
