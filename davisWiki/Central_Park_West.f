
Central Park West is the name for a set of seven new residential dwelings right across from Central Park.  Aside from name, the housing development has no official connection to Central Park.

The homes that were originally at this location were donated to the Solar Community Housing Association and moved to 3rd and J Street.  The buildings are being constructed by Sherman Construction Co.

