Rev. Chad Van Schoelandt unsuccessfully ran for ASUCD Senate both on the Friends Urging Campus Kindness slate in the Winter 2005 ASUCD Election and on SOSSS in the Fall 2004 ASUCD Election.

He was  on the Elections Committee ASUCD Elections Committee from Fall 2005 to Spring 2006.  He was also a member of the IAC ASUCD Internal Affairs Commission before being forced to resign due to his position onthe Elections Committee.

Some refer to him as Hanging Chad van Schoelandt, by analogy to the relevance of hanging chad to elections.

Chad was a controversial figure on campus and in ASUCD, but was a driving force for quite a bit of change with UCDs student government. From new campaiging techniques, to legislation to wellrun elections, Chad left a positive, if not often controversial, legacy.

Chad was also a cofounder of both Friends Urging Campus Kindness and SOSSS Students for an Orwellian Society Slate. He recruited Rob Roy to run for ASUCD Senate.

