
The http://groups.yahoo.com/group/chaistudents/ Chai Society of Jewish Life promotes Judaism Jewish identity and awareness at UC Davis.

Chai Club is a group of students trying to make their college experience also be a Jewish experience and continue practices and traditions their ancestors have been doing for thousands of years. Working along with Chabad of Davis Chabad, this club shows college students fun and exciting ways to practice their Judaism. Free food is offered all the time and events take place all the time.
