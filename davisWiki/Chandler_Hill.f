Chandler Hill currently serves as President of Theta Chi Fraternity and Business Manager for KDVS 90.3 FM.

He was a candidate for ASUCD ASUCD Senate Senate in the Winter 2013 ASUCD Election running on the NOW Slate. Chandler ran on a platform of of fundraising for the ASUCD Scholarship and establishing a UCD Student Credit Union/Bank. He placed 8th out of 19 candidates and did not gain a senate seat.

