Charles Bamforth, Ph.D., D.Sc. is the AnheuserBusch Endowed Professor of Malting and Brewing Sciences and former chair of the department of Food Science & Technology. He is most known on campus for FST 3, a course about beer that he created.

He claims that chemical engineers have no soul.

In Fall 2010 Bamforth was named Brewmaster General by Playboy Magazine as reported by http://theaggie.org/article/2010/10/07/ucdavisprofessorhonoredbyplayboy The Aggie

