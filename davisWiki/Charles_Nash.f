Charles Presley Nash (b. 1932, d. 2007) was a professor emeritus of Chemistry at UC Davis.  From his arrival in Davis in 1957 and well past his retirement, he was an significant and active member of the University community.  He was a coauthor and signatory of the original 1990 Principles of Community.

