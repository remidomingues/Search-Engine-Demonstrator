
The  Chemistry Club is an Student Organizations organization of undergraduate students, run by the club president and vice president, and supervised by the chemistry club faculty advisor. The purpose of the chemistry club is to raise public awareness and to stimulate interest for chemistry and the sciences with chemistry demonstrations and through interactions with children of all age groups and adults.

They have events throughout the year and of course their annual Picnic day show, the Chemistry Club Magic Show, which packs 194 Chem every year during Picnic Day.

All upcoming meetings and shows are posted on http://chemgroups.ucdavis.edu/~osterloh/chemclub/home.htm their website.



