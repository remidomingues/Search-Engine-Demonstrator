During Picnic Day, the Chemistry Club at UC Davis puts on a magic show at 194 Chemistry.  The show is popular enough so that it is usually sold out and tickets are given out well in advance.  Various oscillating reactions, luminescent substances and explosive mixtures are displayed in a theatrical manner with a bit of science to explain what is going on.

Probably the best bang for the buck at Picnic Day.


