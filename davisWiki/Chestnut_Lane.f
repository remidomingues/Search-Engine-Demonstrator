Address(Chestnut Lane) runs from East 8th Street to Pole Line Road. On the north side of Pole Line Road is Chestnut Place.

A residential street with its own Chestnut Park park, it features large trees that provide a lot of shade. The homes in this Neighborhoods neighborhood generally had flat roofs and similar construction, but there has been a lot of Home Improvement & Repair remodeling over the years.

