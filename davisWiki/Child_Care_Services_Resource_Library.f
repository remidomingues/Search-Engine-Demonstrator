

The Child Care Services Resource Library is a service of the City of Davis that contains an extensive collection of books, CDs, and videotapes on parenting issues, child development (birth through teens), activities to do with children, choosing child care, special needs, and much more! It has moved to the Yolo_County_Library__Mary_L._Stephens_Branch Mary L. Stephens branch of the Yolo County public library along with the Davis Toy Library.

