
Chucks Place is a place for horses and the people that love them.  Love to ride them, that is: there are lots of trees and trails to ride. There are trails starting at the back of the property that go for miles. You can also ride the fields and roads, pretty quiet area. Plenty of places to ride.

They also have a friendly, lowkey barn. Trailer parking and tack storage available and special needs and retirement horses are welcome. They board stallions and provide breeding paddocks to pasture breed your stud, although prices are different from regular boarding and are separate from other boarders).

They are located about 8 minutes from campus on CR 96 between CR 31 (Covell) and CR 29.
