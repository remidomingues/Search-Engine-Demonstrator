

Chuck the Mover was Davis only small job mover.  He had no hour minimum, and would work on weekends.  All the other movers were from Sacramento and had a four hour minimum on all jobs, and did not work weekends.  Because Chuck the Mover could be hired for any amount of time, and because he worked on weekends, Davis residents loved to hire Chuck the Mover.  Now that he no longer exists, hopefully someone else will fill his shoes for small weekend moves.  Those who knew Chuck the Mover, hired Chuck the Mover.

> With Chuck the Mover, it used to be NO JOB TOO SMALL  Now its NO JOBS AT ALL <

