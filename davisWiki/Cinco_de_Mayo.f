Cinco de Mayo, Spanish for 5th of May, is a popular holidays holiday ostensibly celebrating the May 5, 1862, victory of the Mexican army over the French at the Battle of Puebla.  In the U.S., it has become a holiday analogous to St. Patricks Day.  That is to say, some people celebrate culture and heritage, while other get alcohol plastered and yell happy Drinko de Mayo downtown at passersby.  Many Mexican Food Mexican restaurants offer drink and/or food specials, and most of the bars in town do a great deal of business.

In 2011, Yolo County law enforcement stepped up enforcement.  Davis police made two DUI arrests and two controlled substance arrests.  Moral of the story?  Celebrate safely.
