
The UC Davis Police Department is offering a free Citizens Academy program during Spring Quarter 2011. This nine week (Wednesdays April 6th through June 1st; 3:00pm  5:00pm) course will offer:

  Community & Police Relationships
  ArrestSearchSeizure
  Defensive Tactics
  Lethal Weapons
  High Speed Chase
  High Risk Stops
  ShootDont Shoot
  Community Oriented Policing

In order to take part in this, you need to get a Livescan and pass a California Department of Justice background check. UCPD picks up the tab for this, though.

The City of Davis Police Department also offers a Citizens Academy, with slightly different subject matter. http://cityofdavis.org/police/cpa.cfm
