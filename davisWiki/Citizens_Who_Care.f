

Citizens Who Care provides social support services to senior citizens older adults and their family caregivers through In Home, Convalescent Hospital and Time Off for Caregivers programs. These services are provided to residents of Yolo County and neighboring communities by trained, caring Volunteer Opportunities volunteers with the support of our professional staff.

Programs include:
  InHome: provides a break to caregivers and/or a friendly visit to an isolated elderly person
  Convalescent Hospital Visitation
  Time Off for Caregivers at the Davis Senior Center
  Pet visits

Citizens Who Care also offers two very popular fundraisers: the http://citizenswhocare.us/Concert.html Winter Concert each February, and a rousing Beerfest each June.
