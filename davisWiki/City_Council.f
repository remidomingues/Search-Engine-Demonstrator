 History 
The current form of city government was decided upon in 1928. Prior to that, the town had a president and board of trustees rather than a city council and mayor.

On June 21, 1943, the Davis City Council approved a resolution supporting the mass removal of citizens of Japanese descent. They were transported to Tule Lake, California and imprisoned in a Japanese Internment Japanese Internment camp.

