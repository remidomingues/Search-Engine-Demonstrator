

Davis is governed by a fivemember council elected atlarge (there are no council districts). All city departments must answer to the council, and the council directs the actions of city staff.  In short, when it comes to the City of Davis, they run the show.

The mayor presides at all meetings of the city council and is the official head of the city for all ceremonial purposes; the mayor pro tempore carries out the duties of the mayor in the mayorâ€™s absence. City Council elections are held every two years, with councilmembers being elected to fouryear terms.  By ordinance, the person who receives the greatest number of votes in a council election serves the first two years of his or her term as Mayor Pro Tempore and the next two years as Mayor.

To submit an issue of concern to the City Council, either attend a regular meeting and speak out during the public comment period or submit your concern via their http://www.city.davis.ca.us/crm/ website. Public comment is accepted at the beginning of every meeting for items not on the agenda. For items on the agenda, public comment is accepted between the discussion and vote for that item. Public comment is generally limited to three minutes per person, but the council sometimes makes exceptions for people who have a large amount of expertise on the topic at hand.

The Davis City Council holds regular meetings beginning at 6:30pm on Tuesdays, except on the fifth Tuesday of any month and Tuesdays prior to a holiday. The council meets in the Community Chambers at City Hall. Meetings are televised live on the City Government Channel (Comcast channel 16) and are replayed the following Wednesday at 9am, Thursday at 7am, 1pm, 7pm, and Saturday at 1pm. Streaming video (live and archived) is available http://archive.cityofdavis.org/media/ online for additional convenience.

