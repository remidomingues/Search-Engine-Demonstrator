

Located in the old http://ark.cdlib.org/ark:/13030/tf4779n7fp/ High School building, Davis City Hall is home to City Council and City Services. In the parking lot, there is a drop box for City Services payments if you dont feel like Post Office mailing it.  City Council and other meetings usually take place in the Community Chambers.

It contains the plaque honoring those who have been awarded the Davis Citizen of the Year.

 

At the southwest corner of the block is the Soroptimist Bench Soroptimist bench.

