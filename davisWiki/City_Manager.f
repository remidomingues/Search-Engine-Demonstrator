
The job of the City Manager is to coordinate the overall administrative activities and operations of the city; and to advise and assist the City Council, exercising independent judgment and initiative.  (Reference:  http://www.city.davis.ca.us/cmo/jobspecs/11000_City_Manager.pdf Position #11000 job specification PDF from the City of Davis web site.)

The current city manager is responsible for the http://sacramento.cbslocal.com/2012/06/08/daviscutscitytreetrimmerscitizensconcerned/ dismissal of the citys only tree crew, with no apparent plans regarding their replacement. http://www.davisenterprise.com/localnews/city/citytrimsnineemployeesfromparkspublicworks/ It would appear that he intends to cut even more positions within the citys ranks, resulting in an even greater reduction in the citys ability to provide necessary services. The official line is that these peoples pay will go towards money the city owes the Davis City Employees Association for errors made during a labor dispute.

The City Manager http://agency.governmentjobs.com/davis/default.cfm?actionviewclassspec&classSpecID60445&agency1226&viewOnlyyes is paid $158,700 annually.

