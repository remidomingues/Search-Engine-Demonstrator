This guide outlines all of the classes available at UC Davis for a given quarter. Information regarding classes includes: meeting times, class locations, enrollment capacities, and final examination times.

Since classes, its locations, and its times will change every quarter, this guide is printed every quarter, compared to the General Catalog which is printed every two years.

See http://registrar.ucdavis.edu/csrg/ to view the guide online.  The site also lists updates to the CSRG that happened after press time.  If youre registered in the class, youll see these changes on http://sisweb.ucdavis.edu/ SISWeb, anyway.

Organizing your class schedule can be hard and overwhelming considering the number of classes available, which is why a site like http://siscast.com can be very useful.
