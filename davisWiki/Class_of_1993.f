The Graduating Class of 1993 at Davis High Schools Grad Night theme was 93 Rocks the World!.

The Spring Musical that year was Brigadoon.

Every year in the year book the entire graduating Senior class poses for a group picture.  For many years, that picture was taken near the concrete slide in Community Park near the softball field, public restrooms and playground.  When this area was converted into the Public Skate Park, This was no longer possible.  The Class of 1993 was the first class to have their group picture taken on the Halden Field Home bleachers instead.

