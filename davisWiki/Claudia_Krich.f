Claudia Krich taught in the Davis elementary schools from 1990 until she retired in 2010.  She led the Willett Elementary School Willett Elementary Choir from 2001 during that time, and continues to do so after retirement.  She received the Harmony in Our Lives Award in 2010 for her work in school music.

