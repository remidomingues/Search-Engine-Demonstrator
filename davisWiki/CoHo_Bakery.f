
The CoHo Bakery  (short for Coffee House Bakery) located in the Memorial Union sells Cookies, Pastries, and Baked Goods freshly baked goods, such as chocolate chip cookies, carrot cake, oatmeal raisin cookies, tatros, sugar cookies, bozo bars, berry streusel, strawberry and chocolate cake.

The Bakery also sells fruit, soda, ice cream, Odwalla, coffee, tea, and hot chocolate.  Its very important to note that they have a tradition of making coffee half off during finals.  They portray this as a way to help out those studying students who burn the midnight oil.

In the morning only, they serve croissants and muffins.

