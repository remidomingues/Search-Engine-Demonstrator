CoHo Live is a live performance venue featuring mostly singersongwriters, but can accomodate most kinds of performances (except bands).  Its the perfect way to wind down from a tough Tuesday.

It is presented by Campus Unions Programs. Not exactly an open mic (which implies that the audience may perform), the booking is done a quarter in advance, so if you want to play, email the coordinator about a month prior to the beginning of the quarter you want to play in.

