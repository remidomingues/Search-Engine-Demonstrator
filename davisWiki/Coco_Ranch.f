

Coco Ranch grows organtic apples, peaches, nectarines, and cherries on the banks of Putah Creek.  They say they are Based on the promise that fruit is not a fungible good. They are all certified organic by the C.C.O.F.

They grow four varieties of sweet cherries that are ripe in May, two varieties of Apricots in July and September, a whipping twenty varieties of peaches, both yellow and white, from June through September, and finally seven mainstream varieties of apples, ripe September through November.  They also have heirloom apples.

  One of many farms on the outskirts of Davis

