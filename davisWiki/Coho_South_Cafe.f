
The CoHo South Cafe is a small satellite location of the Coffee House in the Student Community Center. It has a limited menu of pastries, sandwiches, tea, and coffee drinks. However, the menu does include some items unique to the Coho South, such as the http://dateline.ucdavis.edu/dl_detail.php?id13877 Chili Stack (chili layered with slices of cornbread). The sandwiches and wraps ($4.75  $4.95) include vegetarian options.


