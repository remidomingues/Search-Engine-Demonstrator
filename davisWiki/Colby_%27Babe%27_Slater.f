Colby Babe Slater competed in football, basketball and baseball from 19141917 and was inducted into the Cal Aggie Athletics Hall of Fame in 1980, becoming the earliest Aggie athlete to earn a selection into the Hall of Fame. In 1966, the Slater Award was established in his memory and is bestowed annually on the outstanding male athlete of the year.

Slater earned two gold medals as a member of the 1920 and 1924 U.S. Olympic athletes Olympic Rugby teams and captained the 1924 squad.

As a undergraduate, he was a member of the Phi Delta Theta Calpha Fraternity.

