Cold ShotThe Ultimate Dance Party Band

Cold Shot is a great cover band that plays popular hits, classic rock and old school goodies.  Look for Cold Shot performing as a duo first Fridays at the Davis Graduate and a variety of locations on other days. They are an integral part of Davis night time music scene.

Cold Shot website with more info: http://www.coldshotnow.com/

COLD SHOT members are Richard Urbino on bass & vocals, Users/ReneMartucci Rene Martucci on guitar & vocals and TBone Wilbur on drums & vocals.


