College Confidential(CC) is an informative Locally Owned Websites website dedicated to helping students learn about college life, requirements and academics. Their forum contains a comprehensive listing of different colleges and universities for students and parents to discuss and exchange experiences or opinions about anything pertaining to that college. There are also other sections devoted to college life in general and helping students through the application process. Davis has a moderately active group of future students, current students and alumni. 



