
College Cuts is a new landscaping service out of Winters that also services cows Davis, Vacaville and Dixon. Their theme is that the owner Andrew is a college grad who only employs college students with the slogan we cut your  grass to pay for UC Davis class.  They provide free estimates and the following services:

  Mowing
  Edging
  Trimming
  Weed Control
  Leaf Raking

Services are negotiable, as are dates and prices.  Weekly, biweekly, monthly and one time services are all available.



