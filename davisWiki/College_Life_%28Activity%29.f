College life refers to the act of, well, living in college.  Sometimes it is used to refer to the http://www.imdb.com/title/tt0077975/ Animal Houseesque notion of college being about things like Frat Parties, Alcohol, Marijuana Weed, and Sex.

The black tshirts reading COLLEGE were popularized by the late wiki:WikiPedia:John_Belushi John Belushi, who wore one in __Animal House__.

College Life can also refer to the College Life (Organization).
