
The Address(Commemorative Grove, 38.565486, 121.749367) is a small but beautifully landscaped area on the Covell Greenbelt just south of Covell Park.  It is not strictly a Memorials memorial, as the display case holds plaques with names of those either memorialized or simply honored by loved ones.  There is a small picnic area tucked away to the north.

