

The Community Chambers is a meeting room in the City Hall complex. It is the location of many cityrelated public meetings in Davis. Notably, the Davis City Council and Davis Joint Unified School District DJUSD Board of Education meet here. Additionally, most city commissions meet here regularly. The chambers can be rented for use by members of the public, and other events such as several candidate forums were held here.

It is in a separate building, immediately North of the City Hall offices. It was built in 1981.

