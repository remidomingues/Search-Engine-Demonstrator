
The City of Davis is starting a http://www.cityofdavis.org/cert/ Community Emergency Response Team. The first group of 25 applicants trained in the spring of 2007.  The city plans to train a second CERT team on the 5th, 12th and 19th of April, 2008. If you are interested in participating in the Davis CERT program, please contact the Fire Department through the contact information listed on the http://www.cityofdavis.org/cert/ CERT web page.  Applications are due by March 26th, 2008 for the April 2008 classes.

The training is free and includes 3 days of training on:

  Disaster preparedness
  First Aid
  CPR
  Establishing care shelters
  Sandbagging
  Traffic control
  Missing person searches
  City operations
  Organization and communications during an Emergency Services emergency

