Image(CHL.jpg, left, thumbnail, 350, For the Internetimpaired, the CHL also exists in a convenient paper version in the ASUCD Student Services Office.)The http://chl.ucdavis.edu/ Community Housing Listing is run by ASUCD to help people find housing in Davis.

The website has various search options to find a place along with places to post a listing. They usually publish a housing guide in the spring, but it is usually too late since most people have found housing and signed leases already.

The Community Housing Listing was originally started by the Graduate Student Association, where it lost revenue.  They then sold it to ASUCD, who has been operating it onbudget for years.


