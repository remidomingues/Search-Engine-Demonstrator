


The City of Davis used to offer the community an informal and confidential forum for dispute resolution via the Community Mediation Services. They were located at 23 Russell Blvd and could be reached at (530) 7575623.

Useful for:
  Neighborhood difficulties
  Tenant/landlord disputes
  Customer/merchant issues
  Roommate and coworker conflicts
  Conflict resolution training
  Community workshops
  Meaningful volunteer opportunities
  Facilitation of neighborhood and community meetings

