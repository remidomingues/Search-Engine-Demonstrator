Community News is a fairly innocuous sounding name for a newspaper until you read it. It falls under the category of revisionist history, mainly, it claims that the Holocaust never happened. The paper is based in Sacramento and published by Walter Mueller. Copies of it can be found in Shields Library if you look hard enough, although theyre not listed in the catalog.

