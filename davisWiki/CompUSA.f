


Where America Buys Technology.  Until 2008.

CompUSA was an alternative to Frys Electronics Frys or Best Buy.  There was also a CompUSA at the Nut Tree in Vacaville and one in Roseville until the whole chain bought the farm during the late 2000s recession.  No help desk for them. The CompUSA in Vacaville was converted into a Circuit City store and later closed in 2009.

The rights to the CompUSA name and website were bought by Systemax in January 2008. Systemax has reopened a number of the former CompUSA stores around the country. Systemax also bought the rights to the Circuit City name and website.
