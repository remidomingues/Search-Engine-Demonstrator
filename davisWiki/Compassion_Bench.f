

The Compassion Bench is a town art functional art sculpture/public bench located on Compassion Corner in Downtown Downtown Davis. It is the brainchild of Compassion Guy David Breaux.

David got permission from AT&T (who owns the property) and organized volunteers to build a cob compassion bench there. The bench was designed by whole earth bench cob master Users/BrennanBird Brennan Bird. The ceramic inlay art is by locally famous ceramic tile artists Donna Billick and Mark Rivera.

