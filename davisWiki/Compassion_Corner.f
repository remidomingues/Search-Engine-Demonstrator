

Compassion Corner is the nickname of the southeast corner of 3rd Street & C Street. It is so named because Davis resident David Breaux (AKA Compassion Guy) could be found, for years, standing on the corner asking passersby for their thoughts on compassion. He organized and got Compassion Bench built there.

