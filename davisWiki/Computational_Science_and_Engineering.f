

This is just a small place holder for now, please see the website.  Currently 6 faculty and a few dozen
related researchers are working on various CSE related projects.  CSE computational resources
include Tribe (a 720 CPU cluster with a very high speed interconnect), Colony, and various
activities having to do with the Keck Cave.

In addition to the research activities CSE is currently designing, managing, and hosting HPC clusters
for the UC Davis community.  Currently CSE is involved with designing and managing 7 clusters.

Other services to the community include ubuntu/redhat mirrors and subversion/trac repositories,
including one for the UCD Plone skin.

