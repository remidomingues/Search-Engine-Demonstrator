In the Conifer Collection, you can see conebearing trees, including pines, cedars, firs, junipers, and cypresses. These trees are nearly all evergreens, and are native to Mexico, Canada, Europe, North Africa, Japan, China, Australia, and the U.S. Trees in this collection illustrate a wide range of forms and foliage. The mourning cypress, with its weeping, overhanging branches, is familiar from Chinese landscape paintings. It is located just east of the California Avenue bridge.



