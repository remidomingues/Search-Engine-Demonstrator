
The Davis Consumers Guide is a monthly junk mail direct mail publication of Discounts and Freebies coupons mailed monthly to households throughout Yolo County. They publish four versions: Davis, Woodland, West Sacramento and Outskirts Country. You can also print the coupons from their http://www.consumerguide.com/CG_Flipbook_2/Home.html website. Their website  updated every 30 days  has the advantage in that they are categorized there, though you should check to see that they are not expired. They request that people call them with corrections of any expired coupons on their website.

They do monthly mailings in all of Yolo County plus customized mailings nationwide.  They also print business cards, letterhead, envelopes, business forms, brochures, postcards, and banners.

