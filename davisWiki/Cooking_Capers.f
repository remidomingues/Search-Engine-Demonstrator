

Cooking Capers is a locally owned Sacramento business that organizes fun cooking themed Birthday Celebrations parties.

Cooking Capers Childrens Birthday Parties  allow kids to mix, measure, paint and create their own meals and desserts. These parties take place in your home or the venue of your choice. They have many party options, including glutenfree and dairy free meals and dessert desserts.

Cookie Decorating Parties are a great activity for adults and children alike, especially for the holidays.  They bring dozens of homemade Cookies, Pastries, and Baked Goods cookies and an array of wonderful frosting and sprinkles to inspire your culinary artistry.


