If you drive north on Sycamore Lane, it will eventually run parallel with Highway 113 and its name will change to County Road 100a.  100a will continue north past County Road 29 and then peter out into nothing (or so the maps show).

100a is a part of Yolos County Roads system and is a great place to see some of the Outskirts outskirts of town.



