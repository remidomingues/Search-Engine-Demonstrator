This road runs south from County Road 29 towards north Davis.  As it enters the Davis city limits, crossing the Covell Drainage Channel, its name changes to F Street.  For the entire distance, it parallels the Train Tracks train tracks.

County Road 101a is a part of the Yolo County Roads system and is a great way to see some of the Outskirts town outskirts.

We Davisites call this the F Street Extension, the city made it a nice ride in the early 90s.  Users/StevenDaubert
