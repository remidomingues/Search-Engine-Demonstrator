Starting at West Covell Boulevard near Sutter Davis Hospital, John Jones Road heads northwards and curves so that it parallels Highway 113 on the west side.  At about the location where it starts paralelling 113, the roads name changes to County Road 99d.  Before it intersects with County Road 29 further north, 99d passes by the Davis Golf Course.  After passing 29, it simply runs out of steam and stops (or so the maps show).

Road 99d is one of many County Roads in Yolo County, all of which are great ways to see the Outskirts town outskirts.

Places to go on County Road 99d
  Davis Golf Course
  Binning Tract
   Sharon Avenue
   Central Way
   Barry Road

