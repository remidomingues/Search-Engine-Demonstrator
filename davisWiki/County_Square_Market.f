County Square Market is a  Supermarkets & Grocery Stores supermarket chain that focuses on Ethnic Markets ethnic food.  Their closest location is in Vacaville, with additional locations in Pleasant Hill and Antioch.  They have a large variety of Japanese, Korean, Chinese, Thai and Philippines food, plus fresh and live seafood, like dungeness crabs, Maine lobster, prawns, shellfish and more. They also provide different kinds of fish, fresh beef, pork, lamb and goat.

They also have a bakery, but the most distinctive attribute is that it is clean and large.


