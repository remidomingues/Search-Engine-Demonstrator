Along the sides of the Covell Drainage Channel in North Davis runs a nice dirt trail for hiking or biking.  There are actually two trails, one on each side of the channel.  One trail runs high along the north side of the channel and another runs low along the south side.  Neither trail is paved.  The trails start near F Street and run southwest towards Sycamore Lane, following the channel all along.  Although rare, parts of this area occasionally experiences grass fires.


  

