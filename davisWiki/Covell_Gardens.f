
A speck of land made pretty by fellow Davisites, the garden on Covell is a tiny garden that makes the southeastern off ramp from Highway 113 to Covell Boulevard just a Senseless Act of Beauty little more beautiful.  It is on the right when you head west on Covell having gotten off heading north on 113.

Though the area used to have lots of flowers, it appears as though they have been replaced by more native plants.  The garden is sponsored by the Rotary Club and Tree Davis.  It has been maintained since at least 2005 and possibly quite earlier.




 Not to be confused with Atria Covell Gardens, the retirement home.

