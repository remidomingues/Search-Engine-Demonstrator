The Covell Greenbelt is a section of the The Greenbelt Davis Greenbelt that connects to Community Park, Covell Park, and Northstar Park Northstar Parks, and Senda Nueva Greenbelt Senda Nueva Greenbelts.   It is located in North Davis, in the Covell Park neighborhood.  The greenbelt has at least one set of bathrooms on the east end (there are maps along the pathways to help you along).

Consumption of alcohol is permitted in the Greenbelt under Davis Municipal Code/26 Municipal Code 26.04.020.

Also along the Covell Greenbelt are 3 Covell Greenbelt Dog Sculptures sculptures of dogs.  Real dogs are often found walking their owners through the greenbelt, though some may trust their humans and let them get exercise without a leash.














