
The Craft Center CraftPalooza is an event where UC Davis students can come in and participate in craft projects. There is no charge for admission.

The 2008 Holiday CraftPalooza will be held on November 21, from 6 to 10 pm. Projects include picture frames, beeswax candles, ceramic bowls, jewelry, silk scarves, and more, and they range in price from $1 to $12.

There was also one held on November 30^th^ 2007 from 6 to 10 pm, and a Valentines CraftPalooza on February 8, 2007.



