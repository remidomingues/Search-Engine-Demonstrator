
Crossroads is the Cstore for the Cuarto area residence halls (sister store to The Junction & Trudys). They offer a large selection of food and beverages, including instant foods, snacks, chips, candy bars, gum, and ice cream, as well as water, tea, coffee, energy drinks, soda, and smoothies. Swipes, aggiecash, cash, and debit/credit cards can be used to make purchases at the store.

