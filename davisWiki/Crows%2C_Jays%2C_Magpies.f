
The crow family (wiki:WikiPedia:Corvidae Corvidae) has over 100 different types of birds.  Common features include an elongated beak, shortened or curled tail, and four welldeveloped toes.  Generally considered to be intelligent birds.  These birds are omnivorous.

We also see some crow migration in Davis.

If you see a dead bird, please report it here: http://townsend.ucdavis.edu/reportadeadbird/

