


Cunningham Engineering Corporation is a civil engineering, landscape architecture and land planning firm.

They have been involved in many Davis projects, including Sutter Davis Hospital, Davis Commons/Aggie Village, the University Retirement Community, Harper Junior High School Harper Junior High, the Davis Police Department Davis Police Station, The Lofts, the McCormick Building, The Colleges, Schaal Aquatics Center, the Genome Center, and the new Spring Lake development on County Road 102.

