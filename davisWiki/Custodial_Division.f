The Custodial Division at UC Davis is responsible for cleaning the indoor areas on Campus campus. If youve enjoyed a clean UCD Bathrooms bathroom on campus remember to thank a custodian.

The headquarters is located on La Rue Road in the large quonset hut style building that they share with Bargain Barn. They used to share it with Mail Division.

George Reyes led the division during the early nineties and also handled human resources for the rest of facilities.
