
Cycads are ancient, slowgrowing, gymnosperms. All species are dioecious (some individuals produce only pollen cones and others produce only seed cones).  By far the most common ornamental cycad grown outside in Davis is the sago palm, Cycas revoluta. A number of other cycads can be found on campus, especially in the Ernest M. Gifford Cycad Garden in front of Storer Hall.

Many species resemble, and are often confused with, palm trees. This is particularly true for the Sago Palm which is a common garden plant.

