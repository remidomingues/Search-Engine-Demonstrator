The DLSC Soccer Fields are  well, obviously places to play soccer.  They are the home grounds of the Davis Legacy Soccer Club and are located out of town south of I80 to the east of Davis, past the car dealerships.

In 2010, DLSC is expanded to include 4 more fields, for a total of 7, hoping to sponser more teams, host larger tournaments and attract Regional and National events.
