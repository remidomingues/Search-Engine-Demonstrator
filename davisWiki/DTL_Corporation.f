

Digital Technology Laboratory (DTL) Corporation provides research and development services for Mori Seiki, one of the largest machine tool manufacturers in the world; and consultation services and support for many machine tool users. Their stated mission is to provide advanced manufacturing and machine technology for CNC machine tools.

They are one of the few industrial companies located in Davis. This happened because of a personal connection between a UC Davis professor and the parent company. As the R&D arm of Mori Seiki, their focus is to push machine tool manufacturing into the nanometer precision range.

Their large white building can be seen from I80.

They held a public grand opening on 20090513.

As of July 2011, the DTL currently employs 80 workers.
