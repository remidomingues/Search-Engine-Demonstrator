DaFIS (Davis Financial Information System) is the Financial Information System for UC Davis.  It is an electronic database and transaction processing system that also has a web interface.  All University Departments departments/administrative units use the system for their accounting.  DaFIS is accessed primarily by UC Davis employees who work on financial matters.

DaFIS was implemented on July 1, 1997.  DaFIS replaced the legacy A11 mainframe Unisys computer that had been processing accounting matters until then.  The software that powers and supports DaFIS is purchased from American Management Systems.

