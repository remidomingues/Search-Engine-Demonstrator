
From their website: Dateline UC Davis is a facultystaff publication for the University of California, Davis, that delivers News news about the Campus campus weekly on Fridays during the academic year, with breaks for the holidays.

Written and edited by the UC Davis News Service, the 12,000reader publication delivers news about campus issues and events, research highlights, highereducation trends, and people.

The news deadline for each issue is 9 a.m. the Friday before publication.

