At one time I had several colonies of bees in the walls of an outbuilding and would stand near the knot holes and watch the activity. When a swarm would occur I would stand in the middle of it. One time on a very warm day a bee hit me as I was mowing the lawn and chased me into the house and for about 15 seconds he flew in tight circles near the screen door and then left.

We had the outbuilding removed and a colony moved into the wall of our home, so we have our personal live in bees.

