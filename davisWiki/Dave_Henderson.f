Dave Henderson has served as the Yolo County District Attorney since 1984. He has announced that he will retire in December of 2006.

The Yolo County Board of Supervisors honored him with a http://www.yolocounty.org/org/bos/agendas/2004/062204/06.pdf resolution(pdf) recognizing his years of service as District Attorney.

