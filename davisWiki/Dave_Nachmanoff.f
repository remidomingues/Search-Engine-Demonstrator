Singersongwriter Dave Nachmanoff, a.k.a. Doctor Dave, received his Ph.D. in Philosophy from UC Davis in 1997 and promptly switched gears to begin his music career. Locally, he has played shows at Schools elementary schools, Spiritual Organizations churches, Palms Playhouse, and many other venues. In 2005, he went on tour with Al Stewart.

Dave hosts songwriting workshops around the country. He can also write/arrange custom songs for special occasions.

Dave is married to Jennifer (Jen) Nachmanoff, and they have two children.

