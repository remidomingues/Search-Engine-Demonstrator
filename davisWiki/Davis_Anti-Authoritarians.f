The Davis AntiAuthoritarians is a Davis community anarchist Organizations group working on several projects.  They were originally formed in early 2006 to oppose Targets use of sweatshop labor.  Currently, they support other community groups, such as Food Not Bombs and help to organize Critical Mass.  They can be seen tabling at the Farmers Market

Projects
Primate Center
The DAA collective is currently waging a campaign against the California National Primate Research Center which is up for expansion.  A demonstration has been planned for April the 28th, the last day of The National Week of Liberation for Animals in Labs at the Primate Center site.


