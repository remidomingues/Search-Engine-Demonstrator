

The Davis Astronomy Club is comprised of people in the city of Davis who love astronomy. They aim to bring upcoming astronomy events and new information to the public. They have been part of the Explorit Science Center since 1987. They regularly hold lectures at Explorit Science Center Explorit as well as stargazing parties around Davis. The also have events for meteor showers, eclipses, and iridium flares other fun sky events.

If youre more UCD oriented, you might be interested in the Astronomy Club at UC Davis.
