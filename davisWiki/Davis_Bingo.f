
http://www.davisbingo.com Davis Odd Fellows Bingo! is a wonderful way for the community to support the many needy nonprofit organizations here in Davis by playing Bingo. Each month, Davis Odd Fellows Bingo! connects with a local organization that needs assistance. Community members, friends, and family are encouraged and welcomed to come to the Davis Odd Fellows Lodge for an afternoon of to fun playing Davis Bingo. For as little as $12, you can sit with friends and chat or meet new people while helping people in our community.

The Davis Odd Fellows Bingo! hall is located at:

415 Second Street
Downtown Davis
Davis, CA 95616

Doors open at 12:00 PM. Bingo! play begins at 1:00 PM.

