
Davis Cards and Games, or DCG, is a student organizations club at UC Davis for students who want to play card games and board games.  We have people who play Magic: The Gathering, YuGiOh, Pokemon and Vanguard.  Our players are primarily casual and our goal is for members to have fun more than competition...that is unless we are running a draft.

Meetings are held at the ARC Meeting Room 2, Fridays from 7pm10pm.

