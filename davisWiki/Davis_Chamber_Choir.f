

The Davis Chamber Choir is a small, student run, vocal ensemble. We strive to provide a fun, challenging and supportive environment in which dedicated singers can expand upon their vocal skill and repertoire. We perform a wide variety of music written from the Middle Ages to the present, in a range of genres, though usually what would be considered choral or classical.

This Fall 2014, our repertoire will be based around the theme of Choral Music with a Twist or in other words, vocal ensemble music with elements different from or even contrary to traditional, conventional choral music. Well perform music with influences of world music or popular music styles, unusual techniques, and other interesting such things.

We hold auditions at the beginning of every quarter. If interested, send us an email!


