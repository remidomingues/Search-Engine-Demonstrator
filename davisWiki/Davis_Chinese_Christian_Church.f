

The goal of our church is to bring you to a closer relationship with GOD through worship and fellowship. On Sundays, we have Cantonese and English worship services, followed by a Mandarin service. In addition, we have Sunday school for adults and children and 15 fellowship groups of various age groups, in English, Mandarin and Cantonese. In fellowship groups, we can get to know each other at a personal level, study the Bible together, learn more about Jesus Christ and encourage one another with the love of Jesus.

One of the spiritual organizations in town.

