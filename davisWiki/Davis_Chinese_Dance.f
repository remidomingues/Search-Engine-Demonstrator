

Davis Chinese Dance club is a brand new traditional and modern Chinese dance club for UC Davis students of all backgrounds interested in learning about Chinese culture through the art of Chinese Dance. NO EXPERIENCE IS NECESSARY so whether you have been performing Chinese Folk Dance since before you could walk, or can honestly swear that you do, in fact, have 2 left feet, everyone is welcome to come dance with us!

For more information and to stay updated with upcoming dance classes and performances, visit our Facebook page at http://facebook.com/groups/davischinesedance/. We hope to see you soon!


