
The Davis College Libertarians is a student organization at UC Davis that was formed in the Spring quarter of 2003. Its primary purpose is to promote the ideals and platform of the http://www.lp.org Libertarian Party, namely libertarian libertarianism. To find out more information about the organization, please contact Paul Amnuaypayoat.

