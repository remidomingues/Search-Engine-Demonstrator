Davis CruiseIn is a gathering of local classic automobiles automobile enthusiasts who show their cars, trucks and motorcycles. The CruiseIn is held on the third Tuesday of each month from May through October from 5 to 7 PM at The Marketplace The Marketplace Shopping Center. There is no cost to show a car, and visitors are invited to talk with owners in the informal setting.


Regional car shows include wiki:woodland:Reiffs Street Bash in Woodland and the wiki:winters:Classic Car Show in Winters.

