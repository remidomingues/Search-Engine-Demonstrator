
The Davis Dogs are the mens UC Davis ultimate frisbee team.  They are classified as a Sport Clubs sport club (rather than an intramural team or NCAA).  The team used to also field Mens Open and AA Coed Intramural Sports intramural ultimate teams most seasons, but havent done so recently (2013).

It is believed that no actual dogs are on the team.

The name of the womens Ultimate frisbee team is the Pleiades.

