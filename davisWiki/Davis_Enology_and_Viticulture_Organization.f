http://wineserver.ucdavis.edu/devo/ Davis Enology and Viticulture Organization (DEVO) is the student organization of the Department of Viticulture and Enology at UC Davis. Members of DEVO hold weekly wine tasting sessions, travel to various wineries local and far, and generally enjoy wine together. Find more information about what DEVO is up to at http://devo.ucdavis.edu/

They have held fundraisers by hosting Dinner under the Winkler Vine in the past.


