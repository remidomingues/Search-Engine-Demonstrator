Davis Garage opened August 1^st^, 1911. They had 5000 square feet of floor space. They sold automobiles and repaired bicycles.

On February 4^th^ 1914 Leroy R. Hoffman Roy Hoffman sold his one third interest in the Davis Garage and bought the Bowers Garage which he renamed Hoffmans Garage. Fred Covell joined him in moving to the new business as a mechanic.

This left the ownership in the hands of George B. Hoag G. B. Hoag and G. P. Hoag.
