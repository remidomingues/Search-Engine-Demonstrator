
The Davis Genealogy Club is organized to bring together individuals and resources for investigating family histories.  They have topical presentions each month.  Information about upcoming speakers is available at their website, in the local newspapers, and in the Senior Scene magazine.

In addition, they manage a Genealogy Library at the Davis Senior Center with hours on Wednesday and Friday from 1:00pm to 4:00pm and by appointment.


