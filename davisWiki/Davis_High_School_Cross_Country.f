
The Davis Senior High School Cross Country Team is the reigning CIF SacJoaquin Section Division 1 Champion for both Boys and Girls Cross Country. The team has won 11 section championships in its history (8 Girls and 3 Boys).

In the year 20082009 school year Davis Cross Country team won seven section championships in both boys and girls. In 2008 season, Boys Sophomore team had their best, and Davis cross countrys best appearance in Section Championship.



