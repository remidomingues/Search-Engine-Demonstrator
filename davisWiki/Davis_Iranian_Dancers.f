This is a page is here to represent the former dancers of the Iranian Dance Team and current Davis Iranian dancers (Users/babhari), who have continued to educate members of the community and the UC Davis campus about Iranian culture through dance.

As of Fall 2008, this group has prepared dances of various regions of Iran for events including community and UC Davis events. The group works closely with UC Davis student organization http://www.iscao.com to spread knowledge about Iranian culture. Although the group does not provide teaching in traditional class form, it is a community resource for anyone who would like to learn, perform or teach Iranian dance.

The groups members tend to meet on a weekly basis (including summer!) in Haring Hall on campus.

