

The Davis Joint Unified School District is one of the better districts in California.  Its also the 7th largest employer in Yolo County, with 792 fulltime equivalent employees, according to a list put together in 2011 by the Sacramento Business Journal.  Most of the schools have Academic Performance Index ratings from the state Department of Education in the 850950 range (out of 1000), which is remarkably good.

