  This is about the organization named Davis Music Scene.  If you are looking for information about the actual music scene in Davis, visit Music Scene





Davis Music Scene as an organization appears to be defunct, having last put on a show in Fall 2007 and http://www.davismusicscene.org their webpage has been repurposed as an eBay retailer of vinyl albums.  They have the http://www.livejournal.com/~davismusicscene DMS Live Journal Community for show updates should they revive.  The LJ group now lists shows in the area put on by a variety of other groups.

http://www.davismusicscene.org is an website dedicated to promoting music on campus and in our community. We want to see music thrive; to that end, we support local bands (by giving musicians a chance to connect with each other and a place to perform), the local audience (by holding shows and promoting shows hosted by other organizations), and local venues.

We host concerts weekly on the UC Davis Campus.

Davis Music Scene was registered as a campus club with ASUCD in either spring or fall 2002 as an effort to revive the thendead oncampus music scene. The original officers and organizers were Carl Rennie, Alex Stupakov, Arjun Singh, Blair, and Adam V. (Apologies for botched names)

The first DMS shows were held in 3 Kleiber Hall. Other venues since then have included 194 Chem and the ASUCD Coffee House.

The organization currently puts on concerts every Friday at 7 p.m. at the Coffee House during the school year. Admission has always been free to members, and up to October 2004, membership was also free and unofficial. To recoup some of the costs for hosting these shows , yearly memberships are now sold for $5 at the door, and quarterly memberships are $2. Most concerts will include one Davis band and two outoftowners. If youre in a band that wishes to play at the Coffee House or are interested in volunteering with DMS, please contact us!

