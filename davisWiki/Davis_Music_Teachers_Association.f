As evident from its name, the Davis Music Teachers Association (DMTA) is an organization of local music instruction music teachers. It holds semiannual student recitals, periodic master classes, and teacher gettogethers. Members also get to advertise their services on the DMTA website (http://www.dcn.davis.ca.us/go/dmta/) and on the list at Watermelon Music. Membership costs $15 annually.

If you are looking for a music teacher, this is a good place to start.

