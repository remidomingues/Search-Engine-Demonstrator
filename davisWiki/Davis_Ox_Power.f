




Davis Ox Power is a group of students and community members whose mission is to promote education and innovation in animal powered agriculture and accompanying small technologies.

In 2008 we will begin working with a team of fiveyear old steers in Davis, CA with the goal of hosting classes and public events that feature both the process of training oxen and the manufacture and use of tools relevant to animal powered agriculture.



We believe that the maintenance of a working team will provide opportunities for education about international rural development and create a forum for exploring lowcost, environmentallysound options in agricultural development.

Through our efforts, we hope to help facilitate the creative application of animal powered technologies in ways that are relevant to addressing presentday agricultural problems.
