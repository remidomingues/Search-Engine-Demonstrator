

Park and Ride is a parking lot designed to encourage commuters to leave their cars (or bikes) and take the bus to Sacramento. The Yolobus #42 stops here on the way to/from Sacramento. This parking lot is also (supposedly) also a Greyhound bus stop. Bicycling Bicyclists often meet here before going out in groups as well.

For more information on carpooling and ridesharing, see ride board or http://www.carpoolworld.com/ride.html?toDAVIS&stateCA&countryUSA,US carpoolworld.com.

