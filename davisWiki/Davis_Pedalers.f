

Davis Pedalers are dedicated to the use of bicycles humanpowered transportation. They are composed of businesses and individuals who distribute goods and services on bicycle in Davis.  They are committed to eliminating pollution from transportation in Davis, the major contributor to greenhouse gases. The purpose of Davis Pedalers is to develop a network of communication and support for those groups and individuals interested in exchanging goods, services, and other resources by bicycle. This a developing project and your participation, involvement, and partnership are most welcome.


