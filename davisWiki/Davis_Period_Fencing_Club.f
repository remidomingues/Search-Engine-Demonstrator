
The Davis Period Fencing Club is a historical fencing club at UC Davis.

Rather than using modern texts and techniques, the club works with 17th century fencing texts by the Italian masters and manuscripts that were brought from Spain and translated by our own beloved Mary Curtis. Though not officially affiliated with the Shire of Windy Meads SCA or Renaissance Faires Renaissance Faire, a number of members do fence there.

