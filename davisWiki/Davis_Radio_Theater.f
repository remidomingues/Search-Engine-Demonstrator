


Davis Radio Theater is the KDVS inhouse radio theater program, directed and engineered by LesLight.   Airtime on KDVS is Tuesday evenings, 67pm.  Light often writes the scripts for the programs, which are then voiced by community members, including students, and residents of Yolo County Yolo and Sacramento counties.

In 1999, Les Light won an Ogle Award for his play, Back to Frankenstein.

Between 34 new plays are produced and premiered each year.  Due to the demands of writing, casting, and producing radio performances, most episodes aired are repeats. Davis Radio Theater welcomes new volunteer actors and does consider script submissions from time to time.

