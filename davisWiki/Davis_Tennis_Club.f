


The http://www.davistennisclub.org Davis Tennis Club is a nonprofit organization whos mission is to encourage participation in tennis at all ages and ability levels by sponsoring instructional, social and teambased programs that are open to all.  The club hosts quarterly social tennis events, adult and junior tournaments, competitive USTA and SATA interclub leagues and weekly dropins.  Board meetings are held every first Wednesday of the month at our Clubhouse next to the Community Park Courts and is open to anyone interested in attending.

Adult and junior lessons and summer junior camps are also offered through an agreement with the http://davistennisacademy.com Davis Tennis Academy.

