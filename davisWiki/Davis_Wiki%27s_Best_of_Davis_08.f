Voting is OVER! Voting took place between March 30 and May 2, 2008.

The California Aggie holds a Best of Davis contest every year where students vote on their Davis favorites. However, many complain this results in the establishments college freshman actually go to winning all best of Davis titles. The Davis Wiki community is certainly well more in the know than the average UCD student. Show off your Davis pride and vote for your favorite things about Davis.

 How Voting worked 

People edited the page and ranked the places theyd tried in order of preference.  A simple Choice Voting election was conducted for each category to determine the winner.

