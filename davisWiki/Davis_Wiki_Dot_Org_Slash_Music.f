Davis Wiki Dot Org Slash Music is a music compilation, created by Users/RobRoy Rob Roy, for the 2006 Davis Wiki fundraiser.


Several of these Significant Davis Bands bands are mentioned in a great Sacramento News and Review SN & R article written in 2002 about the http://www.newsreview.com/sacramento/Content?oidoid%3A10574 Paradise lost across the causeway.

The sequel to this mix CD is 2010s Audioversity in the Little Tomato.
