
The Davis Wushu at UC Davis is a Student Organizations recreational club formed in the year 2000 by students who wanted to train Wushu. The club is a Chinese martial arts club that focuses on flexibility, strength, endurance, as well as traditional and contemporary forms. Throughout the year, the club participates in performances, exhibitions, and competitions. We welcome members of all levels of experience.

