

Davisville Apartments is an apartment complex in Davis providing http://www.city.davis.ca.us/housing/affordable/list.cfm affordable housing to low income seniors age 62 and older. The STEAC food bank distributes at this location to low income complex residents. Potential residents should contact Yolo County Housing Authority to apply for Section 8 Vouchers far in advance of their desired movein date.

To learn more about rental housing in Davis, check out our Rental Housing Guide as well as our Apartments pages.


