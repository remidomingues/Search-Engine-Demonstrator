

wiki:dixon:Dawsons is a cute diner in Dixon; one of the Dixon restaurants restaurants in Dixon you can choose.  Worth the drive if you are looking for something out of the ordinary college grind.  Excellent breakfast menu.  You can even make your own toast using the toaster on your table. Half the establishment is a restaurant and half is a bar.

The Bar is frequented mostly by locals and regulars which makes for a very mellow atmosphere.  Thursdays they have karaoke  always a great time to be had!


