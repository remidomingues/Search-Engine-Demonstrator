They were best known by Don and Joe for covering Roots, Radicals by Union 13. It also captured a moment in Hectors life when he was embracing the punk rock lifestyle in the form of spiked hair and cutoff shorts.

Now they are back for a few shows with more whiteness and more hair. Hector now has a perm.

