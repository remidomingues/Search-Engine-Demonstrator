The Dean Witter Fund will give graduate students $10/person to take a Professors professor out to Lunch Specials lunch with a maximum of $100. No joke.

As of 2008 this can no longer be used by or for undergraduates.

A limited amount of money is available through the Dean Witter Fund to help offset the cost of entertainment of graduate students by faculty members or the converse. The fund is intended to promote faculty/student academic interaction in informal settings. The fund cannot be used for alcoholic beverages.

Read more about it http://gradstudies.ucdavis.edu/ssupport/deanwitter.html here.

