Address(Denali Drive) bears the Alaskan Native name for Mount McKinley, North Americas highest mountain.

 It runs from Shasta Drive up to West Covell Boulevard in West Davis.  There is a medical building, apartments and homes next to Denali drive, but there do not appear to be any addresses on Denali.

