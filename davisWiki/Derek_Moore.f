
Derek Moore won UC Davis UC Davis first Division I DI wrestling championship in 2007.  He claimed his win in the 2007 NCAA Wrestling Finals for 141 lbs., not only beating the 1st seed but controlling the match completely and finishing with a technical fall (scoring more than 15 points over the opponent, considered the most dominating victory possible).

