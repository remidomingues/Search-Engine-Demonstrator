
Name: Derick Lennox

Office: Order of Omega, President

Personality:

From July 2006 to June 2008, Derick was the Director of ASUCD Lobby Corps, which is the Capitol advocacy unit for ASUCD.

He was the 20072008 Pledgeship Director in the Sigma Chi Fraternity.

In 20052006, he served as an ASUCD IAC Internal Affairs Commissioner.

Derick is involved with ASUCD elections as a public speaking trainer.

Derick graduated from Monte Vista High School in Danville, CA, in 2005.
