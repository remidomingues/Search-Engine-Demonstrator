Dick Livingston Trio is part of the local music scene and consits of Dick Livingston on keyboard, Rich Livingston on drums and Ted Fontaine on bass. They play a variety of jazz and blues, orginals and covers. 

They can often be seen on Friday nights at Cafe California playing for the local politicos.

Dick Livingston is a retired Davis Senior High School teacher, however he also teaches parttime college course at Travis Air Force Base in Fairfield. He is also active in the ACLU Yolo County ACLU.
