

Technically called Aspen Greenbelt Park (according to a nice City of Davis parks & recreation worker), Address(Dinosaur Park, 38.558131, 121.777506) is a Parks park in West Davis, nestled between the culdesacs of Bryce Lane and Saratoga Place, in the Glacier neighborhood. It features a great green dinosaur (toddler play structure), as well as a big kid play structure. They added new structures and ground covering during the winter of 2008/2009.

The park also has blue babysafe swings, nice young Coast Redwoods to climb around under, picnic tables and a water fountain. It is located on a nice part of the Aspen Greenbelt with interesting Davis isnt Flat hills and mounds adjacent to the park.

