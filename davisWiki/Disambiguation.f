Sometimes one word or acronym is ambiguous, because it has two or more different meanings.  In this case, a Wiki page is created that has a brief summary of each meaning and links to the specific meaning pages.

The Templates/Disambiguation Disambiguation Template is what you usually use to start out one of these pages.  To use the template, create a page like usual but then choose Disambiguation Template in the create page area.  Or, you can just copy/paste the raw text from that page into your new page!

