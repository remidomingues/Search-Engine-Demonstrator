Ditches carry water from one place to another, especially during Winter wetter times.  In Davis there are many small ditches, many Sewers underground ditches and a few larger ones.  Theyre home to many Town Fauna forms of wildlife, especially Rats and other rodents, Ticks and other bugs.  Unfortunately, its also a home to items no longer desired by humans (like Wandering Carts shopping carts).

The more famous ditches in Davis include:

  The ditch south of Covell Boulevard and Lake Boulevard
  The ditch along North F Street
  The ditch on the North Davis northern end of town
  The ditch between Arroyo Avenue and Target
  The UCDavis Arboretum
  Willowbank Ditch
