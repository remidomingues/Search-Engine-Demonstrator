If you are a student, researcher, or staff member at UC Davis and you want to wiki:WikiPedia:Scuba_diving scuba dive you will have to do so under the rules established by the Diving Control Board. The Board is responsible for the safety of underwater programs and for research diver training and certification. Their policies extend to any scuba diving on campus, at the Bodega Marine Laboratory, or anywhere else.

