The Division of Graduate Studies (typically just Grad Studies) is in charge of the education of all academic graduate students.  Theyre somewhat akin to the Colleges for undergrads, but theres only one and they dont oversee the funding so much.  What they do oversee is:
  The creation and dissolution of Graduate Groups
  awarding M.A., M.S. and Ph.D. degrees
  sending out that GradLink email you never read
  coordinating postdocs
  and generally handling paperwork relating to all of the grad students

The current head is Dean Jeffery C. Gibeling.

