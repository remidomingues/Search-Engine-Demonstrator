

DodgeballPalooza is a Annual Events yearly event held by Intramural Sports. A Mens and Womens league are offered which have two sets of winners for each league, an IM Champion and a costume contest winner. As a palooza, it is a all night competition, ending once there is a winner.
 In Winter 2010 DodgeballPalooza introduced a Mens Res Hall league for incoming freshman in a league with other freshman. Along with introducing a Mens Res hall league, IM added an extra player for each team to have it 7 vs 7 then the usual 6 vs 6.


