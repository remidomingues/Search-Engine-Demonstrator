Address(Donner Avenue) is a residential street in East Davis. Oak Grove Park is on the south side of the street.  It is the only point of vehicle access to the McKeon Condominiums.

Pedestrians and bicyclists crossing Donner at Pole Line should be alert to vehicles turning suddenly onto Donner at high speeds.  The curb radius at the intersection is designed to facilitate a highspeed turn from Pole Line (speed limit 40mph) to Donner (speed limit 25mph).

