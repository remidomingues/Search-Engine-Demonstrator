

This phone was once located right in the middle of downtown outside the Hot Dogger and a few other restaurants on E Street (down the street from The Lofts).  Its phone number was (530) 7569782.  At the time of its removal in the summer of 2006 it cost $.50 for local calls.

I called this phone in August 2003 when I was in Orlando, and had a nice conversation with someone on the street who happened to be eating dinner with Ingrid Salim, a teacher at Holmes Junior High School. Ive called it a few other times too, receiving mixed reactions. Its not hard to get someone to pick up, though, since its such a busy area. Users/GrahamKolbeins

