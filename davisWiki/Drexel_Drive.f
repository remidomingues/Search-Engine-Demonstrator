Drexel Drive starts at J Street and runs toward the east crossing L Street and ending past Chestnut Lane where it meets Snyder Drive at a line of bollards. Mostly residential and a good street for bicyclists traveling east west as it is a part of the Davis Bike Loop.

From west to east:

  J Street
  Auburn Drive
  Holmes Junior High School on the southern side of the street
  Hemlock Lane
  L Street, which at one point wasnt a stop for traffic on L Street going north and south.
  Pacific Drive
  Cypress Lane
  Chestnut Lane
  The bollards to Snyder Drive and the bike path to Pole Line Road and Loyola Drive

