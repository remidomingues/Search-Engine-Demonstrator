Drivers Ed or Drivers Education is a class or program that prepares new drivers to take the exam that will allow them to obtain their permit. Drivers Training involves behindthewheel professional driving lessons so that students may be prepared to take their Drivers Test at the DMV. It is important to obtain both Drivers Ed and Drivers Training to learn how to respect other cars, people, and bikes on the road.

