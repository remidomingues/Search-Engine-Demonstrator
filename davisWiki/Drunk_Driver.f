This is a two player drinking games game, requiring one standard deck of cards minus the jokers.

One person is the victim (and should have a sixpack of beer or forty or two) and the other the dealer.

Aces, Kings, Queens, and Jacks are drinking cards.

The dealer begins by flipping over one card. If it isnt a drinking card nothing happens, and they proceed to the next card. If the next card is also not a drinking card, the players switch positions. However, if the next card is a drinking card, the fun begins. For each drinking card, the victim must take the corresponding number of drinks and is dealt that many more cards (Ace4 King3 Queen2 Jack1). If no other drinking cards are turned over in the prescribed number of flips, the players switch.

If you make it through the entire deck once you win, which means you can end any additional rounds youre dealt if youre about to fall over.

