Address(Duke Drive) is one of Daviss more eclectic streets, located just north of Aggie Liquor and the 7Eleven. It starts as a culdesac on the west side of L Street and runs for one block between L Street and M Street. It has over 500 bicyclists per day, and features innovative landscaping and large shade trees. It is entirely residential. The East Davis Bicycle Spiritual Centre is located on Duke Dr.



