
Dutton Hall is address(located, 38.541545, 121.747731) across from the east wing of the Quad and sits slightly behind North Hall and South Hall.

Dutton Hall houses:
    UCD Cashiers Office
    http://eaop.ucdavis.edu/ Early Academic Outreach Program
    http://ets.ucdavis.edu/ Educational Talent Search
    Learning Skills Center
    Student Accounting
    http://jobs.ucdavis.edu/ Student Employment Center
    Financial Aid Office Student Financial Aid Office
    Student Judicial Affairs
    http://financialaid.ucdavis.edu/scholarships/index.html Undergraduate Scholarship office
    http://upwardbound.ucdavis.edu/ Upward Bound
    Transfer Student Services
    Reentry Student Services
    Veterans Services

UCDavis students usually only end up there to resolve Financial Aid matters.  A huge line of people can be seen, waiting to enter Dutton Hall, at the beginning of each quarter.  This is because those who do not Direct Deposit their Financial Aid checks must pick them up here.  Lesson learned:  Direct Deposit your checks.  Check out http://mybill.ucdavis.edu as well, students can now pay bills online!

Outside of Dutton, right in front, sits the Dutton Fountain.

Dutton Hall sits on the site of the old East Hall, which was demolished due to extensive damage and wear and tear to the building.  This building was likely named for Thomas Dutton, the first UCD Assistant Vice Chancellor of Student Affairs. Dutton Hall was constructed 19981999 and designed by Fisher Friedman Associates, according to the http://sunsite.berkeley.edu/~ucalhist/archives_exhibits/campus_planning/atkinson_archive/ucd/ Atkinson Photographic Archive.

Also known, through (sometimes intentional) mispronounciation, as Durden Hall, in reference to the protagonist in http://www.imdb.com/title/tt0137523/ Fight Club.


