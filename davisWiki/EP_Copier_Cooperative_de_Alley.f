

A Departed businesses departed business, EP Copier was a tiny Copy Shops copy shop that had four copy machines. For under 50 copies they charged $.06 +tax per sheet. Over 50 copies the price dropped to a cheap $.05 +tax. They also sold a small selection of Chinese media newspapers ($.50.75), office supplies and womens purses. They may have also been located at 140 B Street at some point.

The closed their doors as of September 1, 2005.

