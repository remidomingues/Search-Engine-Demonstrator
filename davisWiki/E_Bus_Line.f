

The Unitrans E Bus Line services downtown and Central Davis and departs from the Memorial Union terminal.  The topofthehour runs during the day use doubledecker buses.

Check out the schedule and route map at the http://www.unitrans.ucdavis.edu/e Unitrans website for more information.

Destinations on the E Line

  Downtown
  Davis Food Coop Shopping Center
  Central Park
  Community Park
  North Davis Elementary School
  Holmes Junior High School

