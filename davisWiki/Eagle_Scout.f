Eagle Scout is the top rank a Boy Scouts Boy Scout can attain. About 2% of all boys entering scouting obtain the rank.  Achieving it requires, among other things, the completion of a large service project.  Examples of local eagle projects include one of the Davis Cemetery Labyrinth Davis Cemetery Labyrinths, the benches at the Mace Ranch Park Council Ring, and the running routes compiled on http://www.dhscrosscountry.com/nssfolder/greenbeltroutes/index.html this map.

