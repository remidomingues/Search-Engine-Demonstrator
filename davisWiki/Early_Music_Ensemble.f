Comprised of both vocalists and instrumentalists, the UC Davis Early Music Ensemble performs pieces from the Medieval, Renaissance, and Baroque eras.  Instrumentalists play authentic period instruments.  Anyone in the Davis community can join, but must audition.

Info: http://music.ucdavis.edu/eme/main.html

David Nutter, Director. (Music 45/145; meets Mondays and Wednesdays 7:009:00 p.m.; 115 Music Building)



