

Eatwell Farm is a small family farms farm located 10 miles southwest of Davis.  They have a Community Supported Agriculture program where you can buy direct from the farmer.  You can choose to receive a box every week or every other week and you pick up this box on Wednesday evenings in Davis.  The box is a wonderful seasonal selection of Organic ORGANIC fruits and vegetables, and no substitutions are allowed.  You can fill out an online membership application at the Eatwell http://www.eatwell.com website.  Help keep Davis agricultural land in the hands of organic farmers!

Offerings

  Organic fruits and vegetables
  Freerange organic eggs
  Lavender and lavender products: http://www.lavenderfarm.com
  Fresh flowers
