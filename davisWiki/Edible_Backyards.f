
Edible Backyards is an edible gardening/landscaping business.  They are a new local business in Davis, but are available for hire across the Sacramento area. Their services include a free first time consultation, design layout, raised bed construction, and installation of vegetable plants.  Some of their plants are grown from seed (Petaluma Seed bank), while others are bought from local nurseries.  Give us a call for a free consultation! or check out our website for more information.


