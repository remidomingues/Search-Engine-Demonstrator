El Fuego

El Fuego is the fastest bus in the Unitrans fleet, number 4350, an wiki:WikiPedia:Orion_Bus_Industries Orion Mk. V High floor transit bus.

It has been rumored to have hit 80 MPH on the highway (it actually only hits about 67, the top speed isnt too good) and can beat any bus in a drag race from a stop light (even 4735!).

As of 6pm August 2, 2005, el fuego is no more.  Two, perhaps three, pistons have been blown and blown right proper.  While troubleshooting later on in the day chunks were being spit out and thrown everywhere.  While the bus will be rebuilt it probably wont be faster (or stronger or better, its not exactly the six million dollar man).  If anything it will be slower.  Sorry all.

