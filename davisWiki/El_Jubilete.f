

One of the many Mexican eateries in wiki:Woodland.  The quality of the food is considerably better than most of the places in Davis.  However, the big screen TV and the decor make for a pretty not so comfortable atmosphere.  They are a fairly new restaurant, and the owners are there to please, very nice people.



