
El Macero, Mace Ranch, and Mace Boulevard are all named after Bruce Mace, and the land all three are on was once part of his ranch. Mace initially conceived the idea for El Macero and he and his sons plotted its development. El Macero was one of the earliest developments in a previously rural South Davis. It is technically not part of Davis.

