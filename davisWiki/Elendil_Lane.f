Address(Elendil Lane) is one of the many Navigating Davis streets in Davis Village Homes neighborhood that are named after places or characters in J.R.R. Tolkiens books, The Hobbit and The Lord of the Rings.

Elendil Lane branches off from Evenstar Lane.

Elendil is a noble human who fights Sauron (he is seen briefly in the prologue to the Fellowship of the Ring film). Sauron smites him with a mace and breaks his sword, Narsil, in half. Prince Isuldur then graps the broken sword and slices through Saurons finger, separating him from the One Ring and a great deal of his power.
