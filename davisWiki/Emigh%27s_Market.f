

Emighs market is focused on selling premium, local food local lamb and beef products, many of which come from the familys ranch in nearby Rio Vista. They also offer a small selection lunch and breakfast items and have daily rotating sandwiches sandwich specials. Theres some odd and ends specialty items, but the focus is really on quality, local meat.

You can see this place driving by on I80. Take the Pitt School Road exit. The sandwich prices are pretty reasonable.




