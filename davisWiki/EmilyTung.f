ASUCD is a parttime government at best. It is difficult for students to fully dedicate themselves to the promotion of ASUCD without neglecting their studies, sports, jobs, other clubs and internal ASUCD work. Some roadblocks that come up in outreach are: outreach is not an attractive issue that students want to dedicate time to. Most student want to get involved with environmental, cultural, entertainment, social justice issues as a primary passion and maybe outreach as a secondary. And usually if they do want to do outreach it is for their primary passion and not just for ASUCD. Second would be advertising. The Aggie does not cover ASUCD and does not give it discounts to advertise. Also the ASUCD budget is declining and the scarce resources are being fought over existing programs, thus new money for new promotions is hard to come by.
