20060218 20:36:12 nbsp You run a pound of chicken fat (body cavity is best) through the grinder, then either 1. stir well into cold water in a pot on a stove, or 2. put in the top section of a double boiler.  In either case bring the water up to just below a simmer; keep it that hot until all the oil has rendered out of the fat.  Strain then pour into bowl or jar and refrigerate.  The solid cake of fat on top is what you use for the piecrust, the crud and stuff will sink into the water.  Chill it well (near freezing) before using.  Its crumbly and melts quickly, so you have to work fast to get the piecrust together. Users/BlancheNonken
