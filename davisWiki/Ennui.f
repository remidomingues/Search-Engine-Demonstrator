Typical causes of Ennui
  Hot Summer summers
  Outskirts Extremely flat terrain
  A meager night time night life
  An overabundance of New Again family oriented public art
  The unfortunate lack of a RoboChair
Strategies for Combating Ennui
  Start a music scene band
  Commit some Urban Art vandalism
  Join the Craft Center
  Volunteer at KDVS
  Ride Critical Mass
  Go Swimming pool hopping
  Make some new Inanimate Objects with Amusing Faces friends
  Update Daviswiki with new Photo Requests pictures
