Biography:

Enrique J. Lavernia, formerly chair of the Department of Chemical and Materials Science at the UC Irvine University of California, Irvine, was appointed Deans Dean of the College of Engineering at UC Davis in September 2002. He served as UCD Administrative Officers interim provost and executive vice chancellor from January 2009 through November 2010, whereupon he returned to his Deans post. Dean Lavernia holds a masters degree and a Ph.D. from the Massachusetts Institute of Technology and a bachelors degree from Brown University. He joined the faculty of UC Irvine in 1987, becoming department chair in 1998. He has published more than 270 scientific papers as well as conference proceedings, invited papers, book chapters, and other publications. Among many other awards and honors, Lavernia was elected as a fellow of the American Association for the Advancement of Science in 2000, and was selected as Chemical and Biochemical Engineering Materials Science “Science Teacher of the Year” at UC Irvine in 1998. He has held prestigious fellowships from the Ford Foundation; the Alexander von Humboldt Foundation; the Iketani Science and Technology Foundation of Tokyo, Japan; and Rockwell International. He was named a Presidential Young Investigator by the National Science Foundation and received a Young Investigator Award from the Office of Naval Research. He is a member of many professional organizations, actively participates in organizing national and international conferences, serves on various boards of review and advisory panels, and has six patents and pending applications. He and his students have received numerous scholastic awards. Lavernia has held grants from agencies including the National Science Foundation, the Office of Naval Research, the U.S. Department of Energy, NASA, and the Air Force Office of Scientific Research.
