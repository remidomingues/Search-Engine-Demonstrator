
wiki:wikipedia:Entomology Entomology is the study of bugs and insects.  The entomology department at UCD belongs to the College of Agricultural and Environmental Sciences.  The department offers Majors major, Minors minor and Graduate Groups graduate programs (PhD and masters) of study.  According to the http://chronicle.com/stats/productivity/page.php?bycattrue&primary9&secondary69&year2007 2007 Chronicle of Higher Education rankings, UCD has the #1 ranking entomology department of all PhDawarding research institutions in the country.

