

wiki:wikipedia:Horticulture Horticulture is the study and practice of plant cultivation.  The EHUF department at UCD focuses more on ornamentals than edibles, since other fields, like Pomology pomology and Viticulture and Enology viticulture tend to be explored in other departments.

Degrees
EHUF is one of the many majors offered for undergraduates.  Majors must choose one of four specializations: Urban Forestry, Floriculture & Nursery Production, Landscape Management, and Plant Biodiversity & Restoration.  A minors minor is also available.

The department also works with several graduate groups, including Horticulture & Agronomy.  Students can earn either a masters or doctorate degree.

