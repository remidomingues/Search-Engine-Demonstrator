

Environmental Policy Analysis and Planning (also known as EPAP) is one of two majors within the Department of Environmental Science and Policy, which belongs to the College of Agricultural and Environmental Sciences.  The department also offers a minors minor in EPAP.

The major is a mix of politics, science, economics, and law, all revolving around an environmental theme.  Students focus on an area that they would like to specialize in. They are advanced policy analysis, city & regional planning, energy policy, transportation planning, and water quality planning.



