
The Environmental Science and Policy Club is an Environmental Organizations environmental student organizations student group that encourages students to be active in their environment through outdoor activities and conservation efforts and serves to familiarize students with the Department of Environmental Science and Policy and faculty.   Many members are Environmental Policy and Planning majors, though not all.  They do things like Lake Berryessa Lake Berryessa cleanups and hiking trips.

They have meetings every other Monday at 6:30PM in Wickson Hall Wickson 2120J.  Sometimes they give you tasty pizza.

