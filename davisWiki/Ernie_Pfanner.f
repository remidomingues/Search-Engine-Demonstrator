Ernest J. Ernie Pfanner was a former member of the Board of Education DJUSD Board of Education (1981 to 1985) and of the Yolo County Board of Supervisors.  He was also active in veterans issues.  He died in 2001 at age 75.


Source:

Lacy, David; 1 August 2001, Activist Pfanner dies at 75, Davis Enterprise, pg. A1.
