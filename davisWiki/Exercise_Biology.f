http://www.exb.ucdavis.edu/index.cfm Exercise Biology is a program in the Division of Biological Sciences focusing on the study of exercise physiology, biomechanics, and psychology.

This department offers an easy and fun intro class called EXB 10 where one learns the basic science behind working out.  Its considered by many to be comparably bettern than its sister class Nutrition 10.  The classes in sports sociology and sports psychology are also considered to be easy and interesting upper division classes, with no prereqs, that satisfy GE requirements.


