
Farm on Putah Creek offers a peaceful country side setting with both indoor and outdoor facilities for rent. The Farm is an excellent place for activities including tours, meetings, seminars and workshops, retreats, school field trips, educational field days and service learning projects.  As of 2012 weddings are no longer held at the Farm on Putah Creek.

Celebrations, Events and Parties at the Farm on Putah Creek

Birds sing in the shade of centuryold olive trees. After sunset, frogs and crickets beyond the lawn take up the singing in different keys. Surrounded by walnut orchards, gardens, hedgerows, and waterways, the Farm on Putah Creek is an oasis for citydwellers seeking quiet.

