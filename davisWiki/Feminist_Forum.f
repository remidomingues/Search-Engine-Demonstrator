
The Feminist Forum is a Student organizations student group based out of the UC Davis School of Law.  They address legal issues regarding women and support/guide women interested in law.  Those interested in human rights and social justice should look into this group.

 Events 
Past events have included looking at different interpretations of the constitution in regards to abortion and raising money to send King Hall spring break delegations. Delegations include a Gulf Coast delegation to help Katrina victims and a USMexico Border
delegation to study the militarization of the border and femicide in Ciudad Juarez.

The next event theyre putting on is International Womens Week.

