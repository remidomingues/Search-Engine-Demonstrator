
Fer gets annoyed when people pronounce the shortened version of his name Fur.  Fer does not appreciate racial stereotypes, especially pictures of Mexicans wearing large sombreros.  Fer prides himself on his perfect eyesight.

Fer works at Ben & Jerrys, where you may find him wearing large sombreros or wearing the occasional cow costume.

Sadly, Fer no longer works at Ben & Jerrys, but he still enjoys dressing up as a cow.

Fer is the Native American Community Intern at the Cross Cultural Center.  He also works with the Chicano Studies Program in Hart Hall.  You may see him running frantically across campus from time to time.  Dont hesitate to say hi; just dont call him fur.
