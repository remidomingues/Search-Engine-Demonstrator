The FilAm (Filipino American) community at UC Davis is comprised of seven Filipino American organizations: Bridge, Chi Rho Omicron Chi Rho Omicron, Inc., FILAH, Kappa Psi Epsilon, Mga Kapatid, FAHC, and Pilipino Americans in Science and Engineering PASE.

The FilAm community works together to provide events relevant to current and historic issues within the Filipino and Filipino American community.

Annually, the FilAm at UC Davis hosts Icebreakers (September/October), FilAm Thanksgiving Dinner aka FATDs (November), Spring Fling (March/April), FilAm Leadership Retreat (May), and FilAm Golden Games (June).


