

The Film Studies Department at UC Davis is one of several University Departments departments in the College of Letters and Science.  The department offers a Majors major and a minors minor.

 The Program 
This interdisciplinary major emphasizes film history, criticism and theory, but students also have the option to explore film/video production. Students have the option of completing a senior thesis (either a written paper or an original film/video) within the emphasis if they have a GPA above 3.5. To see a list of courses for the major click on the departments website.


 Events 
The Film Studies department cosponsors the UC Davis Film Festival (with the other sponsor being the Department of Theatre and Dance).

