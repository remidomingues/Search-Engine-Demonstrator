Finley Fryer is a professional artist born August 18, 1952 in Santa Monica, California.  He attended UC Davis in the 1970s. Several of his works were installed  downtown in 2012 as elements of the Transmedia Art Walk. Beyond Davis, he is wellknown for his four large creations that were displayed at the Burning Man festival.

