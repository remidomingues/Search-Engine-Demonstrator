The First Church of Christ Scientist is a local spiritual organizations spiritual organization.  They are a branch of The First Church of Christ, Scientist, the worldwide organization, based in Boston, Massachusetts, USA.  Members of the Davis Church hold public worship service services on Sunday mornings at 10:00am and hold concurrent Sunday School classes for children and young people.  A Wednesday evening service includes time for testimonies from the congregation, each week at 7:30 in the evening.  The Davis Church operates a public reading room in the Court and Cedar Building in downtown Davis (corner of 3d and G Streets).

For more information about Christian Science, visit the Reading Room or attend a service.

They are associated with the Christian Science Reading Room.



