This entry is about a department of Campus Recreation.  If you are looking for information on the fitness and wellness in general, try the gyms & fitness or medical services.


Fitness & Wellness is one of the many departments that make up Campus Recreation. Fitness & Wellness provides group exercise, martial arts, dancing dance, personal training, nutrition services, Fit For Life, Aggies on the Move and most recently small group training to students of UC Davis as well as ARC members.

During the first week of every quarter Fitness & Wellness, in conjunction with the ARC, allows free access to ARC as well as any classes provided by Fitness & Wellness.
