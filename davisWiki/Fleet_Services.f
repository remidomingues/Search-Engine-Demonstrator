
Fleet Services manages all UCD owned vehicles. If youve ever got a campus job, and your boss tells you to fill up the tank or wash the car, you go here. They also operate the Davis Berkeley Shuttle, transporting passengers and materials between Davis and UC Berkeley.

University Departments and organizations such as ASUCD can rent vans from Fleet Services.  However, if you have a larger group of people, you should probably get a small or large bus from Specialized Transportation Services.

Governor Arnold Schwarzenegger came here to give a talk about the Hydrogen Highway Network.
