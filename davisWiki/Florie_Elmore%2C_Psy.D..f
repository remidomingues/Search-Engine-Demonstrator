

 I believe that stories and beliefs provide lenses through which we see and experience the world. Each reality is unique. When one is hindered in their ability to work, play, or love it may be due to hunger for more nourishing stories and beliefs. I also believe in love as curative, so therapy is about removing obstacles to intimacy, with yourself and others, which often involves grieving and facing fears.

 The environment I create is caring, challenging, accepting, and honest. There is room for humor, despair, and mistakes. I love my work and am tremendously hopeful about our power to change.



