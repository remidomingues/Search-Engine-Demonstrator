Floyd Fenocchio was a long time administrator in the Davis Joint Unified School District.  He was principal of Birch Lane Elementary School in the late sixties, became principal of West Davis Intermediate, then had positions at the district office, culminating with his tenure as Superintendents of the Davis Joint Unified School District Superintendent of DJUSD from 1996 until his retirement in 1998.

Fenocchio  received Davis Senior High School Hall of Fame recognition in 2012.

