


Flutter and Hum is a town art mosaic sculpture created by local artists Davis artist Mark Rivera.  The installation was completed in September 2008 and the sculpture features a California Pipevine with a Pipevine Swallowtail Butterfly fluttering overhead and a caterpillar eating a leaf of this larval food plant, a Purple Coneflower with a Buckeye Butterfly sipping nectar from its flowers, and Columbine flowers providing nectar to a visiting Annas Hummingbird.

