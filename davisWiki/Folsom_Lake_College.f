
Folsom Lake College is the 4th member of the Los Rios Community College District. Classes are offered through the main campus, offsite locations which include various middle schools around Folsom, the /El Dorado Center El Dorado Center in Placerville and the /Rancho Cordova Center Rancho Cordova Center.

http://flc.losrios.edu/about/campus_maps/index.html Map to FLC, EDC and offsite locations.

Folsom Lake College offers guaranteed transfers to UC Davis, UC Riverside, UC Santa Cruz, UC Santa Barbara, UC San Diego, California State University, Sacramento, San Francisco State, and University of the Pacific.


