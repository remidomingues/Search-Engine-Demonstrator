The Food Championship (formerly the Food Olympics) is an annual event put on by the UC Davis Department of Food Science & Technologyand the http://fsgsa.ucdavis.edu/ Food Science Graduate Student Association (FSGSA). In the past, teams have competed in events such as an wiki:wikipedia:Iron_Chef Iron Chefstyle Culinary Combat, field events, food art, and food trivia contest. The events are held in the Robert Mondavi Institute for Wine and Food Science Robert Mondavi Institute (RMI) and open to spectators at no charge.

