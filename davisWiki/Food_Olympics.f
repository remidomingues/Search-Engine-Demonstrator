#redirect Food championship
The Food Olympics is an annual event put on by the UC Davis Department of Food Science & Technology. In the past, teams have competed in events such as an wiki:wikipedia:Iron_Chef Iron Chefstyle Culinary Combat, field events, food engineering competition, and food trivia contest. The events are held in in Cruess Hall Courtyard and open to spectators at no charge.

