Those rare students who are doing a year abroad in Davis.

Reciprocity Students, also called Foreign Exchange Students, can come from any University that the http://eac.ucdavis.edu UC Education Abroad Program exchanges with, or, really, any University at all.  If UC Davis and the prospective Reciprocity Students University dont have an exchange program, the student can contact a NonUC program for help.  They can also apply and enroll directly into the UC Davis as a transfer student, which requires a student visa.  There are also foreign exchange students at the high school level.  Many stay with local host families for various periods of time.

