

Keith Grzelaks Internal Arts offers relaxing and therapeutic bodywork as well as training in Chinese internal martial arts.

Keith Grzelak offers bodywork in a blend of relaxing Swedish and deep tissue massage. Keith received and continues to receive his bodywork training at the Massage Therapy Institute in Davis, Ca.  Keith also incorporates his training in the Eastern Arts into his practice.

Keith offers private instruction in the Chinese Internal Martial Arts of Taiji Quan (Tai Chi) and Xing Yi Quan (HsingI).  More information on these arts and Keiths background is available on the website listed above.

