
Frailty was a LARP liveaction roleplaying game (LARP) set in http://www.whitewolf.com White Wolfs World of Darkness.  The StoryTeller was Erik Bob Berman. Frailty ran every other Saturday outside of Wellman Hall.

What would you find when you show up?  Friendly people playing pretend, and willing to help you figure out the rules and have a fun evening.  Its essentially improvisational acting with out an audience.  Want to know more?  Show up and ask questions.  The players are always willing to stop and let you know whats going on.

Erik Berman is also the storyteller for Silent Throne Silent Throne, a Changeling: the Dreaming LARP run in the Death Star Death Star.

