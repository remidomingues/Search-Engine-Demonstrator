Francis E. Russell (  February 24^th^, 1907). Sailed in 1849 from Boston around Cape Horn to San Francisco. Arrived in Yolo County in 1858. Married Lucy Ogborn.

670 acres six miles west of Davis that later grew and became the Russell Ranch. Grafted English walnuts onto the black walnut trees on his farm. Twenty acres of almonds.


Son William O. Russell

Son Frank E. Russell moved to Alameda.

Daughter Mrs. Ellen Enos.

Daughter Mrs. Mary Lowe.

Daughter Mrs. Maud Henle died in May 1907 near Davis.
