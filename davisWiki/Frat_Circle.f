Frat Circle, otherwise known as Parkway Circle, is the area across from the ARC along La Rue Road and houses three fraternities and one sorority. Although Davis does have Frat Row, it is not a solid block and more so a street with a few fraternity houses along it, whereas Frat Circle is a concentrated area consisting of only greek life Greeks. Parties and other events happen regularly.

By the way, this is where its at on Picnic Day.

 Fraternities 
  Sigma Phi Epsilon (SigEp)
  Sigma Chi
  Tau Kappa Epsilon (TKE)

 Sororities 
  Kappa Alpha Theta
