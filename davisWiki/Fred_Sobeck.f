Fred Sobeck (born 1927, Pittsburgh, Pennsylvania; died 20 June 2006, Davis, California) enlisted in the Navy in 1945, enrolled in college the following year, and graduated from UC Berkeley the following year.  He graduated from UC San Francisco with a medical degree in 1953 and moved to Davis in 1957.  For 40 years he practiced family medicine in the community.  He also served on the Planning Commission Davis Planning Commission and the School Board Davis JUSD School Board.

Source:

2006 June 22, Obituary: Frederick J. Sobeck, Davis Enterprise, p. 2.
