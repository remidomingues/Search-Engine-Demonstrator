
Fresh Breeze Sangha is a mindfulness practice group with which you can have a peaceful sit on Thursday evenings.
All levels of meditation experience welcome.

Please contact John SalernoWhite ( lotusforyou@comcast.net ) before your first visit so that we may offer a helpful orientation.

Our teacher (Thich Nhat Hanh) encourages practitioners to maintain their nonBuddhist Religious and Spiritual Organizations spiritual roots.

No fees.  Dana accepted to help pay for rent of Redwood Building.

Sangha will gather on most Thursdays.

Fresh Breeze Sangha can also be found on Facebook.
