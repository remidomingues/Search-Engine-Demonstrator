In the early days of the University Farm, the junior class would annually publish the freshman edict.

Heres an example from the September 22, 1916 edition of the Aggie. Yes, commandment #8 is missing. The Aggie left it out or something.

FRESHMEN
Freshmen, listen to the warning, ye poor YOUNG LAMBS, ye will observe the following 10 commandments at all times, for they are for your knowledge and are never to be forgotten.

Listen and read, for any one of you UNLUCKY BABIES who dare overstep any of the provisions provided herein will be seen and dealt with in a manner that we see fit.

Freshies, You Will Observe
1. Thou shalt worship at all times, places, and under all conditions—with all thy heart, thy soul, and without any premeditation or mental reservation—the class of 18 by lifting your hat and whatever is ordered of you by your superiors.
2. Freshies: Ye shall at all times govern your wearing apparel as past customs have dictated from time immemorial: namely; a straw hat shall adorn thy brainless pates. Also, thou shalt not at any time be seen in corduroys.
3. Thou shalt at all times answer the call to clean the swimming tank.
4. Thou shalt be prepared to carry and rustle wood for all bonfires.
5. Thou shalt not queen on or near any part of the University Farm campus.
6. Thou shalt not smoke; unless it be a corncob pile.
7. Thou shalt not display upon they scrawny forms any Sickly Signs of they former prepdom (no block letters).
9. Thou shalt at all times while upon the campus, pursue thy way upon the proper walks provided for pedestrians—hence, no short cuts.
10. Here ye also, Weaklings, under no circumstances let Stanford RED of any kind be found on or around ye—let it be BLUE AND GOLD—THIS IS CALIFORNIA.

Fear the Tank Rush for Ye Will Die
Verily, verily, we say unto ye, unless ye keep these commandments, it shall come to pass that vengence swift and terrible will be wreaked upon thee by thy patron Gods of the class of 18.


