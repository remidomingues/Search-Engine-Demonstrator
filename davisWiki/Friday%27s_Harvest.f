
Every Friday afternoon the Davis Korean Church offers bags of produce to the community for a $1 donation. The produce is seasonal, but may include corn, potatoes, tomatoes, yams, etc. These are standard grocerysize bags, onethird filled (about 10 pounds), and they all have the same items. Cant beat the living cheaply value.

They celebrated their One Year Anniversary on 20100806. The celebration included extra goodies such as cherry tomatoes from Grace Garden, cookies, popcorn, baked treats, snow cones, and a towel in each produce bag. Yolo County Food Bank had a tent too.

