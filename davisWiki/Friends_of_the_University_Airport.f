
Friends of University Airport formed in 2002 when it became apparent that UC Davis growth plans would effectively close the University Airport. In less than a year, the group incorporated under IRS code 501(C)(4) as a notforprofit organization.

The two main purposes are to preserve and enhance University Airport.

FOUA membership is no cost. Executive meetings are usually held Wednesday 6:00 pm each Quarter  anybody may attend. Watch upcoming events for exact date.

