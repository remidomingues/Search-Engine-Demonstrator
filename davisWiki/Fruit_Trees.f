Visit http://maps.google.com/maps/ms?hlen&glus&ieUTF8&msa0&msid101873520512973484071.0004444583b8984b4b0eb here for a google map of fruit trees in Davis.  The map is public and collaborative, so please add trees if you know of their locations.  (You must have a google signin.)  Use pushpins to mark the locations of trees.  If the tree is on private property, please indicate that in the note, and only mark it after getting permission from the owner first.
Update: the new searchable global fruit mapping site http://fallingfruit.org now includes all the data from the above map  check it out, and go there to add more! It also includes tree data from municipal tree records, so many trees in city parks are listed.
You can harvest http://redwoodbarn.com/ripeningchartmacro.htm fruit yeararound in Davis!

There is a volunteer opportunities volunteer organization called Village Harvest that harvests fruit to give to homeless and lowincome individuals. The fruit is picked from private properties in Davis after the organization receives permission from the owners.

