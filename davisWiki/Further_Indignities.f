Further Indignities was the local Rocky Horror Picture Show Shadowcast.  They performed throughout northern California, including at Campus Cinema at UC Davis.  In addition, since the Users/JabberWokky director lived in Davis during the latter half of the casts existence, auditions and rehearsals were usually held here.


They succeeded the previous cast Under Sedation.  Several Under Sedation members were active in Further Indignities, and some FI cast members still perform in the Bay Area casts.



