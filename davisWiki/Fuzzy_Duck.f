An incredibly juvenile Drinking games drinking game played as follows:

We go clockwise around the circle saying fuzzy duck in turn. When it is your turn, you have to say fuzzy duck. If you screw up or you werent paying attention, then you drink.

When it is your turn, you have the option of saying does he? instead, in which case the direction around the circle changes to counterclockwise, and we start saying ducky fuzz. If we were saying ducky fuzz and you say does he? then the direction around the circle changes to clockwise, and we start saying fuzzy duck in turn.
