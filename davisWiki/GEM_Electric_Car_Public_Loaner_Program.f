
The City of Davis had a program where they would loan you an electric vehicles electric car for a week after taking a class and meeting various requirements (e.g. insurance, parking). The cars were part of the citys GEM car fleet. The loaner program was discontinued because of declining interest and vehicle maintenance issues.

It is discussed in an old (Nov 3, 2003) California Aggie http://www.californiaaggie.com/news/2003/11/03/FrontPage/CityPromotes.Use.Of.Electric.Vehicles.Through.Lending.Program1315681.shtml article and on the citys http://cityofdavis.org/pcs/about/fleetsvcs.cfm fleet services page.

In the spring of 2008 the city http://www.cityofdavis.org/pdfs/GEM_auction_Release1.pdf auctioned off some of these vehicles.


