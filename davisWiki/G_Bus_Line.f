

The G Bus Line is a Unitrans route servicing Anderson Road, Alvarado Avenue, and Sycamore Lane.  It provides service four times an hour during the day to the MU terminal.  At the bottom of the hour DoubleDecker Buses doubledecker buses are usually providing service to the MU.

Considering that North Davis has the highest population density out of any area of Davis, it makes sense that this line has the highest ridership of all of the Unitrans lines and is subject to frequent overcrowding at peak hours. In 2004, there were over 800,000 oneway trips recorded on the G Line (at which time it ran both to the Silo and MU Terminals). Passengers outbound from Campus who need the Anderson Plaza stop are encouraged to take the F Bus Line F line which is almost always less crowded.

Extra buses (tripper buses) are often sent out on this line in the mornings, to help pick up the most popular stops, such as the Almondwood, La Salle and the Congregation Bet Haverim Synagogue (formally Church) stops. This helps keep the buses running on time, and the passenger loads down to a managable level. This is unfortunately not possible in the afternoons.

Check out the schedule and route map at the http://www.unitrans.ucdavis.edu/g Unitrans website for more information.

