

Gardenias is Departed Businesses closed. It has been replaced by New Delhi Chaat Cafe.

Gardenias replaced Rositas Cocina, an Authentic Mexican Food restaurant. Perhaps the location is cursed, but it was rarely crowded  and perhaps is even less crowded now as Gardenias. The Mexican Food is decent, although somewhat pricey during dinner.  However lunchtime specials can be gotten for about $5. There are fewer salsas than there used to be as Rositas. (For the best selection of Salsas in town visit either Dos Coyotes or El Mariachi.)
