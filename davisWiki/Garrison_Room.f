
The Garrison Room is located on the second floor of the Memorial Union. It overlooks the second story patio. It is south of the Fielder Room so it enjoys two walls of windows.

The Campus Unions page has http://campusunions.ucdavis.edu/mumap.html maps of the building to help you locate the room.

Groups that Regularly Meet in the Garrison room
  Internal Affairs Commission
  External Affairs Commission
  Ethnic and Cultural Affairs Commission.


