

The genetics program at UCD covers a classical genetics approach and a more molecular based approach to give students a good well rounded view of the field.  The majors major was restructured in 2004, and the requirements were trimmed in some areas.

It is often said that UC Davis has the Number 1 ranked undergraduates Undergraduate Genetics Program in the nation.

Genetics core courses are offered through the Molecular and Cellular Biology department in the Biological Sciences Division of Biological Sciences.




Genetics is quickly becoming a subject that relies heavly on computers and mathematics.  Hence, one of the most popular minors for genetics majors is Quantitative Biology and Bioinformatics.  This minor can be tailored to completely focus on how one uses math and computers to solve current problems in genetics.
