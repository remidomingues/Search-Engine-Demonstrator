

The Genetics Club at UC Davis is dedicated to the promotion of the field of genetics and aiding students who are interested in this growing area of study.

We aspire to organize and facilitate activities including  but not limited to  the following:
  leading discussions on the latest findings in genetic research
  presenting employment/internship opportunities
  organizing visits to area biotech companies
  informing students of graduate school opportunities and requirements
  serving as a link for inquiries regarding genetics and the genetics major
  discussing ethical issues related to advances in human genetics
  arranging presentations by guest speakers relevant to the field of Genetics
  planning social meetings for networking
  communicating with other UCD students majoring and/or interested in Genetics

