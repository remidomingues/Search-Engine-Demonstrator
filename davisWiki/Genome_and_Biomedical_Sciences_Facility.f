

The Genome Center (GBSF) opened in Fall of 2004 as an addition to the Health Sciences District.

It is a six story building designed to bring various labs across campus together into one central environment for genomics on campus. The center houses facilities for DNA Technologies, Proteomics, Expression Analysis, Metabolomics, and Bioinformatics, and is also home to Pharmacology and Biomedical Engineering.

This building is the tallest building in the Health Sciences District.

The basement houses a large number of BSL3 (Biosafety Level 3) labs, much to the dismay of the NIMBY population of Davis. There used to be a Genome Center Cafe on the ground floor, but this was converted to office space in 2013.

