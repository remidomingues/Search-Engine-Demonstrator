
Geohashing is a method for finding an effectively random location nearby and visiting it, i.e. a Spontaneous Adventure Generator. Geohashing was brought to you by the http://www.xkcd.com xkcd webcomic. http://wiki.xkcd.com/geohashing/Main_Page Geohashing Wiki

Geohash points are generated based on the date, the most recent Dow opening, and the location.  The Sacramento regions geohash point for the current date can be seen at http://irc.peeron.com/xkcd/map/map.html?lat38&long121&zoom8.

 Geohashing in Davis 

Davis is in the Sacramento graticule (38, 121), which ranges from Woodland in the East to as far West as Folsom, North past Auburn, and South almost to Stockton.  Sacramento is nearly centered and Davis is slightly toward the West edge.  Depending on your enthusiasm, energy, and spare time it is possible to reach many parts of the graticule by bike and/or public transit.

As of now (May 2009) there seem to be few active Geohashers in the region.  The http://wiki.xkcd.com/geohashing/Sacramento%2C_California Sacramento page on the Geohashing wiki is seldom updated.

