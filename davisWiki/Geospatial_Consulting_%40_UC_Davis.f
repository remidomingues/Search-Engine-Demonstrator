

Geospatial Consulting aids researchers at UC Davis and the surrounding community with the geospatial aspects of their research. The consultants have experience with many aspects of geospatial science, including map making (paper or web), spatial analysis, data preparation, spatial statistics, and many others.

Geospatial Consulting is a division of the http://ice.ucdavis.edu Information Center for the Environment in the Department of Environmental Science and Policy.  It is supported through funds collected completing projects at a rate set by the university.


