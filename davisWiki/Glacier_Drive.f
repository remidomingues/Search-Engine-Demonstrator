Address(Glacier Drive) is a street in West Davis.  It is home to Saratoga West Apartments and Glacier Point Apartments.  Across the street from Saratoga, the Aspen Greenbelt runs south all the way to Patwin Elementary School, passing a couple of playgrounds and a small dog run.

