

Global Brigades is a studentled, NonProfit Organizations nonprofit global health and sustainable development organization with nine different skillbased sectors. University students and professionals travel abroad to improve the quality of life in underserved and underresourced communities on weeklong brigades. Currently, the organization has established relationships with Honduras, Panama, and Ghana.

In 2009, undergraduate Jessica Jaswal introduced Global Brigades to UC Davis, starting with the Medical sector. Since then, seven more of the nine skillbased sectors have been founded on the campus. As of the 20112012 school year, there are eight active sectors at UC Davis:

  Global Medical Brigades
  Global Dental Brigades
  Global Public Health Brigades
  Global Water Brigades
  Global Business Brigades
  Global Environmental Brigades
  Global Human Rights Brigades
  Global Architecture Brigades


