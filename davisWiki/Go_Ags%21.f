Go Ags! a common phrase used to express, well, UCD pride.

Its also a statement that The California Aggie has been proven to loathe.

Case in point:

Rob Roy wrote a http://www.californiaaggie.com/media/storage/paper981/news/2005/06/02/Opinion/Letters.To.The.Editor1320792.shtml letter to the editor, which ran on June 2, 2005. The original version ended with the phrase Go Ags! (and was exactly 200 words) but the editor of the Aggie chopped those two words out.

During Lee Weissmans dark reign upon ASUCD he made every candidate running for office end their statements with Go Ags! due to some sort of belief that it summoned unholy magick.  It is likely that it is the memory of Lee Weissman which cases the Aggie such loathred for the phrase.
