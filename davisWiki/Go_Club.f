Looking for a club that plays the game?  See the DavisSacramento Go Club


goClub is the Alternative Transportation Program for UC Davis, administered by Transportation And Parking Services TAPS.  It has various programs in support of carorvanpooling, biking, taking the train, busing, and walking to campus.  Benefits vary by program, but can include access to showers at the ARC, discounted Unitrans bus passes, free campus parking passes, and more!

Its best to sign up right at the start of the school year, as you only receive a free day of parking for the remaining months of the academic year.

Pretty much, if you live in town and you work on campus, you should be looking into one of these programs.




