


Goaltimate is a wonderful sport  it is a bit like Ultimate Frisbee, but far better. Ben originally brought Goaltimate to Davis from San Diego and he can still be spotted under the PVC hoop at various parks around town. The exact rules are not always the same (Ben is often changing them), nor is the playing time or the playing location very consistent, so no attempt at documenting either is provided here.  People interested in the game can join the mailing list to find local locations and times.


Many ultimate players play goaltimate over the winter, so it can be a good way to meet local players of both sports.
