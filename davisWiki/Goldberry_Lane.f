Goldberry Lane is one of the many Navigating Davis streets in Davis Village Homes neighborhood that are named after places or characters in J.R.R. Tolkiens books, The Hobbit and The Lord of the Rings.

Goldberry Lane branches off from Bombadil Lane.

Goldberry is Tom Bombadils wife, daughter of the river Withwyndle. She is the embodiment of spring, dressed always in silvergreen clothes with dew drops.

