Golden Heart Awards are given by the City of Davis to outstanding 7th12th graders and were created following the Tragic Events death of Andrew Mockus in April of 1992. Typically, up to five Teenagers teens are selected based on recommendations by school counselors and Teachers in the Davis Joint Unified School District teachers. The award is defined by two categories: Community Service service to community and overcoming personal challenge. http://cityofdavis.org/cs/teens/goldenheart.cfm Nominations are due around January.


