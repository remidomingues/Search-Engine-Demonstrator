
Good Neighbor Lawn Care is a landscaping service out of West Sacramento  locally owned and operated there  that services Davis.  They provide free estimates for yard care for residential and small commercial properties, including flowerbeds and hedge trimming. Weekly, bimonthly, onetime, and vacation services are available, and lawn clippings are mulched. In the case of rain or wet grass, they will service on the following day.  Services provided include mowing, trim and edgeing, fertilizing, weed control spraying and Spring and Fall cleanup.

