
Euphorbia lathyris, commonly known in this area as gopher weed or mole plant, is a hardy annual herb originally from Africa. The upright unbranched stems carry four alternately arranged leaves, and in flower the stems are topped with chartreuse gold flowers surrounded by bright green bracts; later the plant produces a large, threelobed capsule fruit. All parts of these plants emit a milky sap when broken, and all parts of the plant are mildly toxic when ingested or on contact  symptoms include nausea, vomiting, diarrhea when ingested and minor skin irritation on skin contact. The common name comes from the belief that it repels moles/gophers from your garden; I dont recall it helping do so in the EC Gardens!


 

