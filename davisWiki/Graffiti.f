Graffiti is a term describing markings (drawings, words, letters, etc) on property.  It is considered Vandalism when the property marked is someone elses property.  Tagging is frequently considered a common subtype of Graffiti.

The City of Davis has http://cityofdavis.org/cmo/citycode/chapter.cfm?chapter25 Municipal Codes relating to Graffiti, and the Davis Police Department Police Department has 24 hour Graffiti hotline to call in order to get graffiti removed: (530) 7575600.

Some see graffiti as a form of urban art and feel that its potential for artistic merit is cause for celebration.


