

On February 14th, 2008 a giant Foothill/Gray Pine gradually began to tilt, eventually coming to rest on the Kaplan Educational Center building in Mansion Square forcing the http://www.cityofdavis.org/pdfs/pinetree_21408.pdf closure of the 100200 block of E Street. The tree will be removed but Mansion Square businesses will be closed in the interim. The tree was one of the oldest tree in Davis and was one of the citys Landmark Trees.

