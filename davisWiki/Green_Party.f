The http://www.gp.org Green Party is a national political party. According to a May 2008 report from the Yolo County elections office, the city of Davis has 791 registered Greens, or 2.27% of registered voters.Footnote(Yolo County Elections Office. http://www.yoloelections.org/voting/stats/sfiles/052208%20report%20of%20registration.pdf Report of Registration  State Reporting Districts. 20080522.)

Currently, no local representatives are members of the Green Party.

