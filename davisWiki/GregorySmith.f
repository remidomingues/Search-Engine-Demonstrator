Gregory Smith, or Greg, used to live at the Lefant House before graduating from UC Davis and moving to Santa Cruz, CA.

Greg played bass, then eventually sang for the Davisbased hardcore band Keep Your Distance.

Greg directed and appeared in three Robotmedia films:

1.  Uncomfortable Moments for Showcase VI (Uncomfortable Moments)
2.  Mon frere... for Showcase VIII (Malfunction)
3.  A Day In the Life of Russell Smith for Showcase X (A Day In the Life...)

