



Hair Saloon became Salon at Maple Court after the building was remodeled in 20089 and the atmosphere was transformed with the sunny new interior.

Haircut + shampoo prices range somewhere between $30$50.

Sharon Gamble opened the Hair Saloon in 2003 with her friend Sherri Cowden.  However, after Ms. Cowden sold her share of the salon, the salon personnel consisted of Sharon, Cecilia Scott, and David Martin. Tenaya Freitas joined the staff for the final year of its the salons history. Sharon, Cecilia, David, and Tenaya all have extensive training in cuts, color, colorcorrection, and hair texturizing.





A short documentary telling the story of business owners Sharon Gamble and Sherri Cowden and their dream to own a hair salon in Davis, was featured at the http://www.davisfilmfest.org/2005/films.htm 2005 Davis Film Festival.  The film is called “Common Hours,” by Elaine King.




