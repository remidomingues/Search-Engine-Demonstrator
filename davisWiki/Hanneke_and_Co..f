Hanneke and Co. is a Davis, CA ballet company run by Hanneke Lohse.

We put on two shows a year, Nutcracker over Thanksgiving weekend and a late spring production of La Sylphide, A Midsummers Nights Dream, Coppèlia, or La Fille Mal Gardée.

We dance at the Davis Art Center and perform at the Veterans Memorial Theatre.

For the most current information on upcoming productions, see Hanneke Lohse.
