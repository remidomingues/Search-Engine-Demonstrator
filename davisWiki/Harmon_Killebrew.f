Alright, heres the deal. Harmon Killebrew is a namedropping drinking games drinkin game named after the 1969 American League MVP and Hall of Famer wiki:WikiPedia:Harmon_Killebrew Harmon Killebrew.


GETTING STARTED

For this game, it is best to have everybody in a circle, preferably around a table. I suggest drinking beer, as other drinks can be associated with aftergame disasters.

To begin, the first person says the name Harmon Killebrew.

The next person clockwise must then think of a first name beginning with K, preferably a celebrity or sports star.

This continues with each person naming names whose first names correspond with the first letter of the previous last name.

One word names and names with the same letter (such as Madonna or Burt Bacharach) make the whole process switch directions (ie clockwise to counterclockwise).

The goal is to get back to the name Harmon Killebrew, in which case the next person has to finish their drink (hence the name).

Last but not least, you must also drink while you think of a name. Smallish sips are allowed.

