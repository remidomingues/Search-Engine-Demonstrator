The Harmony in Our Lives Award is given annually by the West Valley Chorale Barbershop Chorus at the  at the fall Harmony in Our Lives Concert to individuals or organizations who have been significant, longterm contributors to the musical enrichment of the Davis community.  The Harmony in Our Lives Concert is a fundraising event for the Davis School Arts Foundation.  Nomination deadline is usually late September.  The plaque honoring recipients is displayed in the lobby of the Veterans Memorial Theater.

