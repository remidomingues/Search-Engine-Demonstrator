
Harriet Steiner graduated from SUNY Binghamton with a Bachelors degree in Anthropology and the University of New York at Albany with a Masters degree in Library Science. She received her Juris Doctorate from McGeorge School of Law and was valedictorian of her class. Ms. Steiner is a member of the Order of the Coif, a national legal honor society.

Ms. Steiner has been City Attorney of Davis since 1986. She is currently a partner in the Municipal Law group of Best Best & Krieger LLPs Sacramento office.  She was previously a shareholder of McDonough Holland & Allen PC where she specialized in Redevelopment, Public Law and Land Use.


