
Address(Haussler Drive) runs from Chestnut Lane to a set of bollards where it joins Snyder Drive. A couple of culdesacs branch off to the north:
  Craig Place
  Gregory Place

Chestnut Park is accessible from bike lanes at the end of both of the culdesacs.

Haussler is the location of the former Psi Phi House.  Which is actually just where the Psi Phi people happened to live, because a sleepy East Davis suburb doesnt exactly exemplify frat house.

