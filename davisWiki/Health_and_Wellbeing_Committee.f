The Health and Wellbeing Committee was formed by ASUCD on October 31, 2002 (SB 10 (2002)), and was chaired by Maria Isabel Garcia during its first year of existence.

As far as we know, they didnt do anything between 2004 and 2005, and the committee was killed unceremoniously in the 2005 budget.

The original committee was supposed to have a chair and 8 other members, much like a commission, but this was later cut down to a total of five members.  Issues that the committee was intended to work on included:

  The Turbulence unit, now also defunct
  Campus Safety Week
  Breast Cancer Awareness Month
  Quarterly HIV Testing
  A campus blood drive (formerly its own unit)
