Hella the SI Prefix

UCD physics student Austin Sendek is pushing for Hella to be adopted in the same way kilo or mega are standardized prefixes.  Hes seeing an ISO standard of 10^27^, which would follow zetta (10^21^) and yotta (10^24^).

Written out, it would be 1,000,000,000,000,000,000,000,000,000, which is a hella zeros.

