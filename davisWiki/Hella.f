Hella can mean a few things, as a popular and very regional Northern California term for very or lots of.

Hella the Band
Hella is a popular math rock Music Scene band from Sacramento that played their first show ever at the Dam Haus in Davis in support of Lightning Bolt, Pink & Brown, and Total Shutdown.

