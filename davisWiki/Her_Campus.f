
Her Campus at UC Davis, a branch of HerCampus.com, is an online magazine for the college women of UC Davis with features on Campus News, Events, Profiles, Polls, and Campus Cuties. As a branch of HerCampus.com, the content of HCUCD is written by UCD students.  Its intended as a supplement to the national hercampus.com content.

The last update (as of August 29, 2011) was in early June 2011.

The Davis staff includes Downtown Davis employees, sorority women from both unaffiliated and Panhellenic chapters, student journalists, and UCD athletes.

