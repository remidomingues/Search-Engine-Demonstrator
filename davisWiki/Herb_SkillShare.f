The Davis Herbal Skillshare is a public group that meets to teach and learn about medicinal herbs. Past topics include tea, gardening, stress relief, and first aid. The group is open to anyone interested, no matter if they have previous experience working with medicinal herbs. We share books, plants, seeds, tea, medicine, stories and more. So far meetings have been free, with a small charge for supplies used.

Weve made tea blends, tinctures, natural bug spray, herbal vinegar, and more!

