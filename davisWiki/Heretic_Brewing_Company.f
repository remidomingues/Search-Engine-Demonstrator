
Heretic Brewing Company is located in an industrial park off of the Airbase Parkway near the Travis Air Force Base in nearby Fairfield. They make a variety of hand crafted beers including a number of barrel aged, imperials, sours, and other interesting selections. They do free tours and you can taste any of their current beer for $1.50 per taste (usually about 12 different options).  They fill growlers and half growlers of about half what they have on tap, though some of the rarer beers are only available for consumption in the tasting room.

They have gourmet food trucks and live music on weekends.

The brewery was founded by Bay Area home brewing legend turned pro Jamil Zainasheff.


