UC Davis once dead local hiphop scene is slowly returning to life.

 History 

With the rise of groups such as Anticon, and labels like Definitive Jux, to underground fame, came a flood of raw and talented hiphop groups to the Davis scene. A group called Rhyme Scheme begin the official new era of Davis hiphop back in 1999, years after the graduation of the men who put Davis on the hiphop map: DJ Shadow, Blackalicious, and the entire Quannum crew.

But what the hell happened? Even in late 2003 there was a hiphop alternative to the scene revolution nearly every week. Now there is nothing but the scattered and poorly attended performance.

2003 was the new golden age of Davis hiphop. This is the year of the highest concentration of talented, local acts. The problem is, Total Package, Plan B, The Jaded Emcees, iDeaLiSTiK, INE, Nostalgic Progression, and Eufonik all went away.

Maybe it was because of the lack of support. Certainly it is discouraging to see an only half filled show each and every performance. The problem with Davis hiphop fans is that they are fickle. They do not support the general scene but only their buddy in one of the groups. Reminiscent of Tryte fans, they leave when someone they dont know comes on stage.

At least some groups moved on. They did not die with the scene. Others stayed, but are hiding in the shadows, waiting for better times and more eager ears.

Davis is, for the most part, a cyclic place.  People come and they go, and while some make an impact, the impact can only last as long as others are willing to carry it.  There are certainly folks at KDVS who will not soon forget the great Hiphop scene that Davis had.

