 Currently Active Artists 

2010  UC Davis student Soup Da Jour is on track to singlehandedly revive the Davis hiphop scene.  A born lyricist and hook writer, his breakthrough debut hit single http://itunes.apple.com/us/album/voodoofeatnoahtheman/id358391080?i358391093 Voodoo (feat. Noah + The Man) brings a fresh direction and is rapidly gaining popularity in clubs nationwide.  His soon to be released second single is an homage to Davis  the city he calls home.


