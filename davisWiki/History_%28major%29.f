
The Department Chair is David Biale. The history departments faculty specialize in a broad range of subjects, including: Africa, Ancient Europe, China, Early Modern Europe, India, Japan, Latin America, Modern Europe, the Near East & South Asia, and the United States.

The History major offers an excellent education in writing clearly, thinking analytically and reading with curiosity.  In addition to introducing students to peoples, ideas, and cultures that may be unfamiliar to you, the study of history helps put our own world into perspective.  Because the past is as big as the world itself, it offers something for everyone.  http://history.ucdavis.edu/undergrad/ugrad.php#why History Department Website

