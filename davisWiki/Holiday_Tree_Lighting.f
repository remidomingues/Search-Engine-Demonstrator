
The Holiday Tree Lighting is an event that takes place in early December for the first lighting of the Davis Holiday Tree in downtown. The tradition of having a holiday tree in E Street Plaza dates back over 40 years.   There are generally a lot of children wandering around aimlessly with lit candles or glow sticks.  It starts as a candle lit march from the Davis Food Coop to downtown through the streets led by the California Aggie Marching BandUh.  After the walk, the tree is lit and various community groups provide musical entertainment.  The downtown businesses hold special store hours and minievents like horsedrawn carriage rides or free hot cocoa and cookies.

Some people wish that they would just call it a Christmas tree, rather than pretending that Christmas is a universal holiday.

