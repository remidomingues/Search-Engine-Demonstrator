
At some point in the 1970s a Fitness Trails parcourse was errected along the southern and western edges of the Holmes Junior High School playfield. It consisted of many (20+?) stations at which one would do several reps of an exercise before running to the next higher numbered station. The stations were not in sequential order so it allowed people to get in some distance as well as the activities. In the spring of 2005 only three of the stations are left; pullups, wall climb, and raised pushups.

There were balance beams, inclined situps, ring cross, tire steps, and other stations.


