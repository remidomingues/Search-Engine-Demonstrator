Home Ownership Home and business owners can either choose the do it yourself path or choose from an array of professionals to help them with building improvements and remodels. Please ensure that the contractor you select is https://www2.cslb.ca.gov/OnlineServices/CheckLicense/LicenseRequest.asp licensed and insured; this ensures that the work performed is to state standards and protects you in case of future problems. The Davis Enterprise also has a classified section with ads broken down by what kind of work they will accept. For housing repairs, check the DialaPro section.



