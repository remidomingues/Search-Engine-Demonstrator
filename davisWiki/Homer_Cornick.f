Homer H. Cornick (born: 17 Sept. 1896, California; died: 30 Jan. 1963, Oakland, California) was principal of the elementary school in Davis, became principal of Davis Senior High School Davis High School in 1928, and later became superintendent of schools in Davis.  He graduated from UC Berkeley in 1924.

Source:

1963 Feb. 7, Obituary: Homer H. Cormick, Davis Enterprise.
