
Along with all the flowering Town Flora plants and trees in the Arboretum and other places around the Davis city, youll find bees.   Bees are kept in apiaries for a variety of reasons, and there are also Feral Bees feral bees around Davis.  Come springtime, you might see bees swarms swarming. Call the Davis Bee Collective, the Davis Bee Charmers, the Fire Department, or the http://sacramentomidtown.com/sacbeekeeping/sacbeekeeping.shtml Sacramento Beekeeping Supply and a happy beekeeper will come out to collect them.

Dr. Norman E. Gary, a UC Davis research entomologist, is known locally as The Bee Man for his public activities consulting for the television and film industry on bee related projects including XFiles, Fried Green Tomatoes, Man of the House, Candyman, The Truth About Cats & Dogs, Beverly Hillbillies, Invasion of the Bee Girls, Savage Bees, Terror Out of the Sky, and Deadly Invasion: The Killer Bee Nightmare. The famous photographer known for his stark blackandwhite photos of artists and intellectuals, Richard Avedon, shot one of his signature photographs in Davis; the image was titled, “The Beekeeper,” a photograph of a pale, shirtless beekeeper named Ronald Fischer covered in bees. (The famous photo was shot at a Davis tomato farm in May 1981 with the assistance of UC Davis entomologist Norman Gary).

In 2008, HäagenDazs donated $250,000 to UC Davis and Pennsylvania State University to fund research into bee colony collapse disorder, a trend with no confirmed cause causing the rapid decline of the bee population.


