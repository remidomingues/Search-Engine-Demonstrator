If you are planning on raising bees be aware that apiaries in Yolo County need to be http://www.yolocounty.org/org/ag/apiary.htm inspected.

Between the University and the Cooperative Extention there is plenty of information about how to get http://entomology.ucdavis.edu/faculty/Mussen/beebriefs/Started.pdf started (pdf).

