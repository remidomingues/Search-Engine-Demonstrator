Hoopies are Davis own flavor of hippie.  The term derives from the fact that many socalled Hippies residing in Davis are productive members of UC Davis The Machine and thus the term Hippie seems both offensive and lacking.  Hoopie originates from the fact that many socalled Hoopies live in the Cooperative Housing housing cooperatives Davis is known for (Coops). Though their numbers have been in decline since the mid 1970s, restoration efforts are leading to slow but noticeable increases in the local population. Those wishing to observe one of the rare Davis Hoopies should begin their search at one of the following locations:

   The CoOp  The Davis Food CoOp.
   Cooperative Housing  Many, Many CoOp Houses in Davis.
   Delta of Venus  Coffee shop, deli, and live music.
   Whole Earth Festival

Some speculate that the term is an homage to The Hitchhikers Guide to the Galaxy, which uses http://kniggit.net/guide/195.html/ Hoopy.  As in, The Davis wiki creator is one hoopy frood!

