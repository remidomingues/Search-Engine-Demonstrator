Hotel Etna was a two story hotel in existence in 1913 in Davis.  It was formerly known as Hunt Hotel, which was one of the first hotels in the towns history.

R. S. Grady proprietor.

Meals were 35 cents in 1913.

Etna stood where the Davis Barber Shop is currently located.

There was an ice cream and confectionery parlor there that was owned by Pugh & Loftus and they sold it to Jas. B. Tufts in 1914. Pugh & Loftus then moved their tonsorial parlor to temporary quarters during which time there was to be some remodeling.
