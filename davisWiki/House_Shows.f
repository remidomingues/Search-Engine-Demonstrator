
A house show Music Scene concert is orchestrated by locals and happens in a house, coincidentally enough. It is usually illegal because a noise permit is practically impossible to get in Davis but it is always fun. So when going to a house show it is always best to bring a few bucks to give to the bands who may be on tour and a few more bucks in case the hosts of the show get a noise violation. But house shows are always cheaper than seeing a band at a Bars bar.  House shows are the best place to find new music, as most bands start off their careers by playing house shows.

Besides the events board Davis Wiki Events Board  there is also http://undietacos.org to find out about Davis house shows.

Davis has a long history of putting house shows. The Sacramento band Cake used to play in Davis all the time when they were starting. DJ Shadow used to spin records at House dance shows. At the Dam Haus Babes in Toyland, Outhud, Sexy Prison, and Casiotone for the Painfully Alone, as well as many other bands have played in the living room. At the Pirate Ship such touring acts as the Replacements, Violent Femmes, Meat Puppets, Camper Van Beethoven, and wiki:WikiPedia:Jason_Webley Jason Webley have played there over it 25 year history. Recently local bands like GnarToon, ChoadBot, and the Playboy Millionaires have played there.

The legend of Davis houseshows has made it all the way around the world, and consequently, many people in the DIY music community have begun to think of Davis as one of the few exciting small town music scenes (such as Olympia, WA, or Missoula, MT). Live music performances in Davis living rooms have been chronicled on DVDs sold worldwide by Lightning Bolt and XBXRX. This legend of a happening Davis houseshow scene has reached such critical mass that no less than one band per day emails me about playing here. Little do they know that we have no real venues for midweek music other than the campus, which is rather limitedaccess.  Heres hoping the city of Davis gets smart and provides an allages showspace like they did throughout the mid90s with the Third and B 3rd & B teen center.

So that means it is a good idea to check out a house show because they are always entertaining.  And if you want to be able to say I knew them when when a band gets famous then check em out at a house show.

Oh, and always try to BYOB.

