Howard Cohen (born ca. 1952, died 26 June 2013, Walnut Creek, California) was principal of Emerson Junior High School Emerson Jr. High School prior to becoming principal of Davis High School from 1993 to 1998.  He left Davis High School to follow administrative opportunities in other school districts.

