About the project
The human powered utility vehicle project was initiated by Mike Griffith and Jason Moore in early 2008. Mike is a grounds crewmen and had the desire to use a human powered vehicle instead of the traditional Electric Vehicles electric and gas powered vehicles used on campus. The two set out together and obtained a grant from the Campus Sustainability Initiative. Mike then spent several months researching vehicles and trying them until he settled on a Pedal Pickup from http://www.pedicab.com/pedicabs.html Main Street Pedicabs. You can see Mike out and about on campus with the vehicle and sometimes hes even pulling a giant toter full of yard debris. Stop and and say hi, hell probably let you give the trike a try.

