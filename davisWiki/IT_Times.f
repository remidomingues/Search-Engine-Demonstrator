The IT Times is a quarterly Media newsletter which provides practical and timely information, primarily for UC Davis faculty and staff, about technology issues facing the campus, current and upcoming Information and Educational Technology services, and technologyrelated campus projects and initiatives.  Its produced by UC Davis IET department.  Current and past issues are available online at http://ittimes.ucdavis.edu/

Some sample topics:
 A sociallyconscious professors professor with an unusual approach to her job.
 Technocultural Studies
 Downloading Music Legally
 etc.

