
Ida May Harter was a UC Davis graduate in music and French and school teacher in both Davis and Sacramento.  She was one of the first public performers at Music Department UCD Music Department when it started in the 1950s.  She taught piano and music theory in Davis from that time onward.  In the early 1960s, she quit working as a school teacher and exclusivly taught private music lessons in Davis to children and adults until shortly before her death in 1998.  She studied for many years with Claire James of the http://en.wikipedia.org/wiki/San_Francisco_Conservatory_of_Music San Francisco Conservatory of Music.

Harter was the first recipient of the Harmony in Our Lives Award in 1987.

