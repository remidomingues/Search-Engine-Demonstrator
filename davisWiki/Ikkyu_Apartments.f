
Ikkyu Apartments is located in East Davis across from Davis Manor shopping area. The apartment complex looks like an old motel, with a parking area (one car for each unit), and a kiddie pool shaped front lawn. There are about 12 two bedroom units. Mine is inhabited by cockroaches, so Im guessing others are too. The manager (not the owner) works at Party and Vac (aka The Party Store).

