

Impulse Recording Studio is a professional recording studio, started in 1993 by Chris Sanborn.  Impulse offers multitrack recording with ProTools, a soundproof room, high quality equipment, and years of musical and technical experience in a friendly environment.  Please see our website and Facebook page for more recent pictures and updates.

In addition to recording in the studio, Impulse Recording offers:

  CD Mastering
  Duplication
  Music arranging and production
  Audio Restoration (including reeltoreel tapes, cassettes, and LPs)

For a list of equipment, check the http://www.impulserecording.com/ website.
