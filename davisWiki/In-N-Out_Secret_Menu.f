Animal style is the most popular secret style. In addition to the standard toppings, animal style burgers include:
   pickles
   extra spread
   grilled onions instead of fresh onion
   mustard fried onto each meat patty.

Protein style, popular among wiki:WikiPedia:Atkins_Nutritional_Approach Atkins dieters, no/low carb eaters and people who just like lots of lettuce:
   replaces the hamburger bun with large leaves of lettuce.
   It can be combined with other special orders, e.g., animal protein style.
   Lettuce is generally colder than buns, so beware of quicklycongealing beef grease in the sandwich.

3×3, 4×4, or generally M × C refers to a burger with a varied number of meat patties (first number, M) and slices of cheese (second number, C).  For example, the popular DoubleDouble would be 2×2 (pronounced Twobytwo when you order it), while a burger with 3 meat patties and 1 slice of cheese would be a 3×1 (a threebyone).

  As of August 2006, the largest burger that can be ordered is a 4x4. Only four slices of cheese maximum may be permitted on a single burger. That may be a Davisspecific rule, as somebody checked and officially the largest you can order is a 4x6.  If you are wondering about ordering a 20×20, look at http://20x20.ofdoom.com/index.htm this link to deter you. It is a photo essay of the terrible 20 patty journey. Or, to even better satisfy any curiosity you may have, the http://whatupwilly.blogspot.com/2006/01/innout100x100.html 100x100.


DoubleMeat (a.k.a. 2×0) is a DoubleDouble with no cheese.
   By definition a DoubleDouble automatically includes two slices of cheese; for two patties without the cheese, a double meat burger must be ordered.

The Flying Dutchman:
   Two meat patties
   two slices of cheese
   no bun
   Note: other condiments (including lettuce, tomato, spread, and onions) are not included unless you request them.
   This is a great way to circumvent the burger size rules. i.e.: order a 4x4 and two Flying Dutchmans and you have essentially an 8x8

Grilled Cheese:
   a sandwich with two slices of cheese
   no meat
   the grilled cheese comes stock, with spread, tomatoes, lettuce, and onions if you would like.
     Like most orders, this can be combined with other styles such as animal style.
     FYI: The grilled cheese used to be cooked on the side of the grill that was reserved for toasting buns (so that it wouldnt get contaminated from being cooked on the same surface used to cook meat). They now have two separate grills, one for meat, and the other for bun toasting. However, the bun grill is coated with a special nonstick surface that doesnt allow them to cook grilled cheeses on it any longer, and they have to cook them on the same surface as the meat.

Veggie Burger or Wish Burger:
    A sandwich containing only vegetables, and no meat or cheese.
    Wish burger is a reference to wiki:wikipedia:Rubber Biscuit, which was a massive cult hit due to wiki:wikipedia:Dan Aykroyd Dan Aykroyds performance of it as wiki:wikipedia:Elwood Blues.   The song is a lament about not having enough money for food, and going hungry.  The majority of the lyrics are nonsensical vocal sounds, with one of the four actual lines being: Have you ever heard of a wish sandwich? A wish sandwich is the kind of a sandwich where you have two slices of bread and you   hum hum hum hum  wish you had some meat!

Burger Options

Mustard fried  patty spread with mustard, then cooked.  This also adds pickles, unless you tell them to hold the pickles. You can also ask for heavy mustard fried.

Onions can be ordered as whole fresh, fresh chopped, whole grilled, grilled chopped, or any combination of the four possibilities. So to maximize your onion variety you could get a burger with all four types. Whole grilled is certainly worth trying.

Chopped chilis adds some yellow chilies to the bottom of your burger.  Medium heat, little bit of vinegar flavor. (You can also ask for these on the side!)

Mustard and/or ketchup can be substituted for the spread. Only designates no spread. Cashiers may ask if you want ketchup or mustard instead.

Nosalt has no salt added to the the patties.  Once you add the condiments and cheese, you dont need the extra salt anyway and you can leave without feeling like a driedoutbutsatsfied lump.

Extra Salt is added to the tomato layer of the burger for those who feel the need for extra sodium on their burger.

Extra toasted bun/lightly toasted bun/untoasted bun. The bun come toasted by default, but you can ask to have the level of toastyness varied. The buns are toasted to add some rigidity to the burger, as an untoasted bun is flimsy under the weight of all the meat/cheese/condiments. This also prevents sauces and toppings from excessively soaking into the bun, thereby avoiding the lastbitesaresoggy problem. This needs to be the first modification requested, based on keystroke order into the register.

Extra everything  Extra tomatoes, extra lettuce, extra onion, extra spread, extra pickles, extra onions, extra chilis.  You can ask for all of these at no extra charge.  Theyll really fill out your burger and make it a more balanced meal, especially if youre a veggie fan. Onions can come in four ways: whole raw, whole grilled, chopped grilled (like animal style) and chopped raw. You can also ask for both raw and grilled onions in the same burger.

Medium rare or well done. You can specify how well done your hamburger patties are. FYI: Medium rare will definitely show a pink middle.

 Custom Fries 
  Fries welldone are extra crispy fries.
  Fries light well are a little more done than regular fries, but not as crispy or greasy as the welldone fries.
  Fries lite are fries that have been cooked less than normal.
  Fries nosalt have no salt added to them.
  Cheese Fries are fries with two slices of melted cheese.
  Fries animal style include two slices of melted cheese, grilled onions, and spread as toppings. x Youll need a fork to eat them.x  Dont be weaksauce, chow down
     Additional alterations that can be made to Animal Fries include substituting spread with ketchup and/or mustard, substituting grilled onion with raw chopped onion, and finally, adding or substituting chopped chilies.
  Lemon fries you can make your own lemon friesjust grab a lemon slice from next to the ice tea machine, and squeeze it on your fries.  It really livens things up.

 Custom Drinks 
 Milk Shakes 
  A root beer float is a concoction made of half vanilla shake and half root beer soda. Be sure to specify whether you want it more creamy or sodaey.
  A Neapolitan shake is a mixture of chocolate, vanilla, and strawberry flavored shakes. If an order contains a shake of each flavor, this is called Around the world.
  A black and white is a vanilla and chocolate shake (skip asking for a gray; theyll just get confused).
  Strawberry, chocolate, and vanilla can be combined in any way, i.e., just chocolate and vanilla, chocolatestrawberry, etc.
  Large and extra large shakes are also available. The cup sizes for these shakes are one below soft drinks (i.e. a large shake is a medium soft drink cup, while an extra large shake is a large soft drink).
 Other 
  Milk. 2% lowfat is your only option.  Yaaknow, its for da kids.
  Fountain Drinks  you can combine Iced Tea and Lemonade for an Arnold Palmer.
 Side Items 
These items are free:
  Spread  good with fries
  Side of prepackaged yellow chilies.
  Water comes in a clear plastic cup to distinguish it from soda/shake cups.
  Creative Lemonade.  Take your water cup, squeeze several free lemon wedges in, add a few free packets of sugar, stir well to blend.  Fresh and tasty!

