

At InNOut you can order custommade food. Some customizations are available at no additional cost; those that use inventoried items such as cheese, meat, and buns cost extra.  While its not on the printed menu, there are buttons on the cash register for these items. You can pretty much order anything you want, as long as they have the ingredients (e.g., putting onions in a milkshake).  While the secret menu is listed on the InNOut corporate site, it is far from complete.

Also, keep in mind that since you can customize anything, you can feel free to order whatever you want and create your own doublesecret personal menu.  Get a 0x3 with four slice of tomato on a double toasted bun and call it a cheesy wiki.  The following are just the custom orders that are popular enough to go by well known names.



