>Classical Music Performances in Davis<

In C Major is an online calendar that lists Music Scene classical performances in California, Washington and Oregon and more. You can find http://incmajor.org/?id45303 performances in Davis, Sacramento and in other areas.

In Addition to the UC Davis UC_Davis Mondavi Center Mondavi_Center, other campus venues hosting classical music include the Music Building, Freeborn Hall, Wyatt Pavilion, and UC Davis Main Theater Wright Hall and the Main Theater.

