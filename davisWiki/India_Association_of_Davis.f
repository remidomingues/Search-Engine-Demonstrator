
According to the groups website, the India Association of Davis is a nonprofit social and cultural organizations organization established in Davis in 1987 by people of of Asian Indian origins, with membership open to all.

They organise social and cultural events not just for the Asian Indian community, but for the whole community of Davis. One such event is a day celebrating Diwali, the Festival of Lights. For 2009, they have planned a full day of activities at the Veterans Memorial Center Veterans Memorial Centre.


