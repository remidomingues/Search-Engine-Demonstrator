
Instant Runoff Voting is a Voting voting system used for singlewinner elections.  Voters rank the candidates in order of preference.  If there is no majority winner (counting the #1 choice) then the candidate with the least votes gets eliminated.  Then the votes are considered again.  If there is still no majority winner then the candidate receiving the least votes gets eliminated.  This process continues until there is a majority winner.

Instant Runoff Voting (IRV) is the singlewinner variant of Single Transferable Vote and is used for ASUCD President/ASUCD Vice President Vice President elections.  Both IRV and Single Transferable Vote are refered to as Choice Voting here in Davis.

