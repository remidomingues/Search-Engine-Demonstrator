
The Institute of Transportation Studies is one of the Research & Development research groups on campus. It is a UC Davis program that is comprised of roughly 60 affiliated faculty and researchers and 80 graduate students. They seem to be connected with the Move U:  Local Transportation Initiatives in Davis.  With an annual operating budget of $6 million, they work with other groups around the world on sustainability sustainable transportation and climate change with their various projects:

  Sustainable Transportation Center
  STEPS
  Program
  PHEV Center
  Energy Efficiency Center
  China Center
  GATE Center
  Hydrogen Pathways
  FCHV Demonstration Program

