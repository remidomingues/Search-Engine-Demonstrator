An organization to promote interntational solidarity amongst those underprivileged individuals unable to attend the official event in Davis, CA.

This group seeks to develop satellite events across the world, bringing the same cheer and drunkitude to these disparate locations, that exists at the official Picnic Day.

These events should also be attended by locals, so as to inculcate to the greatness of Picnic Day.

