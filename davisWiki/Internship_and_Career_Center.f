
The Internship and Career Center (ICC) is an organization with internships internship and job resources career resources for UC Davis graduate and undergraduate students, alumni, and even prospective students. They have lists of job openings and they can help you arrange interviews. In addition, they publish the http://icc3.ucdavis.edu/iccdocs/UCD_Placement_Manual_0405.pdf Career Resource Manual, a free guide with friendly tips about how to write cover letters, resumes, how to conduct yourself in an interview, and more. The staff at the center can proofread your resume before you submit it to employers. They also hold a variety of workshops to help you land the job that you applied for including topics such as interviewing skills.

The Community Service Resource Center is located here as well.

Among their programs they handle Camp Adventure information for UC Davis.



