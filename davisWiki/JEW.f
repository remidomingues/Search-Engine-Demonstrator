You may be looking for Judaism.  This is about the provocatively named UC Davis related group.

JEW (Justice, Equality, Waffles) is an ASUCD slate, currently holding 1 seat of the 12 member ASUCD Senate.  It is the brainchild of former controller and current senator Eli Yani.  The slate lacks a conventional platform, instead focusing on serving the student body through responsible service at the senate table and quality oversight in budget hearings.


There was also the unrelated ASUCD Jewish Slate in Fall of 2006.
