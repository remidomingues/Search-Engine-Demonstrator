Address(J Street) runs north south from 2nd Street to East Covell Boulevard at the Hunt Wesson Plant. South of 8th Street I Street branches off and is between it and the tracks, north of 8th Street it is the first street east of the Train Tracks tracks.

At its southern end is WA Westgate Company.

J Street was once known as Plum Street.


