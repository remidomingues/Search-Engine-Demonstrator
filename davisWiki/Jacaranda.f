The Blue Jacaranda, Jacaranda mimosifolia more often known simply as the Jacaranda, is a subtropical tree native to South America that has been widely planted elsewhere because of its beautiful and longlasting blue flowers. They normally dont grow well in Davis owing to its harsher climate (relative to Southern California), but theres a remarkable individual growing in the parking lot next to Meyer Hall. Why its flourishing is a mystery.

