
The Susie Boyd Boyd family donated a gas lamp that had previously graced the streets of London to the University in honor of the Memorials memory of James B Boyd. The James B Boyd London Gas Lamp was converted to use electricity and sits at the Memorial Union bus terminal.




The plaque reads:

This was once a gas burning
lamp which illuminated a street in
London.  It was a gift from the
Boyd Family to the University
in Memory of

JAMES B. BOYD

Professor
Genetics / Molecular
& Cellular Biology
19681999

