Janet Damesyn (born 24 July 1936, Philadelphia, PA; died 4 Dec 2010, Yolo Co., CA) was a long time resident and involved community member.  She moved to Davis with her family in 1972, graduated from School of Law UC Davis Law School, specialized in special education law, and was elected to the Board of Education Davis school board from 1977 to 1981.

