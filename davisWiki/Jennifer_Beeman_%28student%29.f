Jennifer Jenni Beeman was the 2005 chairperson of the ASUCD Academic Affairs Commission.  She unsuccessfully ran for ASUCD Vice President in the Winter 2005 ASUCD Election. She ran on the Student Focus Slate slate with Sapana Shende.

Jenni graduated from UC Davis in 2006 and currently works for http://www.heart.org/ The American Heart Association, in Portland, Oregon.




