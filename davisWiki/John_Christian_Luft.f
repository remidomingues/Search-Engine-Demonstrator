John Christian Luft (September 11^th^ 1863  July 20^th^ 1942 ) was a blacksmith in Davis. Married Miss Etta Frittz. They had four children John R., Oliver F., Maud L. and Lawrence. He was a member of the school board and its clerk for a while.

Came to Davis and married in 1893.

Did Horses horse shoeing and built carriages and plows.

Was a member of the Odd Fellows.
