John Craig Drummond (1928  November 12th, 1895) Worked as a blacksmith for twelve years in Sacramento prior to moving to 700 acres of land 7 miles east of Davis. Married to Sarah Frances Reid.

Three daughters, Mrs. Annie Ramey, Mrs. Bettie Tufts who both lived in Davis and Mrs. Lillian Hafner.

Came with the 1849 49ers in search of gold by way of the Horn.
