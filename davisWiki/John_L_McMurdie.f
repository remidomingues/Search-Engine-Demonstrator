
John L McMurdie served on the City Council from 4/20/1962 to 4/20/1966.

McMurdie ran for reelection in 66, but he failed to sense that the political winds in Davis were changing.  One of his more memorable campaign statements was:
The horse and buggy have had their day, and the bicycle is on its way OUT!  (As per an interview with Donna Lott, 2005)
