Name: John Meyer

Title: former Vice Chancellor of Administrative and Resource Management

John Meyer retired from UC Davis June 30, 2014 after 14 years of service to the University. Meyer became vice chancellor for Administrative and Resource Management in Fall 2009. He served as Vice Chancellor for Resource Management and Planning from July 2000 through September 2009.

Prior to joining the UC Davis administration, Meyer was City Manager for the City of Davis from 1990 to 2000 and Deputy City Manager from 1987 to 1989.

Meyer earned a bachelor’s degree in sociology from UC Davis in 1980. He holds a master’s degree in public administration from the University of Southern California, completed in 1985
