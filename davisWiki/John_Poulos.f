
John Poulos was a trustee of the Davis JUSD school board from 1999 to 2002.  He did not finish his term because he moved from Davis to Granite Bay in 2002.  The school board subsequently chose Keltie Jones to finish the remainder of his term.

Poulos is a graduate of Davis High School, son of former Davis mayor, Joan G Poulos, and stepson of former Davis City Council member and longtime Davis teacher, Deborah NicholsPoulos.

