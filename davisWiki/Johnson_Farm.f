


Johnson Farm is a small 5 acre family farms farm located about 2 miles south of Gridley (on Hwy 99 N). They have a large variety of summer produce available at upick and already picked prices: cherries, blueberries, blackberries, tomatoes, cucumbers, peppers, grapes, eggplant, melons, pumpkins and much more. Mark Johnson has his Masters in organic weed control from UC Davis and focuses on sustainability sustainable farming methods. You can call or email them with any questions, or check out their website for produce, pricing and directions.

