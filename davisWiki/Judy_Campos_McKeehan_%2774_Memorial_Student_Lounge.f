The Judy Campos McKeehan 74 Memorial Student Lounge is located in the basement of King Hall. Despite being a lounge in King Hall, it is not King Lounge.

The lounge has two refrigerators and several microwaves so that students can save money by bringing their lunch to school.  At noon, there is usually a short line for use of the microwaves, but the line rarely lasts long.

There is table seating for students who want to use their laptops and several chairs and small sofas for those who want to get more comfortable as they work.  There is also a television for social events.

