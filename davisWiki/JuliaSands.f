Julia Sands (Julie to most) is a UC Davis alum and the author and artist of Semper Terribilis, a comic strip that appeared in the California Aggie from 2006 to 2008.  She also worked as a peer advisor in the Animal Science Advising Center from 2006 to 2008 and at the Dairy Cattle Facility from 2007 to 2008, and volunteered in several animalrelated internships throughout her four years at Davis.  She graduated in Spring 2008 with a B.S. in Animal Science and a minor in English.

Until moving away in August 2008 she lived in South Davis, where she made a lot of casseroles.




