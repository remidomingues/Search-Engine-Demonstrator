The Julies Garden project, funded by private donations, renovates an existing picnic area at the entrance of the large observation deck overlooking Julie Partansky Pond with a new concrete pad with decorative mosaic tiles under the table, wooden bench seating in place of the fence and a bedding area maintained by volunteers.

Donations may be made at First Northern Bank

