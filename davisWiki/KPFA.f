
KPFA was founded in 1949 and is located in Berkeley, but simulcasts on two other stations, one in Berkeley and one is Fresno. Combined, their broadcast reaches 1/3 of the state, according to their website. Their programming consists of eclectic music, public affairs and news coverage. It is a Pacifica station with a mission:

     To promote cultural diversity and pluralistic community expression
     To contribute to a lasting understanding between individuals of all nations, races, creeds and colors
     To promote freedom of the press and serve as a forum for various viewpoints
     To maintain an independent funding base

KDVS likes to consider itself closer in kin to KPFA than to UC Berkeleys campus station, http://kalx.berkeley.edu KALX.
