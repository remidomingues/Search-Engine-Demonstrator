
PBSaffiliated KVIE is the Sacramento area public Media television station. KVIE carries popular PBS shows (such as Antiques Roadshow, NOVA, and the News Hour) as well as producing local programming and documentaries in its ViewFinder and California Heartland series. KVIE also produces the nationally distributed and aired Americas Heartland. UC Davis runs short http://broadcast.ucdavis.edu NewsWatch segments between shows on Monday, Wednesday, Thursday, and weekends.

