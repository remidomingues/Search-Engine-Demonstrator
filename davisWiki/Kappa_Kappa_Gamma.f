Kappa Kappa Gamma is a UC Davis Sororities Sorority. It is sometimes referred to as Kappa or KKG and was founded at Monmouth College in Illinois in 1870.  The Epsilon Omicron Chapter of Kappa Kappa Gamma was established in Davis in 1975, and has remained an integral and active part of the UCD Greek Life Greek Community.  Kappa plans many sisterhood activities to unify our house, organize social events with other sororities and fraternities to promote Greek Life Greek relations, participate in philanthropy events in the community, and involve ourselves in a variety of campus events.


Mission Statement: Kappa Kappa Gamma is an organization of women which seeks for every member throughout her life bonds of friendship, mutual support, opportunities for self growth, respect for intellectual development, and an understanding of and allegiance to positive ethical principles.

 

Our Philanthropy:  Kappa currently holds three main philanthropy events during the year, Kappa Dash and the Kappaccino and Kappa Karnival. These events benefit philanthropies such as the Taylor Family Foundation, Reading is Fundamental, and the Kappa Kappa Gamma Foundation. Kappas also participate in numerous philanthropies on campus sponsored by other organizations including Relay for Life,Tau Kappa Epsilon TKE Dream Girl, Arrow Jam, and Derby Days.

