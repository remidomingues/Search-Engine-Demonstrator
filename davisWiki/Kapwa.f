
Kapwa is the Tagalog word for shared identity or brotherhood. Kapwa is for anyone interested in discovering more about faith and the person of Jesus both in a Pilipino/Pilipino American and university context. We are committed to growing Pilipino students in their ethnic identity, biblical justice, and evangelism through our ministry.

They meet every Thursday at 7:00pm in room 80 of the Social Sciences and Humanities Building.

