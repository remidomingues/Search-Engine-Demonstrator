
Karen Spiess served as a Trustee for the Davis JUSD School Board from 1987 to 1995.  She was initially appointed by the board in January 1987 to fill out the remainder of Helen Thomson Helen Thomsons term when she was elected to the Yolo County Board of Supervisors.  Spiess ran unopposed for her seat the following November and was reelected in 1991.


