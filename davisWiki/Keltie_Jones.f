Keltie Jones served as trustee for the school board Davis JUSD school board from 2002 to 2007.  She was originally appointed to fill out the remainder of the term for trustee John Poulos who moved out of the area in the middle of his term.  Jones was elected in her own right in 2003 and served until 2007.  She was born in Canada, moved to Davis in 1988 and moved back to Canada with her family in 2009.

