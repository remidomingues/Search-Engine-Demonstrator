Address(Kent Drive) is a quiet residential street in Central Davis.   It is part of the college streets in the towns street name themes, named for Kent State University (it was named prior to the Kent State massacre). It runs between Apple Lane and Sycamore Lane. Proceeding north, Kent is the second to last street to intersect Sycamore before West Covell Boulevard Covell.

First Lady Rosalynn Carter once had dinner at then mayor Tom Tomasi’s house on Kent Drive in the late 70’s.
Kent Drive is the only street in Davis to have a private tennis court.

Current locally famous residents of Kent include:
  Ken Wagstaff
  Don Saylor

Former:
  Thomas J Tomasi
