
On the 4th of July Davis holds a Kids Parade.  This 2block parade from 2nd street to Central Park along C Street is an event for kids of all ages and is divided into sections based on vehicles.  The front is led by the antique fire engine, then come the walkers, then the strollers, then wagons, and finally bicycles.  The parade begins at 10:00AM (I think!) and people start lining up at 9:00AM and talking.  Lining up typically takes significantly longer than the walk itself.  All kids receive a blue ribbon for participating and drinks are served in the park during a costume judging contest.

