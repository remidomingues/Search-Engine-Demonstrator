His alternate history novel The Years of Rice and Salt is partially set in Davis, as are portions of The Martians, a companion book to the Mars trilogy. Though not set in Davis, issues discussed in many of his books, such as Cooperative Housing and businesses, Choice Voting, false/post scarcity economics, Climate Change, and trying to clear nutsedge out of gardens, will feel familiar to Davisites. Robinsons latest book, Fifty Degrees Below also featured Disc Golf in a Washington DC park, which may have also come from his residency in town.

Kim Stanley Robinson occasionally has book signings at Davis Bookstores, and signed copies of his books can often be found at The Avid Reader and Bogeys Books. He is an occasional speaker on campus, and has been seen at the Davis Food Coop and Sudwerk.

