Name: Kim Stanley Robinson
Well known science fiction author Kim Stanley Robinson lives in Davis with his wife and children. His most popular work is the awardwinning Mars trilogy. A detailed biography is available on wiki:WikiPedia:Kim Stanley Robinson Wikipedia.

