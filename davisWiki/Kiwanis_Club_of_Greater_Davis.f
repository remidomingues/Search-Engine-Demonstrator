

About The Kiwanis Club of Greater Davis

The Kiwanis Club of Greater Davis is a Volunteer Opportunities community service organization founded in 1958 and focuses on projects with seniors and children. Originally, it was one of two local clubs for wiki:WikiPedia:Kiwanis Kiwanis International, and sponsored Circle K International at the University of California, Davis.  It was also affiliated with Key Club at Davis Senior High School.
