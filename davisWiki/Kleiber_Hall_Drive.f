
Kleiber Hall Drive is a campus road that runs at an angle going north from Hutchison Drive and then turning west and connecting with California Avenue.

Along its journey, Kleiber Drive passes several large instruction halls including: Hutchison Hall Hutchison, Storer Hall Storer, Briggs Hall Briggs and its namesake; Kleiber Hall.
By its southern end are located the UCD UC Davis Fire Department Fire and UC Davis Police Department Police Departments. There is also an entrance to the West Entry Parking Structure new parking facility opposite of Dairy Road.
