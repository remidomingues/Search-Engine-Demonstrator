The Knights of Pythias is a fraternities fraternal organization that was represented locally in the local lodge which was Knights of Pythias, Golden Seal, No. 110. They take their name from the legend of Damon and Pythias, two close friends who were students of Pythagorus. When Pythias was sentenced to death, he requested that he be able to return home one last time. His friend, Damon, agreed to take Pythias place in jail while Pythias visited his family. If Pythias didnt come back in time, Damon would be executed in his stead. Pythias came home, the king admired their friendship, and pardoned Pythias. He even put them both in his court!

During 1909 they were meeting every first and third Tuesday in the odd fellows Odd Fellows Hall.

