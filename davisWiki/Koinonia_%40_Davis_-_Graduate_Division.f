
Koinonia @ Davis  Graduate Division is a Christian Spiritual Organizations fellowship of UC Davis graduate students, postdocs, spouses, and alumni who gather together weekly to have good fellowship together through various activities including studying the profound truths found in the Bible.

We have students and scholars from all walks of life who, in addition to pursuing their academic goals, see their time in grad school as a chance to learn about Christianity and about God as well. Through our studies, many have found that the Bible is deeply relevant to life from all backgrounds and cultures.

During the academic year we meet on the UC Davis campus for weekly noontime Bible studies. Please check the website for the latest information during the summer.

