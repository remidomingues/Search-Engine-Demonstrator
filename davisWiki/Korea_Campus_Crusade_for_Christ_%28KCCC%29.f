


UC Davis KCCC is here to build up each other as a family of Christ. Korea Campus Crusade for Christ is dedicated to creating movements on campuses through believers who are committed to connecting people to Christ. We know that apart from Christ we can do nothing, therefore we depend on the Holy Spirit to work in and through us to accomplish the work God has placed before all believers: the Great Commission. We are here to WIN people for Christ, to BUILD each other up, and to SEND them out into the world as a more fullydeveloped Christcentered follower.

