Kyle Wecker was a citizen of Davis for 4 to 5 years.  Upon finishing high school and one semester of college, he settled down to work at Wecker Surveys, Inc. Wecker Surveys.  After about three years of work, he returned to UC Berkeley where he finished his degree in Computer Science. Kyle subsequently attended Cornell University in Ithaca, NY to pursue a PhD in Computer Science.

Kyle passed away unexpectedly at his home in Ithaca in late May, 2013. His memorial service was held at the International House Davis International House in Davis, California, on June 15, 2013. He is missed very much by his friends and family in California, Texas, and New York.

