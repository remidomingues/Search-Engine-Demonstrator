




Address(This little garden,38.544788,121.733437) was maintained by George Swazo a homeless man who now has some flowers near Target.  It sat right along the bend in L Street that ends L and becomes 2nd Street.

Most probably missed it as they zipped by in their cars. It varied in size and beauty depending upon the season and the amount of time its maintainer had.

The Garden came and went with the seasons. As of November 2004 it was being replanted, but then again it perpetually changed! With the major US holidays festive decorations adorned the garden, and as new items became available they appeared in The Garden, often spray painted colors like peptopink. Town Flora Plants came and went, whether perennials, cacti, or annuals. The Garden seemed to be a temporary art installation, somewhat like a sand painting or balanced rocks.

There was a California Aggie http://www.californiaaggie.com/media/storage/paper981/news/2006/06/26/ArtsEntertainment/FameFriendship.And.Reconnection.Bloom.From.On.Mans.Voluntary.Labor2119514.shtml article and interview with Swazo about the garden in June of 2006.


From a randomly found Craigslist posting:

As we have all noticed, along 2nd street (just out of downtown, the near 90 degree turn) there are many plants immaculately taken care of. If you are a frequent driver you would notice that it is actually a homeless man who does our city this lovely deed. I stopped by on my bike today to talk to him.

He was very kind, a little blunt. He told me of his 12 hour workdays and cold nights. He has been doing this work for three years now. While PG&E does supply free water and the city does allow him to use their topsoil from time to time, he does not receive money for food or equipment and plants. Yes, it would be ludicrous to demand a minimumwage for this man, but I do not see why we couldnt gather up some spare change and help the man out. He did mention he receives social security but that isnt much.

He mentioned to me that he is in dire need of the following:

 1. Numerous hoses  his are leaking
 1. A shovel (as wide as they make them)
 1. A wheel barrow
 1. And of course, cash donations. Regardless of what the money is spent on, he unselfishly does us a great service and I feel that if you have the money to spare, help him out.


