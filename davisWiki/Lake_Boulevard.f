
Lake runs roughly north to south, linking Russell Boulevard and West Covell Boulevard.  North of Covell, it turns into County Road 99, which is a completely straight and beautiful drive through farmlands all the way to the heart of Woodland.

The area around Lake Blvd. is nearly completely residential, with apartment complexes lining both sides and the Stonegate Neighborhood Association Stonegate housing development bordering Stonegate Lake.

Driving down Lake, you might come across the Stonegate Country Club and the Westlake Plaza shopping center.

Lake is one of the streets in Davis that has both bike paths and bike lanes, so you can choose your level of safety.

