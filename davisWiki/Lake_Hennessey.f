Lake Hennessey is about 45 minutes from Davis located in Napa County east of Lake Berryessa on Highway 128. From Davis, take Covell Boulevard or Russell Boulevard towards Winters. Go through Winters and towards Lake Berryessa. After you come around the south end of Berryessa the road will split. Go right. This will take you to Lake Hennessey and if you keep going, the Napa Valley and the city of St. Helena.

The lake is a nice spot for fishing and boating.

