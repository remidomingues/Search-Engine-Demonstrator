

Lambs Ear (Stachy byzantina) is easily recognized by its extremely soft, furry leaves.  This softness is reason for the name Lambs ear, as the leaves feel as if they were plucked off a baby lamb.  Lambs ear is fastgrowing, requires partial sun exposure, and can grow in normal or sandy/clay soils (needs dry moisture).  They can grow to 8 inches when mature, and spread from 1224 inches.

Unfortunatley, uprooting the plant does not yeild a baby lamb.

