Landscape designers design, plan, create, and manage beautiful, natural habitats for peoples enjoyment. UC Davis has a landscape architecture department, and several people live and work in Davis who do landscape design. Landscape architecture is a professional certification. No professional certification or educational requirements exist for an individual to call himself or herself a landscape designer.  The following are some of the landscape designers in Davis:

