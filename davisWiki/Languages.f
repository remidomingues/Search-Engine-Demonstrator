In Davis, youll hear people babbling in all sorts of languages:

  Chinese
  English
  French
  German
  Italian
  Japanese
  Russian
  Spanish Spanish & Portuguese
  Tagalog

Some of the multiculturalism is due to the international draw of the university.  Otherwise, you can blame the speaking of other languages on people who are in mixed company and want a conversation between another person to remain private (despite the possibility that said mixed company may also be keen on that language).

