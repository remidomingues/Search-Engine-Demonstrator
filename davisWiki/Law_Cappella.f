
Law Cappella is an Music Scene a cappella singing group directed by and Student Organizations composed of students at King Hall, UC Davis School of Law. Each fall the group recruits new and returning students for the school year. It is a mixed SATB performance group that has several annual performances.

Yearly performances include:

  Winter Holiday concert at the law school
  Singing Valentinegrams, performed live at school or over the phone
  Admitted Student Day
  Cardozorama, the law school talent show
  Graduation

