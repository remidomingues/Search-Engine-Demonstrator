

The Mabie Law Library is located in King Hall and is open to the public for use, any member of the general public may check out books which are not Reserve items or part of a large set to a limit of 5. It is a great place to do independent legal research, such as preparing to fight a traffic ticket.

The library contains a printer/copier/scanner with a function that lets users scan a document and email it as a PDF.  This function is free for anyone to use (unlike printing, which is something like 15 cents per page).  The machine is located through a door near the circulation desk on the main floor.  When you enter the library lobby, the door is straight ahead.

