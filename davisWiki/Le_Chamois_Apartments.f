





Le Chamois Apartments offered threebedroom, twobath Tahoestyle townhomes with wood beams, vaulted ceilings, lots of windows, private patios and fireplaces.  The complex is conveniently located near the Marketplace Shopping Center, close to UCD, Yolobus/Unitrans bus stops, and freeway access.  Floorplans are approximately 1100 square feet, and include a loft overlooking the living room.  Rent is $1,650/month (fall 2011) and includes wireless internet, water, sewer, and garbage.  Le Chamois is located on the Unitrans B bus line.  There are only 10 units in this complex, which makes for a small community.

