
Leslies Pool Supplies is a store that sells swimming pool supplies and accessories. It makes up a part of the largest chain of pool stores in the country.

The store had a soft opening on March 22, 2012. It celebrated its arriving businesses grand opening from April 1316 of 2012 with discounts throughout the store and some pools pool related toys out front.

The store is located in the former location of the North Davis Blockbuster in The Marketplace shopping center. This location was previously occupied by Halloween City in Fall of 2011.

