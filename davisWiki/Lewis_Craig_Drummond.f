Lewis Craig Drummond (February 2, 1828  April 23, 1882). Came to Mariposa County, California in 1850 via Panama and then to Yolo County in 1852 where he purchased three farms with a total acreage of 1,700 acres.

He established the first hardware store in Davis with his partner E. W. Brown. It was known as Davis Hardware Company.

He served as justice of the peace.

He was a member of the Methodist Church of Davis and a Mason in high standing.

He was the proprietor of The Davis Pool Room.

In 1857 he married Miss Eliza Reid (  1909). They had four children: Mrs. Jennie Drummond Read, Mary I. Long, Malford H. Drummond, and Elizabeth Holman.
