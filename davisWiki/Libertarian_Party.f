The http://www.lp.org Libertarian Party is a national political party that espouses a particular version of libertarian politics. According to a May 2008 report from the Yolo County elections office, the city of Davis has 181 registered Libertarians, or 0.52% of registered voters.Footnote(Yolo County Elections Office. http://www.yoloelections.org/voting/stats/sfiles/052208%20report%20of%20registration.pdf Report of Registration  State Reporting Districts. 20080522.)

Currently, no local representatives are Libertarians.

