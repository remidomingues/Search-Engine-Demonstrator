
The Address(Life Sciences Building,38.539493,121.756819) is located on Hutchison Drive next to the West Entry Parking Structure on campus.

It houses the Biological Sciences College of Biological Sciences in the basement and the Plant Biology department on the first floor.  There is a DNA sculpture that hangs in the stairwell and you can see to the top of the double helix if you stand underneath it.

LSB also contains many labs for researchers in the biological sciences.  It connects to the other large biology lab building, Briggs Hall Briggs via an external walkway.

The building was originally named the Life Sciences Addition in the 90s since it was added onto Briggs where Life Sciences already had offices and labs. The building was renamed after several years to simply Life Sciences.


