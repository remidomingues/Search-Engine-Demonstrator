Liggett and Drummonds General Store was a Davis store that opened in the Davis Timeline/1880 1880s.  The business was very successful in its time; it sold mainly hardware and Supermarkets & Grocery Stores groceries.  One of the original coowners was Malford H. Drummond.

Prominent Davisites Davisite AJ Plant worked as a bookkeeper for Liggett and Drummonds from 1886 to about 1895.
