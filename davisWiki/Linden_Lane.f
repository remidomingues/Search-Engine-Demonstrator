Linden Lane is a U shaped street that adjoins 8th Street between Anderson Road and Sycamore Lane.  This street has a bike walkthrough to Redwood Park and Cesar Chavez Elementary School.  Many parents use this street to pick up their children from school, to the great annoyance of some residents.  The street is also famous for its beautiful rainbow trees, which can be seen for about a week or two in Autumn.

In years past, many epic watergun battles took place between Linden Lane, Cesar Chavez Elementary School, and Maple Street.

The infamous Users/ChrisWaterstraat Chris Waterstraat once resided in a homely abode on these premises.


