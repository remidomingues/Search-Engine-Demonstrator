


The UC Davis Linguistics Club (/ju.si.dejvɪs.lɪŋ.wɪs.tɪks.klʌb/) is a student organizations student club at UC Davis dedicated to providing academic help academic support and social structures social networking opportunities to Linguistics students. Stumped by your latest LIN 1 homework? Puzzled by what your professors teacher means when they say that Japanese uses a topiccomment sentence structure? Want to sit around and chat about various bits of syntax all day? Feel free to stop by and hang out!

We meet once a week, and usually discuss topics in linguistics or homework help.

The club is 2009 young, and new ideas for activities are constantly being considered; if you have any ideas for things we can do, feel free to let us know! And remember to check out our Facebook group!

