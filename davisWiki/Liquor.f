Liquor is a highlyalcohol alcoholic distilled beverage made from any one of a variety of fermented sugars.  Its consumption often leads to Drunk drunkenness, and possibly sex.  It is often found at liquor stores, bars and parties.

Although it is tempting to carry flasks of liquor around on glorious holidays like Picnic Day, the Open Container Ordinance makes this illegal in a lot of places in town and its definitely not allowed on the UC Davis campus.

