

Davis Little League brings baseball to good places to take kids kids ages 512. Farm, AA, AAA, and Major teams play their games at the Davis Little League complex, whereas the Tball league has its games across the street at Community Park.  Teams practice at various fields around Davis.

The Davis little league is divided into two subleagues: American and National.  In general, the American League consists of East Davis East and South Davis residents, whereas the National league consists of West Davis West and North Davis.

The season runs from March until June, and can extend into July for the allstar tournament.

The league is entirely volunteer/parentrun.

