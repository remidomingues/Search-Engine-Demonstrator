Every week, KDVS hosts live musical acts in its secondary studio, Studio A. The show airs every Thursday from 11 p.m. to midnight. Many bands who perform are local to the Davis/Sacramento/Bay Area, but also come from across the country and the world on tour.

L.I.S.A, as it is endearingly nicknamed, is currently booked by Karen Carchidi.
The schedule of past and upcoming performers can be found http://www.kdvs.org/livestudioinformation here.

To be considered to perform on this program, email Karen at lisa@kdvs.org.
