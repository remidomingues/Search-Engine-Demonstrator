Los Tios is a Mexican food restaurant in Esparto.  Its a good place to eat if youre headed up to or back from to the Capay Valley for fun or gambling... er, outdoor fun, that is.  They serve burritos, tacos, etc.

They had a rough time when the building across the street collapsed and http://www.dot.ca.gov/dist3/departments/d3press/press09/09101%20SR16%20Esparto%20to%20Remain%20Closed%20UFN.pdf State Route 16 was closed.  All the casino traffic was detoured around them.  But theyre doing better now that the building was removed and the road reopened.


