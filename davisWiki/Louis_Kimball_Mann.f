

Louis Kimball Mann (19151964) was a UC Davis professor and expert on onions and related plants. Born a miners son in Boise, Idaho, on April 21, 1915, Louis Mann started his UC Davis career in 1942 as an olericulturist, plant physiologist and morphologist in the Department of Vegetable Crops and in the Experiment Station.

In 1965, the Mann Laboratory was built.
