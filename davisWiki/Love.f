Love is an emotion occasionally experienced by Davis residents.

Love between people often leads to (but not always and is not always required for) dating, sex, weddings, and olive oil.  Love by itself is seldom enough for a lasting relationship.  If you cant work it out together, there are Counselors and Therapists people to help.

Never forget that you are loved by somebody, even if it is not obvious who cares about you. Some Davisites view the town, and even the world, as a big family.

