
Lucy Puls has been a professor of art, emphasis in sculpture, at U.C. Davis for 23 years. She is currently chair of the Art Department.

Puls is an accomplished artist, having received her M.F.A from the Rhode Island School of Design in 1980 and exhibited her work across California and New York. A biography of her work may be found http://www.wirtzgallery.com/bios/bio_puls.html here.

