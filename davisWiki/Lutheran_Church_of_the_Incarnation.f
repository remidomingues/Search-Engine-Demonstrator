


Lutheran Church of the Incarnation (LCI) is part of the http://www.elca.org/ Evangelical Lutheran Church of America (ELCA).  It is one of several churches in West Davis.

Their motto is: Sharing Christs Light Daily.

They are Partnered with Davis Lutheran Church and The Belfry.

LCI is a Reconciling in Christ Community, welcoming all.

To learn about other places of worship in Davis, please visit our Spiritual Organizations page.
