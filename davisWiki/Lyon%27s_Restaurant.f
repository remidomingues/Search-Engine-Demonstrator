

Lyons was a American fare diner (a la IHOP or Dennys) restaurant.  They are a Northern California chain that was founded in San Francisco in 1952.  They peaked with 72 restaurants in 2001, but declared bankruptcy that year, blaming the wiki:wikipedia:Early 2000s recession recession, and closed nearly half their restaurants.  The local restaurant, number 469, survived for another four years, finally closing their doors in 2005.  By 2010, there were only five locations remaining.

The 24 hour Lyons in Sacramento remains open and is a common place to sober up or continue chatting into the next morning for people enjoying the downtown Sacramento scene.

Seasons is the current tenant of this space that is colocated with Hallmark Inn.

