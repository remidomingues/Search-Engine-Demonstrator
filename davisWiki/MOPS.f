Not Just Another play group...
(Mothers of PreSchoolers) MOPS is an international nonprofit organization providing small groups designed to support and encourage mothers of children preschool age and younger.

MOPS exists to encourage, equip and develop every mother of preschoolers to realize her potential as a woman, mother and leader.

A MOPS group is a place for moms to share joys and struggles and to learn practical ways to raise our children and care for ourselves. Together we address issues and needs of women and our families covering a wide range of topics.

