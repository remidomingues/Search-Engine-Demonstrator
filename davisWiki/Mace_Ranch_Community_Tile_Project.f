
Around the Mace Ranch Community Park Habitat Area is a collection of Community Created Tiles.  These tiles were created as a community art project during an event held at the Explorit Science Center.  Items from the near by area were collected and pressed into clay tiles to create artistic triangles which were then later placed into the area surrounding the habitat area.  Some are in the ground in arcs, some are in a vertical wall near the tunnel, and some are in a triangle just before the tunnel.

