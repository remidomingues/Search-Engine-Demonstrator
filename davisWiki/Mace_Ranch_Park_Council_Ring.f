
In Mace Ranch Park there is a Council Ring. It used to be a fire pit, but fires are not allowed here anymore. The benches were Doug Spranklings Eagle Scout project and were installed by Boy Scouts Boy Scout Troop 111.

There is a sign here that says:

  The council ring is closed between 10pm and 7am per Davis Municipal Code/27.02.170 Sec 27.02.170 Davis Municipal Code CC
  Fires not permitted per Davis Municipal Code/27.02.130 Sec 27.02.130 Davis Municipal Code CC Violators will be fined.

