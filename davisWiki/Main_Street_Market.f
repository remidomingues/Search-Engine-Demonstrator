


Main Street Market isnt just a regular Liquor Stores liquor store selling some awful bags of chips.  Its a onestop shop for all things Indian.  Lots of Basmati rice in 10 and 20 pound bags at reasonable prices.  Chutney, Masala, dal, Vermicelli, curry pastes, India Chai... lots of powders, incense, and spices.  It also has Bollywood movies along with classical and contemporary Indian music.  Highly recommended.

