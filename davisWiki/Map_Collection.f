

The map collection is the place to go on the UCD campus if you want to find some maps, air photos, atlases or other map related items. It includes a full collection of USGS Topographic Quads for California, and a long history of air photos for the central valley.

Note The map librarian has taken a position at another university as of Jan 2011. There are no immediate plans to fill the position due to internal ongoing reorganizing at the UC Davis Library. The library is accepting suggestions concerning the access and upkeep of the map collection.


