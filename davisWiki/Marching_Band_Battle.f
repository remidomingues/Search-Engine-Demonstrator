The Rules of a Marching Band Battle:
  Each band will play a song when their turn comes up.
  No band may play a song that has already been played.
  When a band no longer wishes to play, or runs out of songs, they play their fight song and leave.
  Last band standing wins.
  An agreement may be made to end a battle if both sides wish to leave. However, the Aggie Band will play their Aggie Fight fight song last, no matter what.

Unofficial Rule:
  The Aggie Band never loses.
