



Marcys New World Herbs & Gifts offered a variety of gift shops gift and personal enrichment items.  Jewelry, candles, incense, crystals, precious stones, soap, Tarot cards and bulk herbs are featured, and many items were handcrafted by local artisans.

Marcy Bradshaw is a certified Reiki master and offered classes in Reiki on an ongoing basis.  Also there were herbal classes, and how to classes. Monthly Alley Faires were held in the alley next to the shop with services such as Tarot readings, Reiki healings and Aura photography offered.

The shop drew Death and the Hanged Man in 2009 and went out of business.


