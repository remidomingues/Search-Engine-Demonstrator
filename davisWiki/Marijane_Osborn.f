Professor of English (B.A. University of California, Berkeley, 1962; M.A. Stanford University, 1965; Ph.D. Stanford University, 1969), she specializes in Old English studies, with an emphases on Beowulf and attention also to Old Norse texts. The Garden of Ladyes in the Voorhies Hall Voorhies Courtyard courtyard, with its David Austin roses, is her creation.

She is a member of the Davis Tree Commission and the developer of the Town and Gown Tree Walk.
