
Marilyn Stettler is a licensed Marriage & Family Counseling and Psychological Services Therapist, license MFC#14328.

She earned her masters degree in clinical counseling at the California State University at Hayward.  She is certified in hypnosis, and is certified by the Masterson Institute on psychoanalytic psychotherapy of the disorders of the self.  She is a founding member of the California Coalition for Ethical Mental Health Care and served on their board for six years.


