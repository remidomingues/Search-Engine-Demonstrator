
 Mission Statement 

Marketing and Business Association provides guidance in career planning in the business world, promotes professional and personal development, foster an environment of networking

 About 

The http://davismbaclub.wordpress.com Marketing and Business Association club at UC Davis is designed to meet the needs of students who are interested in or want to pursue a career in marketing or business. The Marketing and Business Association helps compensate for the absence of a marketing major and better prepares you for a career in marketing. This club will help members understand their educational options, specifically the MBA, after a career in marketing. We also specializing in entrepreneurship, finance, and marketing.There will be workshops, guest speakers, and social events.
