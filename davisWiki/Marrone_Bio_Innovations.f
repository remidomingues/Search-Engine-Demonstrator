

The company changed its name from Marrone Organic Innovations to Marrone Bio Innovations sometime in 2009.

As per their website, it is a new startup that discovers and develops effective natural products that fill unmet market needs for weed and pest management.  The company has already won several prestigious awards, including Best of Show at Golden Capital Network Angel and Venture Capital Summit, Best of Show at GCN Angel & VC Summit as well as having made it into the finals for the Ernst & Young Entrepreneur Of The Year Award for Northern California. Articles about MOI frequently appear in the Davis Enterprise, such as their being selected by the New York State Museum to commercialize the NYSM discovery of an organism for fighting invasive zebra mussels.

The founder of the company, Pam Marrone, was the force behind the Davisbased company Agraquest before moving on to found Marrone Bio Innovations.

