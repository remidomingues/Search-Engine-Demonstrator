
Martha Dickman has been a long time supporter, volunteer, and participant in various community musical projects, including the Davis Comic Opera Company, as long time producer and vocal director, and musical fundraisers for Citizens Who Care.   She moved to Davis in 1970, and has given vocal lessons to Davis students.  She was the 1995 recipient of the Harmony in Our Lives Award and was honored with the A. G. Brinley Award in 2006.

Source:

1995 Nov. 5, Dickman named recipient of music award, Davis Enterprise, pg. A1.
