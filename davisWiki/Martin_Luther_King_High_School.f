


Martin Luther King High School is an continuation schools High School program serving students in Davis.  A continuation high school ensures that students with diverse needs succeed in school and are supported in seeking better job opportunities or college.  King High School is an option for students in need of more flexible school schedules with an emphasis on vocational or career goals. This program is designed to meet the needs of students aged 16 through 18 who have not graduated from high school, are not exempt from compulsory school attendance, are deemed at risk of not completing their schooling and have whom Davis School for Independent Study DSIS isnt a good fit. You could get credits at a very accelerated rate. It produces quality graduates (and a few non graduates)



Note that it is not the Martin Luther King, Jr. High School (presumably to avoid confusion with a Junior High School).

It is colloquially referred to as King high by the Davisites


