 Demolition and Construction 







In Fall 2006, King High moved across the street into available space at the Susan B. Anthony Building for one year while a completely new building is built on the site of the existing school.  The 45year old portable buildings were removed and will be replaced with a new school building specifically designed for King Highs program needs. However, the school district halted construction in November after it discovered that it lacked funds to complete the job.

In the December 7, 2006 school board meeting, the results of an internal audit showed that the district actually does have enough money to cover the project but not all of it in hand. Allocating the funds for King High School immediately would deplete DJUSDs budget. Board members have not yet decided on what action to take. Footnote(Hudson, Jeff. http://davisenterprise.com/articles/2006/12/08/news/023new1.txt District tracks its King funds. The Davis Enterprise. 20061208.)

