

Martin Yan (Chinese: 甄文達; Pinyin: Zhēn Wéndá; Cantonese jyutping: Yan Man Tat) (born 1948) is a Chineseborn American chef and the host of the awardwinning US national cooking show Yan Can Cook. He is a UCD Alumnus, having received a Masters in Food Science in 1975. He is also on the board of directors of the Robert Mondavi Institute for Wine and Food Science.


