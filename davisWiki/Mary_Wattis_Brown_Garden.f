
The Mary Wattis Brown Garden of California Native Plants includes an extensive collection of native plants that work well in home landscapes, arrayed along meandering paths under stately oaks. The garden showcases a native meadow—a Californiastyle lawn—and many http://arboretum.ucdavis.edu/arboretum_all_stars.aspxArboretum Arboretum AllStars, our recommended plants for Valleywise gardens. Mary Wattis Brown was an avid gardener and botanist who recognized the need for education to promote conservation of California’s wild heritage. The collection is located along the southern shore of Lake Spafford across from Mrak Hall.








