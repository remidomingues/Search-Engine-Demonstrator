Marya Welch (25 September 1916  24 June 2012) was a professors professor, deans Dean of Women and major UC Davis Athletics athletics pioneer at UC Davis.  A veterans Lt. Commander in the Navy WAVES, she was the ninth female professor when she joined faculty in 1947, at a time when the student ratio in the 1,200 individual student body was 12:1 men to women.  She founded the womens athletics program, helping coach archery, basketball, softball swimming, tennis, track and field, and volleyball.

She founded the UC Davis chapter of the Prytanean Womens Honor Society.

Recognition

The Marya Welch Tennis Center and the four building cluster at The Colleges at La Rue known as Marya Welch Court are both named for her.

She was inducted into the Cal Aggie Athletics Hall of Fame in 1991.   She served as grand marshal of the Picnic Day Parade in 1999 (double check that year... I calculated that).

