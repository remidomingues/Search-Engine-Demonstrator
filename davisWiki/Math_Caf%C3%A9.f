


Math Cafe is a mathematics oriented student organizations club located in the Womens Resource and Research Center.  They offer tutoring as well as guest lectures.  The major purpose is to help facilitate mathematical learning in a friendly nonintimidating environment.  There is various food and a laid back atmosphere.  Math Cafe has a focus on the success of women in mathematics, since they are underrepresented in the field, but all people are more than welcome.  Basically, it is not a women only club, but has an interest in supporting women in math.


