
Mavis Henson Field is on County Road 102. It is home to the WoodlandDavis Aeromodelers Inc..

While driving into Woodland you may glance over to the right as you approach the new development and see a plane or two in the air performing acrobatic tricks, or precision flight. Sometimes it will be a simple stunt plane and other times an accurately detailed model.

Only pilots are allowed out onto the flight line. There is also a snack bar and a pond for amphibious and seaplanes.


The last day to fly here before the property closes is November 30^th^, 2010. They will be moving operations closer to the Yolo County Central Landfill after that.
