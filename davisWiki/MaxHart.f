 Max Hart 


Max Hart was born and raised in Davis, attending Birch Lane School Birch Lane Elementary, Valley Oak Elementary School Valley Oak Elementary, Holmes Junior High School Holmes Junior High, Davis High School and UCD.  He played in local bands Beef with Society, Girls Soccer, Beatbox, and Deathray, before splitting the scene.

Even though he doesnt live there anymore, he comes back all the time to visit his folks and rip it up with his buddies.  His latest base is Brooklyn, NY where he also maintains shared music studio space in the DUMBO area of town.
