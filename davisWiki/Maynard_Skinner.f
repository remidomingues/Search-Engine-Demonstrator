Maynard Skinner served on the City Council from 4/20/1966 to 4/20/1974 and 6/20/1988 to 6/20/1994.  Serving as Mayor from 1972 to 1973 and 1991 to 1992. He never held office as Mayor Pro Tem despite holding office after the new system was in place.

He is the only person to serve on the council in four different Davis Timeline/Decades decades 60s, 70s, 80s, 90s.

Maynard served as Asst. Vice chancellor of student affairs at UCD from 19711991.

Currently Maynard works as an adjunct asst. professor/ academic coordinator for the Community Connections project at CSUS.

His contributions to the City of Davis are many, but notably, conception and implementation of Central Park, the Davis Municipal Code/34 smoking codes, Davis Municipal Code/27 Save open Space, and Davis Municipal Code/6 bicycle lanes.


