

The McNair Scholars Program is a national program that helps undergraduate students from disadvantaged backgrounds prepare for PhD programs. It especially gives students invaluable research experience. Particularly the UC Davis branch offers free services like:

     Free tutoring
     Academic advising
     Graduate Record Exam (Graduate Record Examinations GRE) preparation
     Funding for travel to research conferences
     Pregraduate school advising
     Presentation assistance
     Presentation technology training
     A big study room
     Computer access
     Email access
     Class schedule planning
     Basic budgeting help
     Basic career counseling
     Mentoring by graduate students
     Help choosing a faculty research mentor
     Science writing assistance
     Publication opportunities
     Graduate book library

See http://www.ed.gov/programs/triomcnair/index.html National McNair Scholars Program for information from the US Department of Education.
