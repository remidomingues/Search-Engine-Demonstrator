
Mediation is a sculpture of hands that can be found in Central Park in downtown Davis.  It was created by artist Donna Billick and placed in Central Park Gardens on January 23, 2000.  It was donated to the City of Davis to honor Thomas H. Frankel for his many years of outstanding service to the community of Yolo County. (from the plaque)


