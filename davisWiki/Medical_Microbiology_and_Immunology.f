
Medical Microbiology and Immunology is a department of the School of Medicine.  They have labs in Schalm Hall as well as in Tupper Hall.

Their mission statement is to develop new approaches for the prevention and treatment of infectious diseases and immunologic disorders, and study the genes and proteins of pathogens in order to develop treatment methods.

They serve as a testing center for suspected cases of wiki:WikiPedia:Coccidioidomycosis, aka Valley Fever.
