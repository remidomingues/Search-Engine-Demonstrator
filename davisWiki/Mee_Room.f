

The George L. Mee Room is a conference room in the center of the third floor of the Memorial Union.  It is most well known for being the meeting room for ASUCD Senate every Thursday night at 6:10PM, although it is also used for several other meetings of campus organizations throughout the year.

There is a pillar right in the center of the room, so it is difficult to see a quarter of the room during ASUCD Senate senate meetings.


SOSSS proposed renaming the room the Yu Room, after former senator Tony Yu, to give the room a more welcoming feel than Mee Room.  They now propose naming it after Senator Cindy Yu instead. Of course, they could compromise and call it the Yu and Mee room...


