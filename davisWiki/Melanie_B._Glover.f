Melanie B. Glover graduated from UC Davis in December 2007. She was arts and entertainment editor of The California Aggie during the 20062007 school year and was an arts staff writer before that.

She has interned at Sactown magazine and is currently a freelance features writer for The Sacramento Bee as well as the administrative and publicity assistant for the Davis Art Center.

She is also in a band, Buildings Breeding.

