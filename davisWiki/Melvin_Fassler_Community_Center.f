
The Melvin Fassler Community Center is a large building available to the residents of Solano Park. It is sometimes also called the Solano Park Community Center. Each resident may reserve the building once per month, starting on the 11th of the previous month. Six large folding tables, dozens of chairs, a basic kitchen and a large BBQ/smoker outside make this a great place to host a large meal.

On the south side is a side room filled with toys. Two oneway mirrors make it possible to perform observational psychology experiments on unsuspecting children at play. An inflatable bouncy castle and waterslide are also available in the warmer months.


