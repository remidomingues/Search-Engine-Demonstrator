

Michael J Singer, Ph.D. is a professor and soil resource scientist at UC Davis.  His specialties are soil genesis, classification, land use and resource development; soil erosion and conservation, and he is a member of the Soils and Biogeochemistry program, part of the Department of Land, Air, and Water Resources.

He appears in the Modern Marvels episode Dirt, which first aired 20100110.

Singer was department chair of LAWR for several years in the 00s, but has since returned to doing more research.
