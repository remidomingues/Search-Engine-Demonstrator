
Davis version of a punk rock legend. His bands include Anal Mucus, Bottle of Violins, Gift of Goats, Feces Munchers, Girls Soccer, the Turdwrenchers, the MagiKool Dudes and a few others.

He has hosted a show on KDVS for many years. He is on Tuesdays at nine pm.

