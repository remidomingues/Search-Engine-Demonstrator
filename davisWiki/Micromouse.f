
The Micromouse design project, offered as EEC194ABC in the Electrical and Computer Engineering department, is an annual competition for teams of up to 5 students to design and build a small, autonomous, mazesolving robot. It is sponsored by IEEE. The goal is to solve a maze (whose layout is unknown) in the shortest amount of time. Complete rules for the mice and maze construction are available at the instructor, Dr Taks, http://www.drtak.org/teaches/UCD/ website.

The course is 5 units, three quarters long and grading is deferred until the end of the year. The first quarter is dedicated to lectures about basic design and programming of the mouse. The second and first half of the third quarter are when the actual work begins and, hopefully, ends. After Picnic Day, each team submits a report detailing their design successes and failures.

There are two opportunities to compete (for prizes) against other UCD teams as well as teams from other schools: Picnic Day and the http://ewh.ieee.org/reg/6/sac/micromouse.htm IEEE regional conference, both near the end of Spring quarter. Prize money is awarded to the fastest three teams at each competition. The winner of the regional competition also brings home a large trophy, currently located in the Kemper Hall lobby thanks to the efforts of the winning 2005 team, until the following years event.

Micromouse is an excellent opportunity for both Electrical and Computer Engineering and Computer Science students to apply what they have learned in class. EEC172 is probably the most helpful course to take.

