At UC Davis, all major exams are called midterms.  You might be inclined to think that, with a name like midterm, it would happen in week 5, and there would only be one.  This is frequently not the case. Some classes will have as many as three midterms, with the first one happening as early as week 3.

Be sure to check your syllabus for the date(s) of your midterm(s).  Its also a good idea to keep a calendar or planner and, as soon as you get all your syllabi, write down the dates of when your midterms are and when The perfect paper papers are due, for all of your classes.

If you want to plan in advance, sometimes professors and instructors put up their syllabi on MyUCDavis before the class even begins.


