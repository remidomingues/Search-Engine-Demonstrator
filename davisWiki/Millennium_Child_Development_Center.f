

The Millennium Child Development Center offers high Quality programs targeted to the specific developmental needs of infants, toddlers and preschoolers. Millennium also offers a schoolage program.  All programs use emergent curriculum methods to explore  academics such as language, art, science, music, social studies and fine and gross motor tasks.

Millennium has two locations; one in Dixon and one in Vacaville.  Call to tour the center and learn about their program.

