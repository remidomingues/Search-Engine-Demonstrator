Miller Hall is one of the three buildings located in Segundo North. It houses the approximately 120 members of the Integrated Studies program.  The building was first opened for use in Fall 2004. It has a separate meeting area on the ground floor, which has been used for summer advising Freshman Seminars.

During the 20062007 school year, Miller Hall instead housed 171 Integrated Studies students, because of increased interest in the program and a larger freshman class. This meant that all of the double occupancy rooms became triple occupancy.

