
Mindy Cooper is part of the U.C. Davis Theatre and Dance Departments visiting faculty. When teaching, Mindy instructs ballet, jazz, and musical theatre classes. She most recently taught History of Musical Theatre in Spring 2005.

Mindy has an extensive Broadway background. She performed in 1996s stage revival of Chicago, performed and was dance captain for the original Broadway version of Titanic (9799), and choreographed Dracula, the musical, which ran from Aug. 2004Jan. 2005.

Cooper choreographed the musical Urinetown at UC Davis in Spring 2007 and will be choreographing the musical Oklahoma at UC Davis in Spring 2009. Oklahoma will be performed in the Mondavi Center.
