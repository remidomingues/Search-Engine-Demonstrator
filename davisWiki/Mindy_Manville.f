
Since 1997 Mindy Manville has taught music classes to hundreds of toddlers and kids up through age nine through the Davis Art Center programs.  Through the DAC, she has also put on various childrens musical theater productions.  She also does various musical projects at elementary schools in Davis.

Manville received the Harmony in Our Lives Award in 2009 for her work.

