The Mondavi Center Summer Music Series has been revived; it last functioned as an event in 2003. Performances take place on the UC Davis campus Quad lawn. Picnicking can begin at 6:30pm and music starts at 7:30pm. All concerts are free and open to the public. No alcohol is permitted.

The 2006 summer bill includes:

  Sidestepper (June 28)
  Diane Reeves (July 27)
  Dirty Dozen Brass Band (Aug. 15)

For more information, visit the http://www.mondaviarts.org/events/summermusic.cfm Mondavi site.

