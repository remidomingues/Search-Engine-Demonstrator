
Address(Montgomery Avenue) runs from Mace Boulevard almost all the way west into Walnut Park in South Davis.  It is one of the few streets in Davis which is designated for bicycle traffic and has a lot of car traffic, but does not have a safe bicycle lane.


The Montgomery Avenue Bridge (if it is so called) looks to be quite a similar style to the Arboretum Bridges  Old Bridge in the Arboretum.

The Avenue might be named after the Montgomerys of Susan Montgomery Ranch fame.
