
The Morris Fountain is a square arch Water Fountains fountain that honors Madison and Nora Morris and Zoa MorrisLycan and was dedicated in 2006.  It is located in the Larry N. Vanderhoef Quad on the UC Davis campus near the Buehler Alumni & Visitors Center. The fountain isnt always on, but you can often find a few ducks hanging out, perhaps enjoying a brief respite from the crowded arboretum.

