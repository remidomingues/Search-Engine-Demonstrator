
Mosaic Coffee & Tea is an upcoming nonprofit coffee shop that will run on a http://en.wikipedia.org/wiki/Gift_economy gift economy system. In addition to coffee and tea, the menu will likely feature healthy smoothies, baked goods, fruits, desserts, and glutenfree options.  A core mission of the shop will be to provide job training and experience for adults with disabilities.

It will be located in the Fellowship Hall of Davis Community Church. Due to the large space, it is anticipated that it will be a good place to bring the whole family.

If all goes smoothly, Mosaic will open in 2014 at the earliest.
