Every once in while a mountain lion will make its way to Davis by following Putah Creek down from the Lake Berryessa area. Usually the first to notice it are those living in South Davis who have homes backing up to the creek.

Users/NickSchmalenberger Nick recalls hearing that in the early 90s a mountain lion came out of Putah Creek by the University Airport, near where the levee road turns north away from the current channel.

