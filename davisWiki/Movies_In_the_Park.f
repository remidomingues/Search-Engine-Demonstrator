

Movies in the Park is a free, outdoor movie series sponsored by Davis Sunset Rotary and Woodland Healthcare  Davis Satellite. The program was formerly run by the Citys Community Services Department, but was cut from the budget in 2011.

The movies start at sunset.

Oriented towards children, with entertainment and food for sale before and during the show (or feel free to bring your own picnic).

