


Address(Mulberry Lane) is a residential loop street in Central Davis. It is a fairly quiet street and does not have much traffic through it.

Their is an alley in the middle of the loop that connects it to the parking lots of The Episcopal Church of St. Martin St. Martin Episcopal Church and Davis Medical Center, and the path ends up behind the University Mall.

Mulberry Lane was the site of the Pirate Ship as well as Fort Bro.

