Ahhhh, the mullet  business in the front, party in the rear; short on top for the fellas, long in back for the ladies. This disgustingly beautiful Hairstyles hairstyle has been known to show up around the Davis area.

It goes by various other names, some more obscure than others:

  10/90 (10% in the front, 90% in the back)
  Ape Drape
  Arkansas Waterfall
  Camaro cut
  Femmulet (for women)
  Hockey Hair
  Mississippi Mudflap
  Norco Neck Warmer
  Soccer Rocker
  Tennesse Top Hat
  Vokuhila (Fokooheella) (German)

