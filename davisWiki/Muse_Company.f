
The Muse Company provides quality entertainment and handson educational programs in the performing arts to children and young adults from preschool through adulthood.

The company provides families throughout the bay area with opportunities to experience quality live theatre at affordable prices, as well as programs that mentor children in all aspects of the performing arts.

The Muse Company creates the opportunity for talented young people to develop theatrical abilities by encouraging participation in the production and performance of theatrical plays. The goal of each performance is to educate and entertain young people through the medium of presentational and participatory drama.
