
My Broskies is a cartoon strip that ran during the 200910 school year in The California Aggie. My Broskies is the creation of cartoonist and designer Billy Hong. The comic strip is inspired by the idea of The bro life. Mr. Hong states, The cartoon is a juxtaposition of something that looks cute, but is actually vulgar and nasty.

My Broskies started is Billy Hongs Design 1 class for a journal project and from then on Billy has made My Broskies comic strip almost daily.

Oddly enough, the cartoon has been officially endorsed by Campus Recreation.

