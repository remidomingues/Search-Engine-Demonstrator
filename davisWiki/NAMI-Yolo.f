
NAMIYolo is a grassroots program of education, advocacy and support dedicated to improving the lives of people in Yolo County with Counseling and Psychological Services psychiatric Center for Mind and Brain brain disorders including clinical depression, bipolar disorder, and schizophrenia.

NAMIYolo is the local chapter of the National Alliance on Mental Illness and part of NAMICalifornia.

It sponsors a yearly Sunflower Art Competition.  The exhibit runs the first three weeks in June.


