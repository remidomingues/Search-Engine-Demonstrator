Nancy has two careers.

She is a Gold Certified Personal Fitness Trainer by the American Council on Exercise since 1990 with special recognition in Mind/ Body Integration. She is also a Medical Exercise Specialist certified by the American Academy of Health, Fitness and Rehabilitation Professionals. She is additionally certified in Advanced Pilates Mat by Powerhouse Pilates.

She is also a psychotherapist (LCSW) and has maintained a Nancy Chadwick, LCSW private practice in psychotherapy since 1976.

These two careers enhance each other, giving her clients a rich experience.
