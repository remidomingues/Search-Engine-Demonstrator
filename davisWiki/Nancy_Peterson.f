Nancy Peterson was  elected to the Board of Education Davis JUSD School Board in November 2012 Presidential Election November 2012.  She has also been involved as a board member of the Davis Bridge Educational Foundation, and as a board member and president (2011) of the Blue and White Foundation Davis Blue and White Foundation.

In 2013 into 2014, Nancy Peterson was connected to a controversial dismissal of Davis High School Girls and Boys Volleyball coach Julie Crawford.  In connection to this, Peterson resigned from the school board on March 6, 2014.

