
Naples onion (Allium neapolitanum) is a dainty early springblooming bulb from the lily family native to northern Italy. Like most alliums, the Naples onion, or white garlic, produces a rounded head of white flowers. Growing to a height of 1218, it prefers full sun and is drought tolerant; this classic is a gracefully sparkling white flower which will grow most anywhere, with enough sunshine. The plant naturalizes well and can easily be propagated by division of corms or collecting seedheads  in the right environment, the Naples onion can selfsow to the point of nuisance.

Most members of this genus are intolerant of competition however. Because they are so long lasting in water, many alliums make excellent cut flowers.

