

The BS in Natural Sciences is a program offered by the UC Davis Geology Department geology department.  It is designed for prospective science teachers, but can also be useful for Undergraduate Students students who like science but are really indecisive.  For the major, you take introductory courses in Mathematics and the four basic sciences, Geology, Chemistry, Physics, and Biology.  You then take indepth courses in two of these sciences.

The Undergraduate Advisers are Howard Day and David Osleger.

