
Neal Van Alfen was raised in Modesto, California, and received a B.S. in chemistry in 1968 and M.S. in botany in 1969 from wiki:WikiPedia:Brigham Young University. He received a Ph.D. in Plant Pathology plant pathology from the UC Davis in 1972.

In 1999, Van Alfen returned to UC Davis to become deans dean of the College of Agricultural and Environmental Sciences.
