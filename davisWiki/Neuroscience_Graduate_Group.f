

The Neuroscience Program offers interdisciplinary training for Ph.D. students in all areas of neuroscience, from molecules to cognition. It is interdepartmental, and includes both basic scientists and clinical researchers, so students have an unusually broad range of research opportunities. For the first year of the program, students are housed in the Center for Neuroscience.

The chair for the program is Barbara Chapman, Ph.D.



