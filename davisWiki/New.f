 Davis Drones 
http://www.gizmag.com/uavcropdusting/27974/
From Slashdot: Researchers at University of California, Davis, in cooperation with the Yamaha Motor Corporation, are testing UAV crop dusting on the Oakville Experimental Vineyard at the UC Oakville Station using a Yamaha RMax remotecontrolled helicopter. The purpose is to study the adaptation of Japanese UAV crop dusting techniques for US agriculture, but not all the hurdles they face are technological.
