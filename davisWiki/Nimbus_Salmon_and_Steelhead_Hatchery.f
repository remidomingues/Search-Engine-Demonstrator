
The Nimbus Salmon and Steelhead Hatchery is an awesome Good Places to Take Kids place to take kids (its also a good place to go without kids).  During the late fall (usually November 1stish) you can watch as fish jump up the fish ladders.  The visitor center is open from 10AM  3PM every day and has great exhibits for kids.  What kids will love most will be walking along the fish tanks and feeding the fish.  For a mere nickel you get a whole handful of fish food to throw into the tanks and watch fish go wild.  Bring a roll of nickels for hours of enjoyment.

There are many walking trails as well as bicycling trails (including the American River Bike Trail) leading from this spot as well.

