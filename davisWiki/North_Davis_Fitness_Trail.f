This Fitness Trails fitness trail runs along the boundary of North Davis, starting from about Falcon Avenue and moving northeastwards toward Sandpiper Drive.  The last station (stretching/cooldown) is in Northstar Park across the street from Sandpiper.

A great feature of this trail is that it is bounded on one side by a Covell Drainage Channel canal and open farmland.  In addition, there is great foliage and flowers along both sides of the trail in many places.

