The North Davis Riparian Greenbelt Project intends to green a stormwater channel in north Davis by replacing weeds with droughttolerant native plants, establishing trees to shade the channel, and creating educational areas to learn about native plants and sustainable landscape management. Funding for this project is provided by the Strategic Growth Councils Urban Greening Program, funded by the voters of California via Proposition 84. The project is lead by Putah Creek Council with a consortium of community and nonprofit partners.

