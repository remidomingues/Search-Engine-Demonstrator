The Northstar Greenbelt is part of the Greenbelt that covers a sizable chunk of North Davis, connected by bridge to the community park next to the Davis Senior High School. It includes a series of winding bikepaths, playgrounds, a hill nicknamed the toilet bowl for its curvature, and the Northstar drainage pond.



