
The Northstar Nature Address(Trail, 38.571052, 121.751422) is a short trail in Northstar Greenbelt adjacent to the Northstar Park soccer fields.  It features some native and droughttolerant Gardening plants, lots of Sun Protection shade, and some Send My Roots Rain artistic sculptures.  The City of Davis city placed an informational sign here about the benefits of nontoxic Pest Control pest management.


