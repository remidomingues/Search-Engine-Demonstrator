


The Unitrans O Bus Line is a shuttle that intends to let riders get their Target shop on from most student housing locations, including West Village.  It is the only bus that will drop you in front of Target.

The O line was created for freshmen and other students living oncampus as an way to get to popular shopping centers around Davis.  It roughly overlaps the A Bus Line A line, C Bus Line C line, E Bus Line E line, and V Bus Line V line, as these particular routes are not serviced on the weekends

It began on January 30th, 2010 and now operates all weekends and certain holidays, except during Saturdays with Finals Service.  See http://unitrans.ucdavis.edu/o/ Schedule and Map at Unitrans website.

NOTE: The part of the route that serves West Village will begin on September 3, 2011.
