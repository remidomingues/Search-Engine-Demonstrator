Address(Oeste Drive) runs parallel to Anderson Road, North to South. While people pronounce the name the Spanish way, the street is actually named for a deceased Fire Chief. He pronounced it Ostee.

The Oeste Manor Neighborhood Association hosted the first Good Neighbor Policy event in September 2005.

The unofficial Tau Kappa Epsilon house is located on the corner of Oeste and Russell Boulevard.

