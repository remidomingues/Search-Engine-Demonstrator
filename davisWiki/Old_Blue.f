
Old Blue was the 1958 Chevrolet Apache fleetside pickup truck that Users/AdamFlowers Adam Flowers drove while he was in high school.  It had a 1968 Camaro engine in it.  At first he thought his dad, Mike Flowers was crazy for letting him drive such a cool and car all around town until it occurred to Adam that there was only one car in town that looked like it and if he did anything mischievous or illegal while driving the car, everyone would know that it was him.

It was sold in 1996.
