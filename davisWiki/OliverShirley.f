
The Souterrain is a threepiece folk outfit from Davis, and all members are, or have been, students at UC Davis. From http://theSouterrain.com theSouterrain.com: We make some strange music, but we do our best to make it pretty. Folk cures, lullabies, and revenge fantasies all have their home in our soundscape.  The name comes from the French word for Underground.

History
The band formed in November 2010, completing its line up when Sachs transferred to Davis from Orange County. Norton and Shirley met in a production of Macbeth in 2009, and, after several chance encounters, decided to play a few tunes together.  The results of their first meeting were Mr. Bones and What Ails Youtwo songs which demonstrate the range and sensibility of The Souterrain: intimate lyricism, spare melodic accompaniment, goblins and insomnia.  They called it lowcore for its understated intensity, a term coined by Norton in 2010.

