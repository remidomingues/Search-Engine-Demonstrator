In late March and early April of 2014, mock banners appeared on campus lightposts near Shields library headed towards the Silo on the South side of the street.  They were done in the style of a current UCD PR campaign One UC Davis (which they were mounted below) and were professionally designed and crafted, if somewhat jankily attached. The banners were created by five art history students to highlight the rising issues of student debt. An accompanying http://www.onedebt.info website provided more statistics and personal stories of students leaving UC Davis with looming loan repayments.

They lasted a couple weeks, but were conveniently removed just before Decision Day.





