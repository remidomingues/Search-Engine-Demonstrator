

One Drop is one of the AS Papers and the focus of this magazine is on issues of mixed experiences.  One Drop aims to have a publication once a quarter.  Pick up a publication around campus at the AS Papers rack.

The Spring 2006 issue appeared during Mixed Heritage Week May 812, 2006 and is now available around campus at AS Papers racks.  The Fall 2006 issue is currently being worked on.

