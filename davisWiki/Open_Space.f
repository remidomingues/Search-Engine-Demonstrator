 Within Davis 

The City of Davis has long been concerned with maintaining open space both within and outside its borders. From Davis Municipal Code/27 Davis Municipal Code 27.01.01c,

 Open space area means a natural, open space area owned, used or maintained by the city, and devoted to habitat, agriculture or passive recreation and not designated a park by the city.

All open space matters are overseen by the City of Davis Open Space and Habitat Commission.

The city prohibits open space land from being developed. They are not the same as Parks, which are specifically devoted to recreation. Some examples include Davis Wetlands, North Area Pond, Sacramento Bypass Wildlife Area, and West Area Pond.

The passage of Measure O in 2000 gave the city more funds for the acquisition and management of open space.

Open spaces differ from the Greenbelts greenbelts inside Davis, which are developed and devoted to recreation. Greenbelts outside Davis are different, see below.

 Outside Davis 
Nearby cities and counties as well as state and federal agencies have an interest in maintaining farmland in the region. Within Yolo County the http://www.yololandtrust.org/ holds open space related easements on various lands and the http://www.solanolandtrust.org/ does similarly in Solano County.  The City of Davis holds many easements in partnership with both the Yolo and Solano Land Trusts. The DavisDixon Greenbelt and VacavilleDixon Greenbelt are two stretches of land along Interstate 80 with conservation easements that restrict development. The goal is to keep the cities separated to prevent urban sprawl.

