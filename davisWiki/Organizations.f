Wherever people go, they tend to form organizations: groups with similar interests who associate for mutual benefit or at least mutual fun.  Here are some of the different types of organizations in Davis:

  Community Organizations  groups which have a broad base of support in the Community.
  Environmental Organizations  cabals that help Arboretum Mother Earth.
  Student Organizations  clubs whose membership comes primarily from the UC Davis Campus.
  Spiritual Organizations  congregations of all types who are concerned with the divine.
  Social Structures  loose associations of people with similar interests.
