A list of pages no other page links to. You cannot find these pages unless you search for them  so lets link them from someplace! One way to do this is to pick an orphan and search to see if it is mentioned somewhere else in the wiki. Orphaned pages are listed first and followed by orphaned redirects.

Note that orphaned redirects are not necessarily a bad thing, as they may be linked to from other external websites, so prune them with caution, lest we disown the orphans with the bathwater.

The dual to this page is Outgoing Links  a list of pages based upon the number of links leaving each page.  Meanwhile, the very few orphans that can never find homes due to no fault of their own can be placed in the /Foster Home Orphaned Pages Foster Home.



