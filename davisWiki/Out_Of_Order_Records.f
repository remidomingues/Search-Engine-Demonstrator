

Out of Order Records is an independent/underground record label that started up in November of 2004. It is run by Users/HeatherKlinger Heather Klinger of KDVS fame. Out of Order is also a mailorder that carries a variety of mainly vinyl releases from other labels and artists. There is also a zine and Blogs blog that focus on other independent music with reviews, interviews, articles, and more.

