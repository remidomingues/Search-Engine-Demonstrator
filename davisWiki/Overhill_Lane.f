Overhill Lane is one of the many Navigating Davis streets in Davis Village Homes neighborhood that are named after places or characters in J.R.R. Tolkiens books, The Hobbit and The Lord of the Rings.

Overhill Lane branched off from Westernesse Road.

Overhill is the township which lay on the far side of Hobbiton from where Frodo and Bilbo lived.
