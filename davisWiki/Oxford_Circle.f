Oxford Circle, is a smallish street with many people living on it.  It is part of the college streets in the town street name themes.
Oxford Circle was also the name of a Gary Lee Yoder band that shared the stage with such famous names as The Grateful Dead. The band never got a large record contract, but released a single Foolish Woman/Mind Destruction that is apparently now a highly soughtafter collectors item.

