

PHM Property Management was a property management company owned and operated by Dave and Patti Miller, who are both are long time Davis residents.

PHM was in existence between 1985 and 2009.  PHM managed approximately 190 properties in Davis.  The staff was comprised of: Nicole (the receptionist), Marianna (the repair coordinator), and Kim (the office manager).  Patti was the chief accountant, and Dave did whatever else needed to be done.

Patti states, we have all been tenants ourselves at one time or another in our lives, and we all understand what its like to have someone who really cares when you have a problem.  We are aware that somtimes things dont go as planned, but we really care about our tenants and we want to help provide a nice home for all of them.

