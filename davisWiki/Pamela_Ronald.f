Pamela Ronald is a professor of plant pathology at UC Davis.  She was part of a team that developed a variety of flood resistant Rice Paddies rice, opening the possibility for improving the stability of a base food supply for many areas in the world that have fragile food supplies.  In recognition of this achievement, the USDA awarded Ronald the highly coveted Discovery Award in December, 2008.


