
The Panhellenic Council (more formally known as the Davis Collegiate Panhellenic Association) is the governing council for 10 sororities at UC Davis. They are responsible for organizing recruitment, and overseeing that all regulations are upheld. DCPA meetings are open to the public and are held weekly on Wednesdays at 6:10 in the Garrison Room, Memorial Union 2nd floor.

The 10 DCPA Sororities include:

  Alpha Chi Omega
  alpha Kappa Delta Phi
  Alpha Phi
  Chi Omega
  Delta Delta Delta
  Delta Gamma
  Kappa Alpha Theta
  Kappa Kappa Gamma
  Pi Beta Phi
  Sigma Alpha Epsilon Pi

Fall Recruitment 2012
The Fall Formal Recruitment process is a weeklong process where women interested in joining one of UC Davis National Panhellenic Conference sororities have the opportunity to meet women from each of the 8 chapters. DCPA Associate chapters alpha Kappa Delta Phi and Sigma Alpha Epsilon Pi each hold informal recruitment during fall quarter. To find out more about their informal recruitment, check out their chapter profiles at http://ucdpanhellenic.org/chapters.html.

Fall Formal Recruitment will take place from September 24th30th 2012. This is a week long event involving a mutual selection process between women interested in joining a chapter, and the 8 National Panhellenic Conference chapters on campus. To signup for recruitment, visit http://ucdpanhellenic.org/howtoregister.html. Questions and concerns about registration, recruitment, and Greek Life can be emailed to the head recruitment team at ucdrecruitment2012@gmail.com. Add Polly Panhellenic on Facebook at http://www.facebook.com/ucdrecruitment2012 to ask questions and talk with other women going through recruitment this year!

Panhellenic Creed
We, as Undergraduate Members of womens fraternities, stand for good scholarship, for guarding of good health, for maintenance of fine standards, and for serving, to the best of our ability, our college community.  Cooperation for furthering fraternity life, in harmony with its best possibilities, is the ideal that shall guide our fraternity activities.

We, as Fraternity Women, stand for service through the development of character inspired by the close contact and deep friendship of individual fraternity and Panhellenic life.  The opportunity for wide and wise human service, through mutual respect and helpfulness, is the tenet by which we strive to live.

