
Paperclips was a comic in The Aggie during the 20052006 school year which focused around Sam, a character apparently based on the author, and a collection of talking objects.  It was drawn by Users/SamSellers Sam Sellers.

A Paperclips book sold at the UCD Bookstore for $5 and is still available. The book is also available for purchase for $3 http://www.samsellers.com/comic.html here.



