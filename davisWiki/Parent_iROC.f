

Parent iROC is a group for parenting parents/caretakers that have children with ADHD/ Executive Functioning challenges (as well as children diagnosed with High Functioning Autism, Aspergers Syndrome, PDDNOS, LD, ODD, etc.)  We will focus on specific topics each month, but will also share and discuss every day challenges and tips with the support of an ADHD/EF coach.

If interested in having an ADHD/EF coach for your child or yourself, please contact iROC Education and Family Consulting.


