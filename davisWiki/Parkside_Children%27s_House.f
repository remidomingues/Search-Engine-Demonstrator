
Parkside Childrens House is a Montessori Schools school for children from the age of 3 to 6 years old (includes Kindergarten program). You will generally see 15 or more different activities going on at the same time ranging from the very concrete to the more abstract. The children work at their developmental ages, not their chronological ages.  The goal at Parkside Childrens House is to provide an environment that will cultivate the childs own natural desire to learn.


