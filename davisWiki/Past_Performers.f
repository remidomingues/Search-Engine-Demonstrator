The Delta of Venus has been host to numerous musicians, DJs and performance artists. This is an attempt to historicize this fact.

Please keep in mind that the following list of performers is incomplete! Use your scrapbooks (or memories!) to recall anyone who played the Delta and might be missing from the data bank! Artsweek in The California Aggie, or the http://www.deltaofvenus.org Delta of Venus website, are good places to search.

