
Paul Hom Asian Clinic is one of the seven free students student  run clinics from UC Davis School of Medicine. This clinic is staffed by UC Davis undergraduate interns called Patient Advocates, medical students, and attending physicians from UC Davis Medical Center and throughout the Sacramento community.

The primary goals of Paul Hom Asian Clinic are to provide free primary care services to the medically underserved Asian Americans, to provide translation services in clinical settings for Asian immigrants, and to provide opportunities for medical students and undergraduate UC Davis students to gain experience in community medicine.

