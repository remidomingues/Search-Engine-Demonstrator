


PawiesPets  (Pawies Pet Services) provides pet sitting, dog walking, and other pet services.  The business originated in Capitola/Santa Cruz, but now provides local services for Davis pet owners, with discounts for those located within a mile of UC Davis campus.

Note: They walk one dog at a time, unless otherwise requested by the owner. Special discounts and group walks for multipet households.

