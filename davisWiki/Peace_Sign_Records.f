Peace Sign Records is an indie record label specializing in lofi recording, started by local Davis musician Users/WillJobe Will Jobe. The label emphasizes the DIY (Do It Yourself) ethic, especially with its recording equipment, which primarily consists of a boombox recording system and two computers with linein mics running recording software. Its studio is located in Wills garage.
