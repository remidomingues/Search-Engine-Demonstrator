This is a simple list of the people of Davis, pretty heavily biased to those who use the wiki (since this list is compiled by people who use the wiki). You can also see some User Statistics. When people get together they form Social Structures.

You are part of Davis, so add yourself: make a homepage for yourself then add it to this list! Dont be shy! To make a homepage for yourself, click on your username in the upperright corner of the screen OR enter your username into the search box. Then, choose to create a new page with this title (or maybe if theres already a page for you, then youre awfully lucky!) Then just fill in your page with whatever you want. This is, in general, how you create a new page in the wiki without adding a link to an existing page. Keep format, and add after youve made yourself a nice page, keeps Wanted Pages down!

Then, once youve got a homepage, come back here and add yourself, but keep it alphabetized so Users/AmitVainsencher doesnt cry!

Or add other people. Some people are so notable locally that they have entries even though they havent edited the wiki. Please please please add people if you think theyre relevant to Davis! The more the merrier! The only reason most of the names on there are of people who have wiki accounts is that no one has taken the time to flesh it out more. As with all things on the Wiki, theres a selfselection bias. People with accounts are the ones who add stuff. One cautionary note: if the names youre adding arent those of public figures, its probably better to check with the people first to make sure they dont mind having their name on the page.

There are also some Wiki Animals that use the wiki (or their owners do...)
