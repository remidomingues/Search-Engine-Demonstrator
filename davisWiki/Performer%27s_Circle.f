
The Village Homes Performers Circle is an opportunity for experienced and inexperienced performers to present stories, music and dance, etc., and of course to watch...  It is usually unmiked, though occasionally people will bring small amplifiers. The performances lean toward folk music but all styles are encouraged and welcome. This is a free event.

