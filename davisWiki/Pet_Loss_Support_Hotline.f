
The Pet Loss Support Hotline was a telephone hotline operated for years by the School of Veterinary Medicine for owners who had lost or were going to lose their pets.  The universitys budget crisis forced the closure of the hotline on June 27, 2009.

While the UCD Pet Loss Support Hotline is now gone, the vet school still maintains a website (see link above) with information on other hotlines and other resources for those dealing with the loss of a pet.
