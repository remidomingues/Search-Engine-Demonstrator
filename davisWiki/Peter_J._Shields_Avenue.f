Peter J. Shields Avenue, or Shields Ave. for short, is the default address for the university, One Shields Avenue. The street runs west from the corner of 1st Street 1st and A Street A until it Ts into California Avenue. It passes the north side of Shields Library.  Try as you like, you will not find an actual, 1 Shields Avenue address to go with a building.

The Avenue is named after Peter J. Shields, a man who helped create UC Davis.

 

