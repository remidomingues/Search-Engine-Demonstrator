

Peter S. Beagle is a Noteworthy Published Authors well known author who lived in Davis for many years.   One of his more famous stories is wiki:WikiPedia:The_Last_Unicorn The Last Unicorn.

In 2006, Users/JabberWokky attended Balticon 40 and got a chance to speak with him about his time in Davis.  Flashing a wide smile, he spoke warmly about how much he had enjoyed living in Davis, and said he still had friends there and visits occasionally.  He said he had lived at 2135 Humboldt Avenue (right near Arroyo Park).  The line to see him was held up, but he was very happy to talk about his time in Davis.  Very nice guy who clearly loves his former hometown.

On a support the (formerly) local artist note, he does not get royalties for the new DVD release of The Last Unicorn.  Unless you order it via http://www.conlanpress.com/Merchant2/merchant.mvc Conlan Press.  Hell even toss in an autograph if you buy it from them.



