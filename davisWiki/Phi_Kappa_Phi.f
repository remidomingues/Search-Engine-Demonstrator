The Honor Society of Phi Kappa Phi (or simply Phi Kappa Phi) is the oldest, largest and most selective alldiscipline honor society for colleges or universities in the United States. Induction into The Honor Society of Phi Kappa Phi is considered as one of the highest and most prestigious forms of individual academic achievement for graduates of colleges or universities. The societys motto is Philosophía Krateítõ Phõtôn which translated is Let the love of learning rule humanity.

Phi Kappa Phi inducts more than 30,000 students, faculty, professional staff and alumni at approximately 300 select colleges and universities. Membership is by invitation only and is reserved for the top 7.5 percent of secondsemester university juniors and top 10 percent of seniors and graduate students. Faculty, professional staff and alumni who have achieved scholarly distinction also qualify. Good moral character is an essential supporting attribute of membership in Phi Kappa Phi.

Chapter #069 at the University of California Davis was founded on November 12, 1954.

