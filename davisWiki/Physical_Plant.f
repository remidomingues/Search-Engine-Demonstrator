The Physical Plant complex is located between King Hall on the east, Physics and Geology Physics Geology on the north, and Mathematical Sciences Building and Academic Surge on the west.

The complex includes:
 Physical Plant Offices  Operations & Maintenance the people who keep the physical portion of campus running.
 Service Request Center  call 7531655 to submit a work order or report a problem.
 The primary campus Water Towers water tower
 Rifle Range
 Storehouse
 Shipping and Receiving
 Physical Plant Shops

The site that all of this rest on (with the exception of the water tower) is space that is scheduled to be used for other purposes. A new complex will have to be built to house all of these operations before that can happen.
