Picnics (and Barbecue Grills BBQ grilling) are a great way to enjoy the outdoors and socialize with other members of the Davis community.

There are also periodic Wiki Gatherings at which all are welcome.

Places to Picnic
There are lots of places in and around town that can be the perfect setting for a picnic:
  The Quad
  The Parks in town
  The Putah Creek Picnic Area
  The Yolo County Grasslands Regional Park
  The Yolo Bypass
  The Arboretum
  The Lake Berryessa beaches
  Picnic Day (BYOB)
