Image(IMG_3458600.jpg, thumbnail, 300) Image(IMG_3433600.jpg, thumbnail, 300) County Road 53 in the Capay Valley provides public access to Pierce Canyon Falls overlook. You need to be there shortly after a big rain to see much flow over the falls. The trail also offers diverse wildflowers in spring, panoramic views of Capay Valley and a nice trail along a tiny creek.

