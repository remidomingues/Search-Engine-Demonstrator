


Pioneer Park is a very nicely shaded park next to the Pioneer School, accessible via Swingle Drive.  Its a great place to take your wellbehaved dogs to let them run around offleash  lots of space, lots of shade  though keep in mind that there is no fence, so its not a good option for dogs that might run away.

Note that parts of the park are subject to advanced registration  call Community Services Department City of Davis Community Services Department at (530) 7575626 for information if youre planning your event there.


