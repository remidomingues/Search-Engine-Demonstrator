
Plant Man (Phytovir) is a tender seasonal shrub with unusual legs that stride across campus during Picnic Day 2012 rambunctious events. Growing six to seven feet tall, the shiny green foliage of this plant causes quite a spectacle. The Plant Man is very susceptible to heat and drought, and rarely travels for more than an hour at a time. Its wandering movement often attracts the attention of tourists.

Propagation of Plant Man has not proven to be successful.

