Image(PocketUnitransIcon240.png, right) Pocket Unitrans is a simple and intuitive mobile phones iPhone app that gives you realtime bus predictions for the Davis Unitrans bus lines.

Pocket Unitrans was conceptualized and programmed by Users/GiladGurantz Gilad Gurantz, features graphics by Users/BrianFong Brian Fong, and is available for free as an homage to their alma mater.

