Pomegranate Place is a hidden collection of 11 cottages and 2 houses located at 521 7th Street.  They fill a oneacre lot behind the visible house, and are accessible via a winding cement sidewalk which runs from the end of the driveway on 7th Street all the way through to 8th Street.

These cottages were all relocated to this land from F and 1st Streets, where they were a part of a little enclave called Asbill Court.  All of the cottages there were moved to make way for an office building, which is now a part of the Hallmark Inn


