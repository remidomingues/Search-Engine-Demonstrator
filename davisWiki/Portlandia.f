Portlandia is a lovingly mocking television show about Portland, Oregon and occasionally the rest of the west coast.  Starring Carrie Brownstein and Fred Armisen, it is a series of often surreal skits.


In the third episode of the third season, one of the recurring skits was a series of mock public service announcements by the Portland Milk Advisory Board.  They advocated various alternative milks such as zucchini milk and berry seed milk in a staccato back and forth between two people in lab coats standing amid beakers of colored water.  At one point, the boss is surprised that theres been actual research performed.


