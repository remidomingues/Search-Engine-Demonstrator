
The Positive Energy Kids Triathlon is put on by A Change of Pace.

This is a kids triathlon (ages 415 years) that typically takes place the Sunday prior to Memorial Day at Arroyo Park in Davis. The focus is on completion, not competition; distances are age specific and designed to be achievable for most athletic abilities. All kids receive a medal upon crossing the finish line.

Website: http://changeofpace.com/pekt.html 


