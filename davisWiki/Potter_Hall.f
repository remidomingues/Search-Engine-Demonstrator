

Potter Hall is one of three new Residence Halls dorm buildings in Tercero South Phase II Tercero South II. Located Next to Laben Hall and Kearney Hall, Potter mimics Laben and Kearney in design, and shares a courtyard with its new neighbor halls, Wall Hall and Campbell Hall.

