

PremierWest Bank was a banks bank that was headquartered in Medford, Oregon. The company was founded in 1999 and had branches in Northern California and Oregon. The bank was taken over by AmericanWest Bank in 2013.

This branch location was previously a Wachovia branch until Wells Fargo sold the branch to PremierWest Bank. Originally this location was a World Savings Bank branch until October 11, 2007.


