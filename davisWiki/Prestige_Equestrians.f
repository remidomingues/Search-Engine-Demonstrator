


Prestige Equestrians moved to Oakland, CA in 2011.  They offer http://prestigeequestrians.com/index_files/horsetraining_lessons.htm lessons from beginners to advanced experience levels.  They have created a unique program to suit all types of riders.  Whether you ride for pleasure or hope to conquer the show circuit, Prestige Horses Equestrians can help you.

They are located at Creekside Stables where they are accepting new clients into training and for boarding.

