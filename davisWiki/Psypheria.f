
Psypheria is a brutal death metal Music Scene band from Davis, CA.  They were formed in 1992, in the wake of the band Enthroned.  The band has since broken up, with guitarist/song writer John Oster going on to soley pursue classical guitar.  John Oster is also a teacher at Davis Senior High School, and has taught English there for many years.

The band was known for their experimental usage of keyboards and classical music within the context of brutal death metal, and have two full length albums, Gothic Disturbance (1998) and Embrace the Mutation (2002).


