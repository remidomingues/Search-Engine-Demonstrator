

 

Pub Live is the series of free shows sponsored and organized by Campus Unions Programs.  Its a great way to have fun on Friday afternoons while waiting for parties to start, as well as meet others who share the same interests as you (beer and music!). If you are in a Music Scene band and are interested in performing at Pub Live, please send program coordinators Anne Julia T. Baluyot or Andi Perry an email.

This Spring, Pub Live will also be hosting Thursday shows in light of the great Davis weather! Come and share a pitcher with your friends after class!

They were voted one of the Best entertainment events of 2006 in Northern California by MUSE, The California Aggies Arts and Entertainment Magazine.


