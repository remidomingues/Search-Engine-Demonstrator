Welcome to the Wiki Public Figure. Please also read the Welcome to the Wiki general welcome.

 Who is a public figure 

 1. Anyone who runs for an office. This includes a range from City Council or ASUCD
 1. Notable Davisites

 Helpful Information 

 1. People who arent public figures can create a user page that has the format Users/username.
 1. Public figures can have a user page, but in addition there is a public figure page that has the format First Last with any appropriate initials as well.
 1. You can edit your user page to your hearts content, but the public figure page is a public page.
 1. If you want to add something or respond to something placed on the public figure page you can use the /Talk page, or edit the page and be respectful of other editors.

