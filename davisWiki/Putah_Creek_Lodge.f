

Putah Creek Lodge is one of the facilities that can be rented out on Campus campus through Campus Unions. It was built in 1965.

It is located toward the west end of the Arboretum and is surrounded by Grounds Division well maintained lawns.  Putah Creek Lodge is also the location of the Summer Advising BBQ.

 In the Surrounding Area 

There is a large fire circle, a Barbecue Grills barbecue area, and large grassy expanses. The Foundation is just a short walk to the east from here.


