

The restroom is a portapotty in a concrete enclosure. The bike trail that borders the southern edge of the park can be accessed to the east side of the park, but you have to leave the park to the north to get to the path. There is a Coast Redwoods Coast Redwood Memorials dedicated to James Hetrick near the northern entry to the park.

The greenbelt trail to this park is a very pretty bike ride alongside the creek.  The park itself isnt very big, but there are two playground areas, a semilarge grassy field, BBQ grills and covered picnic tables.  A trip down the big red electric slide is highly recommended.

It appears that the citys open container ordinance does not apply to this park.

