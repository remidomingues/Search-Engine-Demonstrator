
Puzzle hunts are scavenger hunts  think Amazing Race, except you do a puzzle (called a clue) at each location.

http://playdash.org DASH (Different Area, Same Hunt) is a puzzle hunt held annually in Davis, starting in 2010. (See http://theaggie.org/article/2010/04/26/nationalpuzzlehuntbroughttodavisthissaturday Aggie article.) DASH has been covered by the http://wordplay.blogs.nytimes.com/2011/03/11/innamorato/ NYTimes and http://www.wired.com/magazine/2011/03/dashacrossamericapreviewingaprilscrosscountrypuzzlehunt Wired Magazine.

Other hunts in this area include http://www.bayareanightgame.com BANGs, which mostly take place in the Bay Area.


