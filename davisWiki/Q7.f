Should GATE eligibility be expanded, restricted, or remain the same?


  Bob Schelen: GATE is a successful program for Davis and I would not do anything to harm it. It seems to me eligibility really depends on how many children would benefit from the program, what those benefits are, as well as how the program benefits the schools, and more particularly the students as a whole. There are some ideas I have for pilot programs with interaction of GATE and other students, but they would not be mandated or forced upon anyone.
