

REI is a Cooperatives cooperative dedicated to outdoor activities outdoor recreational equipment. They carry equipment for hiking and backpacking, climbing, biking, and paddling as well as clothes, tents, tools, and food.  Their line of duffel bags is high quality and nearly indestructible.

Their Sacramento store (the nearest location) is right next to a Costco.  Other nearby locations are http://www.rei.com/stores/86 Folsom, http://www.rei.com/stores/74 Roseville, http://www.rei.com/stores/119 Stockton, and http://www.rei.com/stores/12 Berkeley.

