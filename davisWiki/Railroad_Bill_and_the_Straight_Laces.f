
Railroad Bill and the Straight Laces is a former Music Scene local band that consisted of Evan Daly (lead guitar and vocals), Colin Wagner (bass), and David Provost (drums).  All three were Davis Senior High School Davis High School students.  They mainly played original metal songs, written and arranged by Evan Daly. They developed a small, but loyal following. Their most recent gig was at Cafe Roma on E Street.

Official band website: http://www.purevolume.com/railroadbillthestraightlaces

After disbanding, two of the original members have moved on to another band.


Evan Daly is now a guitarist for the band, Glug GLUG.
