

While not technically located in Davis, Ramtown Martial Arts and Self Defense Karate has had a positive impact on the Davis community. Many of the instructors have taught classes at UC Davis and often give demonstrations to the public with members of the dojo.

Ramtown Karate offers a variety of classes for youth and adults, which include:
traditional Okinawan Shorin Ryu karate (known as Ryukyukan)
MMA fighting
Fightfit
The dojo also teaches grappling, self defense, point fighting, full contact fighting and other defense tactics.

Ramtown Karates website is a great place to get more indepth information about the dojo and programs offered.

