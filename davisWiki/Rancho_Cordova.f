Rancho Cordova, CA is a city in Sacramento County. It was recently incorporated in 2003.

The city is east of the City of Sacramento. You can get to Rancho Cordova by driving eastbound on Interstate 80, staying in the left lanes, and then taking Highway 50 towards Lake Tahoe. It takes about 35 minuites to get there, without traffic.




