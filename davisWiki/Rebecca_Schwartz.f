Winter 2008 Election

My name is Rebecca Schwartz, and I am a third year student majoring in Political Science and Spanish, running for ASUCD Senate with L.E.A.D. Currently, I am proud to be the chair of the ASUCD Outreach Assembly. I have served our community since I arrived at Davis, where I have been a member of the ASUCD Environmental Policy and Planning Commission. On campus, I am an active member of both the Jewish community, and the Jewishinterest sorority, SAEPi. I recently completed my term as the Panhellenic delegate. If elected to Senate, the following is a condensed list of the things I will accomplish:

First, I am going to provide cheaper readers for students by working with our ASUCD unit, Campus Copies. These readers will also be made available at the reserves desk at Shields Library. As a former intern for the Student Assistants to the Chancellor, I will use my experience working with the administration to encourage professors to use Campus Copies, rather than offcampus sites, to provide more convenient and affordable readers. Textbooks and readers are the two main sources of student fees, so more affordable readers will decrease our student fees.

Next, I will work to increase freshmen participation by creating a “Campus Club Information Guide” to be distributed at Summer Orientation. This would allow freshmen the opportunity to get involved in student clubs, and as a result, our overall campus involvement would increase. Just imagine if a quarter of the people who showed up to see Clinton also attended major campus functions. Our campus has shown us the interest is there, and now we have to meet that demand!
Join me in my efforts to accomplish these goals for students by voting me, Rebecca Schwartz, #1 for ASUCD Senate!


