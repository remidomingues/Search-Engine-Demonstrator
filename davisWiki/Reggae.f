Reggae is a popular form of Music scene music and can be heard at various venues in Davis.

KDVS broadcasts two weekly radio shows with a focus on reggae. Radio Wadada can be heard on Sundays between 1:00pm and 3:00pm, while Riddim Sounds is on the air every Wednesday night from 9:00pm to 10:00pm.

Reggae Mondays are happening at the Delta of Venus (on Mondays, lest you think theres a trick in the name) from 4:00pm to 9:00pm.  They feature DJ the Apostle Gabriel of the Mountain Lion Sound holding it down with special guests passing through.
