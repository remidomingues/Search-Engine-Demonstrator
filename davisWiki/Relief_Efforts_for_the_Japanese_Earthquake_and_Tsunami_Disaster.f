 Wednesday, March 23, 2011: Japanese Relief Effort: Potluck Dinner Fundraiser 

Time: 6:00 PM (US/Pacific)     Location: 10 College Park
In an expression of solidarity and compassion, please join International House Davis for a http://digital.davisenterprise.com/briefly/ihousehostsfundraiserforvictimsinjapan/ Japanese Relief Potluck Dinner fundraiser on Wednesday, March 23rd from 68 pm.

Please bring a dish to share (Japaneseinspired dishes are especially encouraged) and checks in any amount made out to International Red Cross to this potluck dinner. All donations will benefit the victims of the earthquake and tsunami disaster in Japan.

Come hungry and with open hearts to this community event where we will gather, reflect, and enjoy one anothers company while sharing thoughts, prayers, and hopes for our friends in Japan.

Thank you and please spread the word!

For more information, please contact: mailto(dir@internationalhousedavis.org)

