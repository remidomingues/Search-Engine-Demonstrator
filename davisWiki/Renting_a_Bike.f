
Tons of people ride bikes in this town, but if you do not want to be Buying a Bike because you are not here for long or some other reason a rental might be the way to go.

Step one in renting a bike is determining your needs. Are you planning on traveling to France to ride in their wiki:WikiPedia:Tour_de_France little race, or are you just tooling around town?

Places to rent

  The Bike Barn does short term and longer term bike rentals.
  B&L Bike Shop has rentals.
  Davis Bike Rental does short term and longer term rentals.
  Kens Bike & Ski has rentals.
  Hallmark Inn has complimentary rentals for guests at the hotel.  They require a deposit.

Also check out the Bicycle Shop Alternatives on the Bicycle Shops entry.

