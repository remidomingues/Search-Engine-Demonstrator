There are some Research Groups centered in Davis, wherein persons interested in a field come together and conduct studies to gain insight and improve the world. This also includes students, including grad students, who meet in order to discuss their individual research.

Some research groups in Davis:

  Center for Animal Disease Modeling and Surveillance
  Davis History Research Group
  Far Western Anthropological Research Group
  Human Geography Research Group
  Physical Geography Research Group
  UC Davis Dream Team

