Davis Research Park is a major business development in South Davis by Downtown.  It has UCD academic and records buildings.  It includes South Davis Chevron, Automobile Repair auto repair shops, Applebees, IHOP, several Hotels motels, Fast Food fast food restaurants,  Kaiser Permanente Kaiser Clinic, Moller International, and the Davis campus of Sacramento City College.

It was developed by Paul Moller in 1980 and according to the website of his company, Moller International, it is a 38acre industrialresearch complex in which the Moller International building is located.  He used the profits of the development for Research and Development of his flying car.

Research Park Drive runs through it.

Campus Safety Escort Service services Research Park.
