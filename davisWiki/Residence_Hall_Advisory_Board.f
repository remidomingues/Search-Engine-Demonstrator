


The Residence Hall Advisory Board (RHAB) meets weekly during Fall and Winter quarters to discuss & provide input on Student Housing policies, facilities and food services.  Students are not required to attend all meetings and first time attendants are welcome to drop into any meeting.
The goals of RHAB are:
1. To educate residence hall students about the various operational components of the UC Davis Student Housing program.
2. To give residence hall students the opportunity to collaborate with Housing administrators and serve on campus committees that makes decisions directly affecting housing residents.
3. To survey the residence hall population on current housingrelated concerns.
4. To allow RHAB members to develop leadership skills

Some great things have come out of this program like Late Night at the DC and two ply toilet paper!



