The movein recycling drive, or Resource Recovery Drive, takes place in several locations near the residence halls during movein weekend, so items such as cardboard and polystyrene (styrofoam) can be recycled. In 2010, the two day drive achieved a 61% recycling rate (by volume) and collected approximately 11,000 pounds of cardboard.

The 2011 event will occur on September 17 and 18.

For more information, including event maps, please visit http://www.housing.ucdavis.edu/sustainability/resource_recovery_drive_fall.asp


