The Resource Recovery Exchange gives the campus community the opportunity to pick up great donated items, such as clothes, linens, electronics, & school supplies.

Typical items for sale: clothes, linens, electronics, & school supplies

The items come from the freshman dorm move outs and are collected and sorted by the R4 Recycling Program during their annual Resource Recovery Drive. This exchange occurs Annual Events every year in October and it promotes the concept of REUSE!
