Richard Dick Livingston is a former Davis Senior High School Teachers in the Davis Joint Unified School District teacher and a local activism activist. In 2002, he received the Thong H. Huynh Awards Thong H. Huynh Lifetime Achievement Award.

He is one of the founders of the Yolo County ACLU. He has also performed in a selfnamed jazz band, The Dick Livingston Trio.

He was a longtime buddy of Jack Forbes since jr. high school days in Eagle Rock, California.

