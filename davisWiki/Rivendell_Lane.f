Rivendell Lane is one of the many Navigating Davis streets in Davis Village Homes neighborhood that are named after places or characters in J.R.R. Tolkiens books, The Hobbit and The Lord of the Rings.

Rivendell Lane branches off from Bucklebury Road, and Creekhollow Lane branches off from Rivendell Lane.

Rivendell is the Elven stronghold where Elrond and Arwen Evenstar live, and where the four hobbits take refuge from the Nazgul. It is situated in the foothills of the Misty Mountains between the rivers Mitheithel and Bruinen.
