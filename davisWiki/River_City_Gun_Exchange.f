
River City Gun Exchange is a guns firearms dealer (aka gun store) in Sacramento.  They have a good selection of guns, particularly handguns.  Their prices are decent, and they have lots of ammunition including army surplus rounds.  They also sell guns on consignment which can usually be had at a better price.

Exit highway 99South at Fruitridge Road (west ramp), and the store is on the left hand side just past 24th Street.
