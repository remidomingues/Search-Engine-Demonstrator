
Robert Arneson Park is a five acre parks city park located in Wildhorse.

The parks namesake, Robert Arneson was a Twentieth Century American artist and one of the seminal figures in West Coast ceramic funk art.  He taught at UCD throughout his career and was famous for his whimsical sculptures.  The Eggheads were created by Arneson. Originally named Frank Lloyd Wright Park. It was changed to Robert Arneson Park in 2001. It appears that the citys open container ordinance does not apply to this park.

