You may have noticed a Davis Houses house in East Davis that had a giant KDVS billboard in the back yard. You dont need to know why the billboard is here and not along I80, but let it stand as a marker of rad live music.  (The billboard was relocated to the Goondocks on Lehigh during Elisas goingaway party!  We should construct a giant robot/rocket to replace it. Now that the Goondocks has retired, we did steal it back. I think its hanging out at KDVS newest house show venue, Babelyon. But we should still build a giant robot rocket anyways!). You may have thought the house was dead! But the Davis house show music Gods have shined upon us at the last minute, and we will still be open for another year! So come on by and enjoy some music like always.

The most current listing of Robot Rocket shows is the https://www.facebook.com/groups/4697986234/?frefts/ KDVS Events Facebook page.

Shows here tend to be on the mellower, poppier end of the spectrum, partly because of the noise, but mostly because of tradition. We book many different kinds of shows: folk to rock to electronic to experimental, to even some punk (nothing too housethrashing though). Shows are almost always free with donations encouraged. Serpico may not let you pet him if you stiff the bands, however. Potluck baked goods and fruits are always welcome.

(Formerly known as the Rocket Tones Residence, and perhaps even more formerly known as a show house in the 80s according to Mick Mucus.)

For booking info, contact Chandler Lavin @ chandler.lavin@facebook.com

Check out this cool write up in L.A. Weekly that the RRR got about the Fidlar show! Read it (http://blogs.laweekly.com/westcoastsound/2013/01/fidlars_blitzkrieg_run_through.php/ here), then consider the fact that Robot Rocket had to go dormant for months after the ensuing noise violation.  Hmmmm...

Elisa wrote an article about http://www.elisahough.com/words/rrr.html the origins of the Robot Rocket Residence for the summer 2012 issue of KDViations.

