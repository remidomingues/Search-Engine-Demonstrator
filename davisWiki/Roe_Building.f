
The http://www.roebuilding.com/ Roe Building is located at the southwest corner of the intersection of 5th Street and G Street. It was built by the same developers who built The Lofts, Aggie Village and Crepeville. It has 8 residential units above a retail first floor.

In July of 2008 Barista Brew announced that they would be setting up in the south east street level section of the building.

 Occupants 
  Avant Garde Studio
  Barista Brew Cafe
  European Wax Center

