wiki:WikiPedia:Roseville,_California Roseville is located northeast of Sacramento along Interstate 80. The http://www.roseville.ca.us/ City of Roseville is a shopping destination for many Davisites.

If you like trains, Roseville has a large switchyard.  Its located across from the historic downtown, a very pleasant place to spend the day.  If you enjoy museums or technology, you might be interested in the http://www.rosevilletelephonemuseum.org Roseville Telephone Museum.

