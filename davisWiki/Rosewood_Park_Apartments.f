

Rosewood Park Apartments is a rental housing community developed and managed by Community Housing Opportunities Corporation CHOC for very low, low and moderate income households. Rosewood Park also provides a resident services program which provides residents with professional staff to assist them in enhancing the quality of life within their residential communities and achieving individual personal goals.

Rosewood Park is a 24unit family complex that was part of a land dedication in 1989. Rosewood is comprised of one, two and three bedroom units, a mix of both townhouses and flats. The site has two central laundry rooms, abundant landscaping and open space, which includes a tot lot.

