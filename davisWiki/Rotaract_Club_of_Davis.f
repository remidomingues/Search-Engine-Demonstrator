


Rotaract is an international program for adults ages 18 to 30 who want to made a difference.  It was founded in 1968 by Rotary International, an association of clubs made up of serviceminded business and professional leaders.

The Rotaract Club of Davis was founded in 1999 as a community based club sponsored by the Rotary Club of Davis Davis Rotary Club.  Davis is located in Northern California 11 miles west of Sacramento and is home to the University of California at Davis.  We hold meetings twice a month, featuring speakers, discussions, and service projects.

