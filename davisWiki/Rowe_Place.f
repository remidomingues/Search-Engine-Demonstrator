  The Klassic Kanine
  The Door Shop
  Rowe Place Shops:
   Midtown Animal Clinic
   Bernards Tire Brake & Alignment

 History 


This street was named after the Rowe family, specifically Jesse Gray Rowe, who came to Davis in 1875.  See also Rowe & Wire.
