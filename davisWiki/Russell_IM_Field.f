

The Russell Intramural Fields Intramural Sports Field is one of the larger fields (if not the largest) on campus, but has recently (2005) shrunk with the construction of the Marya Welch Tennis Center. Because of its large size, the field is used for a lot of special events, such as Davis Ultimate Invitational ultimate frisbee invites. The field is also a shortcut for people walking from campus to Oak Avenue. The field features a tall, narrow concrete wall for lacrosse players to practice throwing/catching.

This is also the location for the Davis Boffer Club.
