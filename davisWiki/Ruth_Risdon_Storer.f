
Dr. Ruth Charlotte Risdon Storer, born 25 February 1888 to Herbert T. and Ruth (Newhall) Risdon, was the first female medical services physician on the UCD campus and the first female pediatrician to practice in Yolo County. She was known for maintaining beautiful gardens and being dedicated to the Friends of the UC Davis Arboretum.

She was married to Tracy Irwin Storer for whom Storer Hall is named and died 13 September 1986.
