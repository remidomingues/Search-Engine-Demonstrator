
The Sacramento Area Council of Governments a.k.a. SACOG, is a key player in Davis Politics local politics and the City of Davis is a member. The counties of El Dorado County, Placer County, Sacramento County, Sutter County Sutter, Yolo County and Yuba County are all members, plus 22 cities contained within those counties.

The focus of SACOG is on region issues, with a focus on transportation.  Long range transportation planning, bicycling routes, clean air and the distribution of affordable housing over the greater area are some of the issues they work on.



