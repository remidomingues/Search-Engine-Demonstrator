



The Sacramento City College Davis Center moved in January 2012 to the UC Davis West Village for Spring Semester 2012.  Classes begin Saturday January 14.  According to a UC Davis news service article, the center will be a permanent home for Sacramento City College, which has offered classes in leased space at 1909 Galileo Court in Research Park in the City of Davis for more than a decade. FootNote(http://www.news.ucdavis.edu/search/news_detail.lasso?id9533) It seems likely that Sac City College classes taught at UCD such as the Workload classes for Freshmen will be taught here.



