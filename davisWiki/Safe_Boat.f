Safe Boat was operated during Houseboats 2008, 2009, and 2010 and planned for 2011.  Support and donations were provided by ASUCD. Medical Care and EMTs were provided by Onsite Medical Service.

ASUCD had been trying to organize this since 2005, but the Universitys policy of risk management put fear of lawsuits ahead of irresponsible bros.

