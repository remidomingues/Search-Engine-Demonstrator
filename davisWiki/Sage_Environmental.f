



Fritsch Environmental was a professional services firm, partnering with government, commercial, and private clients to deliver environmental consulting services. They helped clients assess the environmental impacts of a wide variety of development, restoration, and conservation projects.

Services included: CEQA/NEPA compliance (401/404 CWA, NWP 27, DFG 1600 permits), mitigation planning, construction monitoring, ecosystem and habitat restoration, and habitat management plans.


