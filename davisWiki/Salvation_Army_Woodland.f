



The Salvation Army is tucked away behind the historic Hotel Woodland.  To find it, make a right off of Main Street at the cross street Hotel Woodland is located at.  This Thrift Stores thrift store has heavily used clothing in poor condition.  Often good videos can be found and a few good hiphop and punk records have actually shown up in the past and they have daily 1/2 off sales for specific items.  If pressed for time, its best to skip this thrift store.
