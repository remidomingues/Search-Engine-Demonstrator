



SAME LoVE is a closed environment for those Middle East / South Asia Studies MESA students who identify as Gay Community LGBTQIA community and/or questioning. Providing a safe space to talk and gain support while also addressing important questions about our role at UCDavis.

The meetings are closed so that identities of closeted individuals are maintained and protected. For more information about the meetings, or any questions at all, please email the moderator.
