The park was named after Sandy Motley, who served on the Davis City Council (197684), including a stint as mayor (198082). She also was one of the founders of the Dinner at the Dump. She died of a pulmonary edema while scuba diving off the coast of Belize in January 1989.


 

The park is adjacent to Nugget Fields. It appears that the citys open container ordinance does not apply to this park.

