Santa Paula Park is a legendary wiffleball field in the South Davis neighborhood of Rancho Macero.  It is named after one of the two streets that form its boundaries, Santa Paula Way.  The fields home run boundary is formed by the curved sidewalk that results from the meeting of Santa Paula Way with San Ramon Drive.

It was the site of numerous games of wiffleball and Home Run Derby involving Steve Walter, Users/AdamFlowers Adam Flowers, Tim Walter, Joe Walter, Matt Flaherty and many others.

