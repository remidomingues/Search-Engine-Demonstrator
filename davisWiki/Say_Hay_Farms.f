

Say Hay Farms is a 20acre farms farm in Yolo County, California, specializing in raising certifiedorganic vegetables, melons, and eggs in an ecologically sustainability sustainable, integrated manner.

They operate a local Community Supported Agriculture program comprised of products raised solely on their farm, including options for pastured eggs and preserve shares.

The entire farm is certified organic by CCOF.


