Oscar W Schalm Hall is a fairly old building located on campus that is part of the School of Medicine.  Formerly known as Med Science 1E, the square shaped building has entrances, classrooms, and laboratories from all four sides.  This little building is nestled in the heart of the Vet/Med complex: located directly North of the new Gladys Valley Hall, right across from Scrubs, the Health Sciences Library, the Health Sciences Bookstore and Tupper Hall.  The Faithful Partner memorial is just in front of the building.


