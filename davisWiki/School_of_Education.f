
The UC Davis School of Education has been offering highly reputable Education programs for 85 years.

Formerly located at 2060 Academic Surge, until AOB IV was vacated when the Graduate School of Management got a new building.

The programs offered include a Ph.D, Ed.D. and M.A. in Education, as well as a collaborative credential program with CSUS, and a unique combined Masters/Credential program for secondary schools.
