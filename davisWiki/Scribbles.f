Scribbles is a comic that runs in The California Aggie.

Its about the adventures of a talking cow with a plunger on its head and its talking udders, which are not actually connected to the cow, and is apparently named utters.

 Reviews  

Very seldom does the comic make sense and it is never funny. If you had a nickel for the amount of Scribbles strips printed that contained one or more panels filled only with text, often referencing how the author didnt feel like drawing much that day, you would probably have at least $.45.
