

Seele was the undergraduates undergrad literary media magazine at UC Davis. It was an annual publication of poetry and prose by UC Davis students. It gathered submissions in the fall, put them together in magazine form in the winter, and distributed it in the spring, usually attempting to have it out by Picnic Day. It started in 1997 and eventually replaced the local magazine A Light Left On.

The word seele is German for soul.  The deadline was early January and could be dropped off at 177 Voorhies Hall, Lynda Joness office.

The magazine was funded by SPAC.
