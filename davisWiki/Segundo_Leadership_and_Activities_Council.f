
Segundo Leadership and Activities Council, referred to as SLAC (pronounced slack), is a great way to get involved in leadership and activities (what a surprise!) if you live in the Segundo area.  Each quarter, the SLACers put on several activities, including arts and crafts, Dormal, Homecoming Game Prep, Tie Dye, etc.

SLACers who help out at events may earn a SLAC cow, which is highly coveted.

Meetings are on Mondays at 7:00 PM in the Thompson Hall Thompson Lounge



