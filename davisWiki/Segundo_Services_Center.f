
The new Segundo Services Center houses an Area Service Desk, the Segundo Academic Advising Centers Academic Advising Center, a Learning Resource Center, The Junction, and a Laundromat Laundry.

It was opened in Fall 2011 and replaced the Old Segundo Services Center which was the building that housed the original Segundo Dining commons, the old Segundo Service Desk, the original Junction, and the old Academic Advising Center.

During construction, the Segundo high rise dorms also received upgrades. They closed for about one year.  Regan underwent some construction, but is not in the area of the SSC.

The new Segundo Services Center was built on land that used to be parking lot 24 and part of the old Segundo Services Center. This allowed more lawn area after the new building was built.



