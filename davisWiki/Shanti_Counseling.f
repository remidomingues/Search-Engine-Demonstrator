

Shanti Counseling, formerly Thriving Families Alliance, is a collective group of Counselors and Therapists professionals committed to helping families thrive through a variety of services for the whole family.  The group focuses on social aspects of family life and self development, specifically having to do with effective parenting, and developing a variety of youth social skills and coping mechanisms.  They offer services through a variety of venues, including private sessions, workshops, support groups and classes.

Classes/Workshops for Children:
  Coping with Divorce divorce / divorce support group
  Choices a mother/daughter class, offered through the City of Davis
  Developing social skills
  Developing high self esteem
  Character education

Private Sessions:
  Social skill development packages (for children)
  Team parenting plans
  Mediation

 Davis offers a wide variety of Counseling and Psychological Services.


