
A Shell gas station is located in East Davis at the intersection of Mace Blvd. and Interstate 80. It is right next to the Valero gas station. Usually, the price per gallon is equal to the price at Valero. The minimart here is a lot smaller than the one at Valero.  It used to be a Spirit and still retains the same owners, simply with a different sign out front and new brand of gas in the pumps.

There are other Shells (and of course, there are the Nine Shells of Baator)... this just lacks a pithy common name.  Check out the University Shell and Davis Shell as well.

