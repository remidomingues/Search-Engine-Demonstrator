Sheryl Freeman (now Sheryl Schaffner, postdivorce) served on the City Council from 6/20/1998 to 4/20/2002.

She worked as an environmental attorney for the State of California for much of her professional career.  She recently left her job as Senior Staff Counsel at the State Water Resources Control Board to become the City Attorney for the City of Eureka.

She no longer lives in Davis.

