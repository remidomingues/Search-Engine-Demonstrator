
Shire Lane is one of the many Navigating Davis streets in Davis Village Homes neighborhood that are named after places or characters in J.R.R. Tolkiens books, The Hobbit and The Lord of the Rings.

Shire Lane branches off from Bucklebury Road.

The Shire  is the home of the race of the Hobbits. They live a simple life and care little for the events and news of the outside world. Hobbits enjoy a quiet rural lifestyle of farming, gardening and raising animals.
