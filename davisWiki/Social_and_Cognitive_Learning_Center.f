
The Social and Cognitive Learning Center uses a multidisciplinary approach to assess and provide services to help the social social, emotional, and cognitive needs of individuals.  They work with family members and other people in the individuals life as part of their approach.  They work with both adults and children, collaborating with schools.  They help people with autism spectrum disorders, sensory processing disorders, anxiety, depression, memory and executive functioning issues and people who have no formal diagnosis but have problems creating and maintaining relationships.


