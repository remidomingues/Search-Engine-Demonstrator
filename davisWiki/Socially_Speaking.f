


Socially Speaking is a now departed businesses departed business that was a service that provides developmental play groups, socialization groups, and speech therapy offered for children birth through teens.

These services benefited the social development of:
  Children who appear to have have issues with appropriate social behavior
  Children who need help developing interaction skills (may act out or withdraw in social situations)
  Children with sensory processing issues
  Children with Autism / Spectrum Disorders

Similar services are provided under the new business name of Social and Cognitive Learning Center at the same location.

