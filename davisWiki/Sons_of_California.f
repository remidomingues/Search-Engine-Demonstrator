One of the Aggie songs

 Sons of California 

{{{Were sons of California,
A loyal company,
All shout for California
While we strive for victory.

All sing the joyful chorus
As her banner we unfold,
Then hurrah for California
And for the Blue and Gold.}}}

In addition to Hail to California, this is another song of Brick Morse.

The http://www.calband.berkeley.edu Cal Band certainly still plays this song!  It is still strong as the second song in Cals pregame show at football games!  At the end of the first verse is typically also a spell out of C A L I F O R N I A.
