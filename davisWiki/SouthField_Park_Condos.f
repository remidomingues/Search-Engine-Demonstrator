

SouthField Park Condos  also known as Greene Terrace, are part of the CHOC Affordable Housing Purchase Program. Two Bedroom/Two Bath Condos and Three Bedroom/two Bath Condos are available for sale. Because they are among the best affordable housing options available in Davis there is a waiting list . Details and application are at the City of Davis website under Affordable Housing. Ironically, many people who live here are teachers. To learn more about rental housing in Davis, check out our Rental Housing Guide as well as our Apartments pages.


