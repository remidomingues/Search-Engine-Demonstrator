

Features include a grove of heritage Canary Island Pines, a nesting Swainsons Hawk, and a carpet of pine cones and needles.  The South Wildhorse Greenbelt preserves a remnant of the walnut orchard that once covered Wildhorse and is an important pocket of our agricultural heritage in the middle of suburbia.


