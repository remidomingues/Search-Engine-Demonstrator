
Annual Events Every year Spay Day USA Sacramento is an amazing collaborative effort involving the UC Davis School of Veterinary Medicine, the Sacramento City of Sacramento, the Sacramento County County of Sacramento, the http://www.sacanimal.org/ Sacramento Area Animal Coalition, the Association of Veterinarians for Animal Rights, the Sacramento Valley Veterinary Medical Association, Teaching Everyone Animals Matter (TEAM), United Animal Nations, the Sacramento SPCA, and the Yolo County SPCA to spay and neuter 1,000 dogs and cats in the Sacramento area.

Spay Day 2012 is February 23th, 2012. Veterinarians and their techs from all over Yolo, Sacramento, and Placer Counties donate their clinics and services to offer low cost spays and neuters to the pets of lowincome residents.  Families or individuals earning less than $35,000 per year are eligible to have up to two pets (cats and dogs only) spayed or neutered at participating veterinary clinics.  For $15 per cat and $20 per dog, each animal will receive over $250 worth of services. Each animal at Spay Day receives a spay or neuter surgery, core vaccinations, a microchip identification, and a dose of flea preventative.

SPAY DAY SACRAMENTO is the largest one day spay/neuter event in the country. Since 2000, more than 6,000 dogs and cats have been altered on Spay Day Sacramento, preventing the births of thousands of unwanted animals and reducing the burden on area shelters.


Appointments are required and must be made in advance. You can either call or fill out an (http://www.saacvoucher.org/saacvouchers/spayday.php online application). Eligible families should call (916) 808SPAY (7729) starting Saturday, January 19, 2013 for appointments, which fill up fast.  See the http://sacanimal.org/spay_day.html Spay Day Sacramento web page for more information. Residents of Sacramento and Yolo Counties with a household income of $35,000 a year or less are eligible. Proof of income will be required to receceive a Spay Day appointment.

