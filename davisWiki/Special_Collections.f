
Special Collections is located on the first floor of Shields Library. The department is home to rare books, faculty papers, and the university archives. Special Collections materials tend to be old, one of a kind, or hardtofind books and other items. They are not available for check out, but can be used in the reading room Monday through Friday from 10AM to 5PM.

Because the reading room is the only place that people can use Special Collections materials, it is not available as general study space.


