

As Winter in Davis winter begins to break apart the town sheds heavy clothing and the Town Flora local plants start to show off. Enjoy the spring as Summer in Davis summer will all too soon be here.

Spring is the biggest Allergy Season.

There are many happenings in Spring including Pow Wow, Picnic Day, Celebrate Davis, Whole Earth Festival, events at the Farmers Market, see Ducks ducklings at the Arboretum, among Annual Events many others. Its also time to attend the many Gardening workshops offered to the community.

