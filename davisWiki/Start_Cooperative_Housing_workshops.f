Kevin Wolf and Users/NicholasBarry Nicholas Barry (of N Street Cohousing) and the Davis Cooperative Community Network (DCCN) run workshops to help people create cooperative households. The demand for living in cooperative communities is much larger than the supply, so DCCN wants to help seed new cooperative households.

To find out about the next workshop, contact Users/NicholasBarry Nicholas Barry, or join the the http://groups.google.com/group/dccnetwork DCCN mailing list here.

See also the How to live in a cooperative page. (Add to it if you have suggestions or wisdom of your own!)

