This page is a part of our Wiki Bureaucracy, formed so that we can do things like open a bank account for Davis Wiki.


A statement of organization is something that says:  What the name of the organization is and who the directors of the organization are (titles are in ../Bylaws the bylaws).  This means this is essentially a list of who the directors are, as in Person A is X.

The Davis Wiki board of directors serves as the administrative end of the Davis Wiki organization rather than administrating the content of the DavisWiki.org website. Issues with content should be dealt with by using the Talk feature. The board may be contacted by emailing MailTo(board AT daviswiki DOT org).

During the Wiki Spot transition, the board basically transitioned into the Wiki Spot board, formally incorporating under that name.


