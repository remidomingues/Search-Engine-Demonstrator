
Stephen Peithman is a longtime Davis resident, founding member of the Davis Comic Opera Company, participant in many area musical groups, and recipient of the 2002 Harmony in Our Lives Award.  For many years Peithman hosted the weekly radio show, Musical Stages, on public radio station KXPR.

More recently he has hosted another weekly radio show, http://www.capradio.org/classical/connections Connections, also on public radio station KXPR.

Source:

Sykes, Bev; 2002 Oct. 2, Living in Harmony, Davis Enterprise, pg. B5.
