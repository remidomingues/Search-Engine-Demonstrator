


Stepping Stones is a youth grief support program of the  nonprofit organizations nonprofit Yolo Hospice. Stepping Stones is specifically designed to help children and teens cope with the death of a loved one. Stepping Stones offers grieving children and teens support and education, and allows them to direct their own healing process with the guidance of the Stepping Stones staff and the support of peers.

Bereavement specialists use a variety of interactive approaches including art, music, play, stories, writing, videos and education to guide children and teens through their grief journey. Specialists are also available to work with parents to help them as they support their children through the grief process.

