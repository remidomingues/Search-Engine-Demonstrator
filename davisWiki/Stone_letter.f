Feb. 6, 2006

Dear readers,

Over the last eight months, I have had the pleasure of serving as
the editor in chief of The California Aggie. It has been a job that
has brought me much fulfillment and there was hardly a day when I
was not happy to be working for such an esteemed organization.

In the past week, however, The Aggie has faced challenges because
of my leadership and I know that my presence is now a hindrance to
the papers success. Therefore, I regrettably resign my post as
editor in chief.

I am leaving the future of the paper to an able and talented staff
and I wish The Aggie success. I hope the readership continues to
find value in our remarkable organization.

Respectfully,

Daniel Stone
