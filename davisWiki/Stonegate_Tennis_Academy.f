
The Stonegate Tennis Academy provides an opportunity for people of all ages to experience the sport of tennis in personal and group settings. They offer private one on one lessons, clinics for individuals and small groups, camps throughout the year and tout one of Californias top ranked http://www.usta.com/ USTA junior teams. Their goal: to help you strive for excellence in every aspect of the sport, whether it be as an individual or incorporating you into one of their teams.


