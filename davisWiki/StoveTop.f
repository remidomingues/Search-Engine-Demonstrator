StoveTop is an open source instant stuffing for those times youre too lazy to make your own, whether on Thanksgiving or just for a nice dinner.  Rumor has it, its also an open source software program you can use to do the choice voting count for ASUCD Elections.  Its the only software out there that can do countback.  It runs on Apple Mac OS X.

