There are 162 miles of streets within the City of Davis and a few more on the UCD campus.  The streets of Davis are often populated with automobiles, bus buses, other motor vehicles, bikes, and pedestrians.  Then theres the outskirts.

The names of the main streets were all changed in the early 1920s when the town was still very small. Concrete curbs were introduced in the late 1930s as part of a series of WPA improvements.

