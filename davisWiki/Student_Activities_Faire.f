
The Activities Faire is an annual event that usually occurs during the second or third week of October.  It is the largest event at Davis during Fall Quarter and last year featured over 150 student organizations, sport clubs, ASUCD units, and departments on the quad.  The Activities Faire is operated by SPAC.

The 2007 Activities Faire will be on October 17 from 10 am  2 pm.  Any organization can participate if they fill out and turn in an Entry Packet to the SPAC office (457 MU) by TBA.

