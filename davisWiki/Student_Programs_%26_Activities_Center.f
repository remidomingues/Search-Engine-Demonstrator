
Effective for the 0910 academic year, The Student Programs and Activities Center (SPAC) has undergone a name change and is now known as the Center for Student Involvement.

The Student Programs and Activities Center (SPAC) is the University Departments campus department through which groups officially register as student organizations at UC Davis. The groups are comprised mostly of fulltime UC Davis grad students graduate and/or undergraduate students undergraduate students, though they need not be composed exclusively of UC Davis students.

The SPAC Club Finance Council is the organization that SPAC registered Student Organizations (i.e. student clubs, etc.) apply for ASUCD Student Funds. Eligibility criteria are on the SPAC website.

The SPAC also sponsors Culture Days and Student Recruitment and Retention Center. They also organize the Student Activities Faire.
