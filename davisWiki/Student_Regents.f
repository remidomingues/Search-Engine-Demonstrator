Student Regents rotate in on the UC Board of Regents via a twostage process. The first stage is nonvoting, and the second stage is voting. There is only one student regent (plus one student regentdesignate ). The UCSA Board of Directors, the Council of Student Body Presidents, and the Regents’ Special Committee to Select a Student Regent are all involved in the annual selection of the Student Regent. They participate in a process where three finalists are selected. The Regents may reject the list of finalists. When the finalist list is finalized, the Regents vote to select the student regent.

