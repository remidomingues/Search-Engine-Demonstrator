Study parties can be a very useful tool to help one actually break down and study. These are most useful when studying with other students from the same classes so information can be passed readily between those involved. Make sure good background music is playing (something like Bach usually works) so people do not go completely insane with the complete silence.

Sites that link you to others in the same classes  such as Facebook or DavisCoeds have made it even easier to have study parties with other students from your classes.

Not to be confused with Grad Students study pirates.
