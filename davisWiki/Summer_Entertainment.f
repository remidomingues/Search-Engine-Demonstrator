Many of the popular activities in Davis disappear during the summer, when the students leave and the citys population is much lower. Do not despair (too much). For you can still do any of the activities listed below:

  $5 Movie Tuesday $5.00 Movie Tuesdays are still in session! A.C.! woot!  Previously $4 Movie Tuesday, $4.25 Movie Tuesday, and then $4.75 Movie Tuesday
  Delta of Venus  stills has some shows here and there...not as many as when school is in session, but some shows beats no shows.
  Poetry Poetry Night at Bistro 33  still running during the summer. Usually a featured poet opens the night, followed by Open Mic time.

