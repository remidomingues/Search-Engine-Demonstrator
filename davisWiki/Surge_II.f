Home to ATS Mediaworks and Classroom Technology Services; their building is located on the south side of Hutchison Drive just west of The Silo and Surge IV. To the north you can see the Sciences Laboratory Building. The campus television studio is housed here.

There is a courtyard between it and Surge III that has some interesting looking outdoor lighting; they are shaped like wide and short semiopaque white cylinders. The courtyard also has some nifty trees.

The southern lobby (Mediaworks entrance) has a mirrored ceiling.

