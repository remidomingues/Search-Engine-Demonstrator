
Surge IV is actually a series of temporary buildings (TB 200  TB 203). They are located across the parking lot from the Silo and across the street from Hutchison Hall. The Engineering Deans Office (Scholars Center) and Vet Med Deans deans office are here.

Surge IV is recognizable from its very 70s style covered courtyard and wooden deck.  The cover is by no means waterproof, which makes it rather interesting when it rains since it has all sorts of random slippery deck locations and localized downpours.

