 The Second Annual Sustainability Fair 
presented by the Davis Food Coop


Saturday, July 26 2008, noon4pm
Davis Food Coop parking lot, 620 G Street, Downtown Davis

The event is free. The Coop will sell freshgrilled meats & meat alternatives outside. Of course there is plenty of food and drink for sale inside the Coop as well.
Anderson Valley will host a beer tasting with a cost of $4 for the slate of beers; proceeds will go to the Yolo Basin Foundation.

The Coop has about 20 participants confirmed (so far).
