
TROTR, or the Therapeutic riding and off track rehabilitation is a nonprofit organizations nonprofit organization on the outskirts of town off Poleline/102 just past the dump exit
The volunteerbased organization is dedicated to assisting retired racing horses learn new careers, as well as caring for abused or abandoned horses and finding them new, loving homes.

New Volunteer Orientation: Orientation is required before volunteering and is held on the first Saturday of every month from 8AM  12PM.

They run the petting zoo at Bobby Dazzlers Pumpkin Patch

