Tapas are essentially appetizers in Spanish cuisine, including both cold and hot foods.  They often include cheese or fruit platters, fried croquettes, grilled meats, and so forth.  Theyre well suited for groups of any size; a meal typically consists of 1.52 tapas per person (though this may vary by location), which are typically shared.

Sadly, Davis does not have any dedicated tapas restaurants.   Options in the area include:

  Ficelle in Winters
  Tapa the World in Midtown Sacramento
  Aioli Bodega Espanola in Sacramento (the Davis location closed)
