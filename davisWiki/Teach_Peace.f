

Teach Peace is a Davisbased peace Community Organizations organization with an http://www.teachpeace.com/opensource.htm open source model.

Their mission is, To reach peace, we teach peace. We invest our energy because we want to safeguard life, liberty, and the pursuit of happiness.  To help end and prevent future wars, we work to transform from a world based on greed, pain and cruelty to a world based on generosity, compassion and love. Our members hunger for a world with meaning, authenticity, and freedom of opportunity.

They have a list of projects on http://www.teachpeace.com their website under the programs tab. Additionally, if you would like updates, you can click the join button to join their mailing list.

Prior to January 2007, they were known as Freedom From War.

