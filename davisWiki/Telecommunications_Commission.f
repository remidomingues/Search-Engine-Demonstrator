

The City Council dissolved the Telecommunications Commission on Sept. 10, 2013.

The Telecommunications Commission was one of the City Commissions that advises the City Council. It had the following functions:
  Advises staff in developing letters of support or nonsupport of legislation affecting city telecommunication issues using already established criteria
  Facilitates communication projects and shared information between the city, the Davis community and community partners in a proactive, clear, effective and straightforward manner to improve telecommunication services.
  Advises staff on the community uses of the Institutional Network.
  Advises Council about the development of telecommunication agreements.
  Recommends ways to bridge the digital divide for all Davis residents.

