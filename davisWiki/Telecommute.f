Some links and info for people who telecommute from Davis to the Bay Area and other places.

If you need a Bay Area phone number and you use DSL you can get a http://www.vonage.com Vonage account. You can then get a phone number in the area code of your choice.

When tele just isnt good enough and you need to actually go to the Bay Area you do not have too many choices.

   Lots of people just drive. Leaving Davis at 5:30 puts you into the Fremont area sometime before 7.
   Amtrak is a nicer but slower way to get there. http://www.amtrakcapitols.com/

Other interesting ways to get there that Id love to try:

   Small airplane from the UC Davis Airport
   Boat from Sacramento to 3rd Avenue or other places on the Peninsula.
