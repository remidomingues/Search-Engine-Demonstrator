
Tercero B Building was the closest (Dorms dorm) building to the The Dairy campus Dairy Farm until its demolition in March 2012. If it was REALLY quiet in the building, you could have sometimes heard the cows mooing too. If it was sunny outside and parking lot 48  is empty, one could have easily viewed the Cows cows from the second/third floor balconies (that face the farm, not those of the other side of the building), and even through the window of the hallway on the first floor.

Compared to the other buildings in Tercero, B building always had a large number of triple rooms.

Up until Fall 2003, B  building used to house the Integrated Studies program at UC Davis, since then it has been moved to Miller Hall in Segundo, though several murals that advertise the program that were painted by former residents are still present in the building.  Below is a picture of a mural found on the first floor.





