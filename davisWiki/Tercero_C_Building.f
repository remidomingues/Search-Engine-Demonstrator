Tercero Residence Halls Tercero C Building was one of the buildings that borders Parking Lot 48A until its demolition in 2012. The first floor of C Building housed women only while the second and third floors are coed. It had two Resident Advisors RAs and about 70 residents.

This building didnt have a particular theme but there were some cartoon characters painted on the walls of the first floor and a piece of wall art on the third floor.


