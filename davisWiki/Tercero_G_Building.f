
Tercero G Building was home to the SubstanceFree program until its demolition in March 2012. It was also part of Pierce Hall. Because of the attached program, the building tended to be pretty quiet and mellow.

According to Sean Davis, G building used to be a On Campus Coops coop and was, ironically, anything but substancefree.

As of 2007, it was home to Gunit which originally referred to only the Coed Res Hall soccer team, but now includes most of the G building population.


