Tercero Hall Circle is a campus street that runs eastwest between Bioletti Way and Dairy Road.
On the south side the street is bordered by the Tercero Residence Halls Tercero Dormitories and on its north side are the Surge III Surge buildings.

The naming structure of the street seems odd considering there is an opening at either end of the road. Beckett Hall Circle ended in a parking lot that could be considered a circle if stretched enough, however Tercero Hall seems completely unjustified. Perhaps at some point in the past there was a traffic circle.

