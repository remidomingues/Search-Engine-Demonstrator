
Tern Inc. is a designer and manufacturer of 16/32bit x86 C/C++programmable embedded solutions. They make about 40 different models of SBC (Single Board Computer), using Am188ES, Am186ES, i386EX, and V25 microprocessors. Prices are $100$200 range. They include TTL I/O, HV I/O, EIA232, and ADC/DACs. Expansion boards add motion control, LCD interfaces, PCMCIA flash, and Ethernet capabilities.


