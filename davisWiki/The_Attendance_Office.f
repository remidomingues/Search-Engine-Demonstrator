The Attendance Office is a lil house on L Street that hosts some pretty serious events.

In 2009, the Attendance Office was referred to as the The Hospice of French Ruud Solace but was later retired due to trying too hard to be clever.

The houses current name comes from a sign from one of the occupants high school attendance offices that now hangs over the door to the backyard. Image(Picture 8.jpg, Ash Reiter and her band., right, thumbnail, 350)Image(Picture 4.jpg, right, 350, thumbnail, The Shants.)


