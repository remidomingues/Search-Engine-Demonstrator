The Avid Reader Book Club meets the fourth Monday of each month at 7:30 pm at The Avid Reader book store at 617 Second Street, downtown Davis.

This group of avid readers has been meeting for several years now. Join us for some great reads and lively discussion. We read everything from fiction to nonfiction and are always looking for interesting books on and off the bestseller lists. The Avid Reader bookstore in Davis will be providing the months book club picks at 20% off for anyone interested whether you are a member or not.

