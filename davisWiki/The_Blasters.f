The Blasters are an influential music band that began playing in the late 1970s in Downey, California. In 1982, they were subjected to a local bans ban following a crowds unruly behavior during a Davis performance.

The bands members were pardoned in 2011 by Mayor Joe Krovoza, who is reportedly a huge fan of the groups original guitarist, Dave Alvin.

