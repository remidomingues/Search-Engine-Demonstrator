The Boondock Saints Drinking Games Drinking Game is a drinking game based on the movie wiki:WikiPedia:The_Boondock_Saints The Boondock Saints. 

The game is very simple. Every time any character says fuck, take a drink. 

It is suggested that you do not drink a shot of hard alcohol every time a character says fuck because youll end up in the Sutter Davis Hospital emergency room.  Use a mixed drink or Brew your own beer beer instead.

Cheers!

