


The Buzz is a relatively new annual parties party event at UC Davis campus, beginning around 2005. Featuring free entertainment, games, food, and fun, its presented on the first Friday night of the academic year.  It generally features music scene live bands, such as indypop artists http://www.myspace.com/thecataracs the Cataracs and rapper http://www.myspace.com/shawnchrystopher Shawn Chrystopher (2010), http://www.myspace.com/unimuzik UNI (2009), http://www.myspace.com/naturalroots Natural Roots (2009), Double Funk Crunch (2008), or  Suburban Legends (2007). The free event also features inflatable games, performance competitions, carnival games, and information booths.

The Buzz is similar in concept to the nowdefunct Turbulence, which had attempted to be an oncampus concert and game night aimed mostly at freshmen, although its open to all UC Davis affiliates as well as community members. It began the same year that the Turbulence program had been canceled. The Buzz, however, is presented by ASUCD Entertainment Council, Campus Unions, Campus Recreation and Student Housing, and has been more successful with a turnout of more than 12,000 in 2009.

The 2011 Buzz will be held on Friday, September 23, from 6 p.m. until 10 p.m. on the East Quad and West Quad.

For more information and updates about the Buzz event, you can follow the http://www.facebook.com/ucdavis.thebuzz The Buzz on facebook




