The Essentials are:
 Reggie Balino (bass)
 Stacey Sheston (percussion)
 Linda Duval (guitar, vocals)
 Cathy Speck (vocals)

They play at many local festivals and events, such as Gay Pride Day and Davis Farmers Market, as well as private shows.

Cathy and Linda are also known as Duval Speck. Stacey Sheston used to play bass with Natalie Cortez & The Ultra Violets.

For upcoming shows and booking information, see their website at http://www.duvalspeck.com/essentials.

