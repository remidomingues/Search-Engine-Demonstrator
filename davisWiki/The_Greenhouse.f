The Greenhouse is a reincarnation of The Pink House, one of the Davis houses.  Its host, EliseKane, named it for its unattractive olive green color, and the intrigue of a place where town flora flowers and other plants are grown. The modest home doubled as a music scene music venue. As of Feb. 2010, it ceased to be a show house.

