
The Loneliness of Integrity is the title of one of Davis many pieces of town art. This rock statue is between the Davis Branch Library Yolo County Library and Veterans Memorial Building.  It was created by http://www.jayjayart.com/Nielson/Nielson_bio.html Jack Nielsen in 1986 as part of the Art in Public Places program.

