The Musical Drum Key (http://themusicaldrumkey.bandcamp.com/ Website) is an electro project guided by Josiah of the Future, who is also of the Music Scene musical groups mile signs and earthly.

TMDK has existed as a solo project, a full band, and back to a solo project with collaborations.

TMDK has played shows all over Sacramento, CA, with additional shows in Davis, CA & Reno, NV.

