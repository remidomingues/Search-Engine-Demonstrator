
The Pink House was a Davis Houses residential music venue in Davis with ties to KDVS. It is not really pink in color but more of a peachy salmon, though the individual who named the house insists that it is indeed pink in hue. The fall 06summer 09 residents of the house have jokingly titled the garage The Meth Lab, a reference to the possibility that there was once a meth lab in the neighborhood. According to a former resident, the interior of the house, now eggshell white, once had walls painted different colors. One bedroom was green, the living room had either purple or green, and the primary bathroom was dark green with artistic Chinese characters running in vertical lines a la The Matrix. No traces of these colors are left, save for some dark green in the bathroom that peeks through the current paint at the edges of the shower.

The house is no longer a music venue as of August 2009 because its pioneering music residents have vacated. However, shows that would have taken place here were hosted at a thennew venue on J Street, called The Greenhouse. The Greenhouse is also no longer a show venue as of Feb. 2010; the host moved out of town.

