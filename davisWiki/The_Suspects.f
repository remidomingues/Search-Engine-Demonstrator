The Suspects were a Davis band whose members included Steve Wynn, Kendra Smith, Russ Tolman and Gavin Blair (who would form True West).

Both Wynn and Kendra Smith were former KDVS DJs, when they recorded and released their first single, Its Up to You b/w Talking Loud, in 1979. Gary Saylin helped them record.

Smith later played bass in the Dream Syndicate, and sang in the band Opal, with David Roback until she left. Hope Sandoval became vocalist, and the band changed its name to Mazzy Star (they had a hit called Fade into You).

