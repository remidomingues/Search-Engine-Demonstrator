




The Tiki Hut is a combination Tiki and Pirate store that offers a selection of gift items.

Founded by a 87 graduate of UC Davis, they are (in their words), on a mission from Tiki... to sail our Pirate Ship into any tropical oasis possible. Escape from the boring, hot, typical day in the Valley and begin a quest into the exotic! We carry goods from around the world and loot from the finest cargo holds ever plundered. Pearls, silver and gold, finely crafted tiki mugs for boutique Rums and other fine elixirs grace the stock of the Tiki Hut in Davis. Music, art, smells, and tastes abound in the most fun new store this side of Tortuga/Yolo bypass.




