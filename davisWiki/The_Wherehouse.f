
The Wherehouse was a video rental store and music/movie retailer. They sold snacks, candy, popcorn, cds, VHS tapes, DVDs, stereos, VCRs, DVD players, posters, magazines, headphones, movie merchandise, and music related merchandise.

The store closed down during the 19992000 school year. It used to be a very popular place to rent movies and buy music since it was very close to the residence halls.

The building was later occupied by a Rubios. Rubios closed and Davis Imaging later took over the building.

Davis Imaging moved out and the building was demolished to build Trader Joes.

