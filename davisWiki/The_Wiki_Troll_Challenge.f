
Stated simply, the challenge is this: For every unproductive edit made and every reply to an unproductive edit made by another user, make at least that many productive edits.

For instance, on Monday April 24th this would be at least 25 edits (or, after I press save, 26).  Good places to find pages to edit youre not likely to find by staring at Recent Changes: Random page, Explore or your Bookmarks.  On the whole, most changes are productive.  By taking up the challenge directly you send a clear signal that unproductive, useless edits will not phase you or your editing habits.  You also help put more positive contributions on Recent Changes at a time when most users are focusing on Trolls trolling.






