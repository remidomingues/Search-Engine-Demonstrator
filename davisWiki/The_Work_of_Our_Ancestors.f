

This class will help you connect to your ancestors through learning and
understanding traditional witchcraft and shamanistic practices of the modern
and ancient world. We will introduce you to many traditions with the goal
of helping you find a style that resonates with you, either because it was
what your ancestors practiced, or because it is what makes the most sense
with your worldview and spiritual needs. This class will feature lectures,
discussion, rituals, and divination work.

