
According to the website for Thistledown Farm, they started out with a few chickens, and got hooked.  As of Spring 2011, their small family farms farm offers eggs and chicks for the following breeds: French Black Copper Marans, Orpingtons, Silkies, Ancona Ducks, DUccles, and Araucanas.  They are members of a number of breeding societies.

