

We are a husband and wife team who love to Photography photograph families in outdoor settings. We have a small studio in our home that is used for photographing infants and producing head shots for business related prints.

We have both worked as photographers for wiki:dixon:The Dixon Tribune and The Davis Enterprise, so we have backgrounds in photojournalism as well as portraiture.  Our focus is mainly on portraiture and weddings, but we are also available for commercial photography.

