Todd has worked at KDVS longer than most people. He is currently the head studio technician, but has in the past served as general manager and programming director. He hosts a sweet show called Hometown Atrocities every other Thursday evening from 6 to 8 p.m.

He founded and heads the organization Common Frequency, which works to jumpstart noncommercial lowpower, community and college radio stations around the country, in the same vein as KDVS.

Todd lives at the DAM House and enjoys a good hamburger.
