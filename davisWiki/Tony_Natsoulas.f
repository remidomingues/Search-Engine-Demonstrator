Tony Natsoulas, born 1959, is a local artist who creates large ceramic sculptures.  He grew up in Davis and acquired both his bachelors of arts degree and masters of fine arts from UC Davis.  Natsoulas works usually depict people, often engaging in whimsical activities.  The subjects physical characteristics are frequently exaggerated to inject a sense of humor or playfulness.

Several Natsoulas pieces are public fixtures located in prominent Davis locations. These include:
  Freaky Shoe Guy
  The Joggers
  Lamplighter Players

Natsoulas currently works out of a Sacramento studio.  According to Tony Natsoulas http://www.tonynatsoulas.com/ official website, he has no connection to John Natsoulas, the Davis art gallery owner who shares his surname.
