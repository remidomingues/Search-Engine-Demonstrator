Many Wiki pages have multiple links pointing at them, and its not always the same one.  Whether its for clarification or just using a common nickname, a redirect is the thing to use rather than making a duplicate page. Just create a page with the alternative name you want, and add only this single line:

Then just replace <Page Name> with the name of the page you want the redirect to point to!  Note that it doesnt need to be enclosed in quotes, or other wiki markup.

A good example is the more commonly known The Homeless Professor Homeless Professor page.  Its only a redirect to his real (and relatively unknown) name, Arthur Shapiro using  {{{#redirect Arthur Shapiro}}}.
