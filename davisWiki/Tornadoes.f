While not a terribly common Weather weather condition, funnel clouds (which dont hit the ground) do occur in the region, occasionally becoming fullfledged tornadoes (which do hit the ground).

The vast majority of tornadoes around here are rated as F0 on the wiki:WikiPedia:Fujita_scale Fujita Scale with the rare occasion they might make it to F1.  The Central Valley doesnt get severe enough storms to get anything much bigger  no tornadoes above F2 have ever been recorded in California. The Fujita Scale has been replaced by the Enhanced Fujita Scale which began operational use in 2007. Los Angeles County is the tornado capital of California with the most tornadoes. Orange County also has had a high number of tornadoes. The last F2 tornado to hit Sacramento County was in 1972, and the one before that was in 1921.

Users/BenjaminMiller Benjamin Miller recalls seeing a tornado touch down from the Student Farm House in the early nineties.  A photo and video of a funnel cloud was captured in East Davis by Terry Nathan and Kyaw Tha Paw U, both faculty in the Atmospheric Science Program.  Both are available http://atm.ucdavis.edu/weather/archives.php on the programs website.

 Recent Sightings 
  June 1, 2011  a funnel cloud was sighted in Davis according to http://www.sacbee.com/2011/06/01/3670129/hailfunnelcloudindavisas.html this article in the Sacramento Bee, while several locals reported hail.
  Feb. 28th, 2006  A tornado touched down in eastern Fairfield.
  Apr. 8, 2005  Four tornados sighted in Sacramento region:   one 9 miles north Of West Sacramento, one near Arco Arena, at the Sacramento Airport, one at Truxel and I80.

  Feb. 21, 2005  Severe winds and at least one reported tornado hit West Sacramento and Natomas. The twister itself knocked over vehicles and tore gutters and roof tiles off buildings but did not cause major damage. No injuries were reported. http://www.livejournal.com/community/ucdavis/1898503.html Some photos of these twisters.

  May 20, 2002  A tornado touched town in Knights Landing (north of Woodland). No damage was reported.

