Due to a variety of circumstances in Davis, both the town and campus feature a staggering array of plants, both native and exotic.  Many years ago, Davis made national news when the media picked up the story that Davis Julie Partansky mayor had decided to plant fruit trees in all the empty lots and unbuilt areas to provide food for the homeless. Both UC Davis and the City of Davis recognize the many ways plants enrich our lives by supporting programs and concepts such as the http://www.city.davis.ca.us/pcs/trees/ Street Tree Program, Arboretum, http://grounds.ucdavis.edu/ planting dedications, and edible landscaping.

Some of the species planted in Davis are invasive species and could become pests in the natural environment (yes, Davis provides important habitat value for many native species). Experts recommend using them carefully, or not at all.

