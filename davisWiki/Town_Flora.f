 Poisonous Plants 

Many plants have compounds in them that are skin irritants, or are harmful if ingested. Yet other plants cause allergies to some people. Below are some of the most notable chemically irritating plants found in Davis. Some are native, some are not. Mind you, lots of commonly eaten plants have toxic partsmany of the plants related to cherries have poisons in their pits. The list below is certainly not a complete one. If you think you are suffering from some kind of plant poisoning, contact Poison Control (18008764766). Also refer to this really helpful http://envhort.ucdavis.edu/ce/king/PoisPlant/ToxSCI.htm plant toxicity guide.

If you are a gardener that has a yard filled with interesting horticultural introductions, you might have a whole apothecary in your back yard. If you have children, you ought to crossreference the names of the plants in your garden against a good list of toxic plants. The Western Garden book may provide that information.

If you have pets, you need to be aware of which plants in your garden or home are toxic to pets.  UC Davis Center for Companion Animal Health has a helpful web page http://www.vetmed.ucdavis.edu/ccah/health_information/plants_pets.cfm.

  Angels trumpet Angels trumpet (Brugmansia sp.)a beautiful horticultural tree, highly toxic.
  Chinaberry (Melia azedarach)berries and other parts of the tree are poisonous to humans and probably dogs as well, although not to birds.
  Delphinium (Delphinium spp.)also called larkspur, highly toxic.
  English Ivy (Hedera helix, Hedera canariensis)absolutely harmless to most, but some people can develop limbbloating allergic reactions on contact.
  Foxglove (Digitalis purpurea)a beautiful horticultural plant, highly toxic.
  Lilies (Lilium)all species of lilies are toxic, including Easter lily, Daylily, and Lily of the Valley (Convallaria majalis).  Cats are especially sensitive and sadly quite a few cats die each year from ingesting household lilies.
  Lupines (Lupinus spp.)the seeds, which look like edible beans, are attractive to children.
  Morning glory (Ipomoea spp.)do not eat this plants seeds.
  Oleander (Nerium oleander)all parts of the plant are toxic. Do not use its branches for roasting marshmallows or http://www.snopes.com/horrors/poison/oleander.asp hot dogs! The white latex can cause skin irritation.
  Poison hemlock (Conium maculatum)the plant that killed Socrates. Ingesting this very common weed can be deadly in large amounts.
  Poison oak (Toxicodendron diversilobum)depending upon the person, the active ingredient (urushiol), ranges from harmless to dangerous. Sensitivity to poison oak is an allergy.
  Sago palm (Cycas revoluta)all parts of the plant are poisonous and ingestion can be fatal. Dont wait for symptoms to appear. If you think a child or pet has eaten the seeds or leaves, call your doctor or veterinarian right away.
  Tree of Heaven (Ailanthus altissima)an invasive species that is mildly toxic (but that smell! Who would eat it?), and can cause skin rashes.
  Tree tobacco (Nicotiana glauca)occasionally encountered as a roadside weed.
  Yarrow (Achillea millefolium)mildly toxic, also it can cause skin rashes.

