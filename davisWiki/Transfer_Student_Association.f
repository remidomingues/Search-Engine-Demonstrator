The Transfer Student Association is an undergraduate club at UC Davis and provides a social and informational network for transfer students. They coordinate various programs throughout the year designed to ease the transition for new transfer students to the University. Membership is open to all students of U.C. Davis.

TSA was founded by Andrew Wong in Fall 2003.

Numerous TSA members are known to become very involved on campus; Andrew Wong, Naomi Amaha, Billy Durham, Ivan Carrillo and Eric Friedman.

