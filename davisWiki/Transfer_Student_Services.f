
Transfer Student Services offers peer academic advising and periodic events to help transfer students network and succeed.  The unit offers a good variety of information tailored specifically to transfer students.

Generally the first Tuesday of every Fall quarter is the Transfer Meet & Greet.  In addition, every first Tuesday of an academic month they hold Transfer Tuesday.  These days can either be workshops or social events.

According to their website, they claim to have a Facebook group called Transfer Students Unite, but it does not seem available.

