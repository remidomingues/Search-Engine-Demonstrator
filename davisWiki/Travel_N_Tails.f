


Travel N Tails Departed Businesses closed in August 2005, and has been replaced by Just KIDding. Previously, this spot held Accents, with the same phone number.

This was a really fun store for an animal lover or anybody who needs to buy a gift for an animal lover. With an excellent selection of Dogs dog related gifts, you could get everything from socks to statues with any given breed of dog. Plenty of dog and cat beds, bowls and other pet things were available.

In a slightly odd twist, they also had a selection of travel related items like luggage tags, travel wallets, small bags, etc.


