Because South Davis is separated from the rest of Davis, the citizens there tend to be unified in the knowledge that everyone thinks they live in some mystical hinterland of rich golfers and farmers.  This was even more pronounced before the overpass at Pole Line Road and the bike overpass connecting to Mace Ranch and East Davis in the mid to late 1990s.  Since there has always been this separate identity, many infamous and notable incidents involving South Davisites have passed from the realm of memory to that of legend.  Please add your own stories!

