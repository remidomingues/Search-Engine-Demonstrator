Tubing is a lot of fun. It involves two cars, inflatable float tubes, & PFDs (life jackets) for all members of your party.  Float tubes can be purchased from Big 5 Sporting Goods Big 5 or you can get innertubes from a truck tire store. The American River in Sacramento is a good place to go, & so is Cache Creek.

