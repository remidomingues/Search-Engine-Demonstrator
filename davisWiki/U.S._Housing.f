
US Housing is an online database of rental housing options.  Listings include apartments, homes, duplexes, condos, sublets, rooms for rent, and roommates.  In Davis they also publish DavisHousing: The Print Edition in February, June and October.  Copies can be found at various distribution points on campus and around Davis.  It has been described as follows: The magazine is a thick listing of housing helpful for those looking for a place to stay in Davis that litters campus during springtime.

Their http://www.davishousing.com website is an option besides Craigslist and Housing Guide other places for apartment/home hunters.

US Housing shares office space with Aria Communications and SoliSys.

