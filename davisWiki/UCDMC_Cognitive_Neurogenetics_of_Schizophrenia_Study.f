

If you are between the ages of 12 and 54, and have an established diagnosis of schizophrenia, schizoaffective disorder, or bipolar disorder, you may be eligible to participate in studies.

If you have a family member who is being treated for schizophrenia or schizoaffective disorder, you and your family member may be eligible to participate in the Cognitive Neurogenetics of Schizophrenia study.

Studies involve detailed clinical assessments (interview); computer based cognitive testing, MRI scanning, ERP, and in some cases a blood draw for genetic studies. Research subjects are reimbursed for their time and travel expenses.

Your participation will help the scientific community , learn  more about the neural and genetic basis of schizophrenia and how we can develop ways to better treat this condition.

If you are free from mental illness, they are also interested in working with you. They have many different studies and pay pretty well. The amount depends on the study. You should give them a call if you are interested. They are located in the UCDMC.


