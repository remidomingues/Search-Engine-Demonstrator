
The Cashiers Office is the University department which takes your Undergraduate Student Fees money.  They accept checks, cash, and Discover card (but no other credit/debit cards) to pay fees and other bills.  If the University actually owes you money, your best bet is to head over to Student Accounting.

Additionally, they do some other random stuff.  Theyll cash checks of up to $100 for a $.50 fee for students.  Also, they sell Davis Berkeley Shuttle and UCDMC Bus Line tickets.

There is a self service computer where you can use your Reg Card to check if you owe any money.

