


Intramural Sports (or IM sports) at UC Davis are sports that anyone can play.  Literally any UCD affiliate (students, faculty, staff, Cal Aggie Alumni Association alumni, and even spouses/domestic partners of students/faculty/staff) can throw together a team and play, and its a lot more lax than, well, real UCD Athletics because you only compete against other intramural teams (no outsideUCD teams). There are several intramural fields on campus.

Students just have to join a team or show up to the first game with their Aggie card.  Nonstudent affiliates are required to purchase an IM Card at the ARC.  The purchase generally requires some sort of documentation proving your affiliation with the school (e.g. a transcript for alumni).  For the 20102011 school year, IM cards cost $60 if purchased in the Fall, $50 in the Winter, and $40 in the Spring.  They are valid until the end of the school year (June 30).  Note that if your IM sport takes place inside the ARC, youll need an ARC membership to get in.  If you dont have an ARC membership, you can purchase daily passes.


