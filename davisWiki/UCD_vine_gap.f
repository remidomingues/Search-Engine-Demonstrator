
Located on the South East side of Roessler hall the UCD vine gap is well know in Skating circles. While its illegal to skate on campus that doesnt stop lots of people. UCD also has a few 715 stairs scattered around campus.

Its a pretty massive gap, you can see pro skaters jump it in the bonus footage of Tony Hawks Pro Skater II, or alternatively its the first jump in this video here: https://www.youtube.com/watch?vJoPv_Tyy7x4

