

CourseBooks.com was a locally owned websites locally oriented website created by UC Davis students to serve the Davis community by helping members save money on textbooks.

The site serves as a textbook price comparison engine by comparing prices from theUCD Bookstore against over 20 online retailers including Amazon.com, BookRenter.com, Half.com, and Chegg.com to help students save money on rising textbook prices.

