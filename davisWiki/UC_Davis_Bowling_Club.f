

The UC Davis Bowling Club is a student organizations student club on campus at UC Davis. They practice in the MU Games Area.
The mens club sport team is led by captain Jesse Anzelc and finished their 20112012 season ranked 35th in the nation, out of about 150 teams.  The team earned two collegiate titles this season, the Camarillo Open (November 2011) and the Silicon Valley Classic (February 2012).
