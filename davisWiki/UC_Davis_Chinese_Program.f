
The department of East Asian Languages and Cultures, part of the College of Letters and Science, offers both a majors major and minors minor program in Chinese. Courses include language (Mandarin and Classical Chinese), film, and literature. Anyone interested in enrolling in a CHN course must first take a placement exam. More details can be found at the department website.

