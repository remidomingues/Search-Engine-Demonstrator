The UC Davis University of California, Davis has four colleges for students to choose from. Within each College are the available majors, minors and graduate programs.

The College of Biological Sciences was jointly administered by the College of Ag and the College of Letters and Science prior to its transition from a division to a college.

The four colleges offer over 150 majors and over 80 Graduate Groups graduate programs.

  College of Agricultural and Environmental Sciences a.k.a. College of Ag
  College of Biological Sciences
  College of Engineering
  College of Letters and Science

See also University Departments

The analog to the colleges for academic graduate students is the Division of Graduate Studies; others students are part of the professional schools.

