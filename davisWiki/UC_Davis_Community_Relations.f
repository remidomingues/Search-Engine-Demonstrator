
Community Relations works closely with communities throughout the region on issues that affect the quality of life. These issues range from economic development and K12 education to diversity and leadership development.
Community Relations:

     Works with communitybased organizations to enhance access to UC Davis both as an educational institution and as an informational resource.
     Forges a closer working relationship between UC Davis campus leaders and local and regional community groups in addressing a myriad of social, economic, education, and environmental issues.
     Fosters partnerships and identifies areas of collaboration with a wide variety of community organizations.
     Highlights issues of emerging or growing concern in the region for UC Davis officials and faculty.



