
The UC Davis CrossCountry team is a collection of 15 men and women and at least that many friends and affiliates. These people run 40100 miles per week to compete against the best athletes in the country, and hold their own against everyone they challenge. Winners of several conference championships, most recently the men and women both won their respective NCAA Division1 Independent Crosscountry Championships in Texas in December 2004, which the men did again in 2006.




