The Annual UC Davis Film Festivals Film Festival is produced by the Department of Theatre and Dance and presented by the Davis Varsity Theatre in association with UC Davis Technocultural Studies and cosponsored by Film Studies and Art Studio.

The festival sets your finger on the pulse of studentproduced cinema. Each year the array of short films is unpredictable and exciting. Filmmakers include a wide range of undergraduate and graduate students from across the campus. Categories include comedy, drama, animation, documentaries and all things in between.

