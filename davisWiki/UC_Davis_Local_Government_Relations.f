
Local Government Relations facilitates close collaboration and communication with local government to promote understanding of the universitys core mission and related public policy initiatives.
Local Government Relations:

     Creates and fosters partnerships between the university and local government in the areas of: housing, transportation, air quality, public policy, economic development, landuse planning, agriculture, and the environment.
     Provides a forum for increased understanding and analysis of public policy issues and research.
     Forges stronger working relationships between the university and local government through the identification of areas of mutual concern or interest, and by encouraging dialogue between campus and local government leaders and the public.
     Identifies potential opportunities for increased collaboration and cooperation



