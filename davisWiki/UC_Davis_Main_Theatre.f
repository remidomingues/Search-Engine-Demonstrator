

The address(Main Theatre, 38.538974, 121.747872) is a part of Wright Hall.  The Main Theatre is one of the primary http://theatredance.ucdavis.edu/venues.html venues used by the Theatre and Dance Theatre and Dance Department, which also performs at the Mondavi Center and at Wyatt Pavilion on the other side of Putah creek. Main Theatre is also used by other performing arts groups on campus. Reservations are made through CEVS Campus Events.

The Main Theatre was the same pale yellow color as the Art Building and the Music Building until it was repainted in the mid90s.

There are The Eggheads Eggheads located in front of the building.


