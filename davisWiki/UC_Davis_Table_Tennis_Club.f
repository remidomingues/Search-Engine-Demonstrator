UC Davis Table Tennis Club

Meetings are held on Mondays, Wednesdays, and Thursdays from 7:30 p.m. to 9:30 p.m. in the Upper South End of the http://cru.ucdavis.edu/content.cfm?contentID382&redirectedFromcontent&redirectedFromID90&CFID2008831&CFTOKEN41263842 ARC Pavilion. Players are always welcome to stay later until closing time.

This club is NOT limited only to students of UC Davis. Anyone can join the club, no matter what skill level you are, once you can gain access into the ARC. If you are not a student at UC Davis, then you will need to buy http://cru.ucdavis.edu/content.cfm?contentID397 ARC Membership in order gain access into the ARC to get to the Upper South Pavilion.

Nonmembers are free to come and play with friends during club meetings, as well as get a few tips on technique.
Round robins occur monthly and cost $2. You are placed in a group of 45 people around your skill level and play against the other members of your group.If you win your group, you can get a table tennis prize!

Summer 2014 Update

The UC Davis Table Tennis Club will be having unofficial practice during the summer for those people who are in Davis and want to play table tennis. All you have to do is show up and start playing. There is no membership fee for the club during the summer. All that is required is that you can gain access to the ARC as mentioned above.
The scheduled summer practice times are Mondays, Tuesdays, Wednesdays, and Thursdays from 6:30 p.m. to 9:30 p.m. in the Upper South Pavilion. To check whether there is practice or not, check the https://drive.google.com/file/d/0Bzp0zAplQLaydlBiZnBJbTBDNGc/edit?uspsharing Table Tennis Summer Practice Schedule. The schedule will be updated on a weekly basis as to whether the Upper South Pavilion is open or not.
For more information and updates, please visit the https://www.facebook.com/groups/185678768181070/ UC Davis Table Tennis/Ping Pong Club Facebook page or email James Wang at jajwang@ucdavis.edu

Officers of the UC Davis Table Tennis Club for 20142015:

Member Benefits
  Free tshirt
  Access to a coach who comes on Mondays
  Play against other schools in NCTTA tournaments
  Travel provided to round robins in Folsom on Sundays
  Frequent social outings with a fun group of people!

Steps to Membership
  Pay ARC Membership fee if you are not a student of UC Davis
  Fill out the insurance and B2H forms, available at https://myrecreation.ucdavis.edu/
  Pay the $10 insurance fee
  Pay club fee of $12/quarter or $30/year to the Treasurer

Equipment
  3Star DHS table tennis balls are provided
  4 Butterfly tables, 2 Killerspin tables
  Purple Newgy barriers
  Beginner paddles are available, bringing own paddle is recommended

UC Davis Table Tennis Team

The UC Davis Table Tennis team play in the http://www.nctta.org/regions NCTTA Divisionals, Regionals, and National tournaments. The top 8 players from the club will be selected for the team.


