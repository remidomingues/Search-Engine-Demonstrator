The http://nrs.ucop.edu/ UC Natural Reserve System protects over 135,000 acres of land in California for research and educational purposes. Most (not all) reserves have restricted access, meaning you either need to go with an organized group or http://nrs.ucop.edu/reserves/UseApps/UseApps.html apply for entrance. Each and every one of them is a very special place and well worth a visit for some Outdoor Activities outdoor fun.

