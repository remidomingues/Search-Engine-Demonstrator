

The United States Department of Agriculture has several USDA agencies and offices at the USDA Richard E. Lyng Service Center.

It is located at the corner of G St and 5th St., on the same block as the Regal Cinemas Davis Stadium 5 theatre. This isnt just any USDA office, its the headquarters for the USDA in CA. It was too expensive it get it in Sac, so it has its own office. By far the best part about it is the mysterious OIG door with a cypher lock.

The building was dedicated on October 16, 1998. Richard E. Lyng was the only Californian to have served as U.S. Secretary of Agriculture.

