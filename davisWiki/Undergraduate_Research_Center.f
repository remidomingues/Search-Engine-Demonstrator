
The Undergraduate Research Center at UC Davis helps undergraduates undergraduate students find research opportunities. Research is a job, experience for future jobs, and pregraduate school training all in one. Research is cool, kids!

They help all majors. Not all research is lab work, so do not be shy, nonscience majors.  If you are a science major planning on getting an advanced degree, having some research under your belt is one of the  key ways to get into a PhD program.  Not only do you get to claim the research on your application, but you are likely to get a much more impressive letter of recommendation from a professor youve worked with.


The Undergraduate Research Center opened in October 2008Footnote(http://www.ucop.edu/pathways/ucnotes/nov08/campus/davis.html).

