

UC Davis undergraduates in all UC Davis Academics academic fields can present their research projects at the Undergraduate Research, Scholarship and Creative Activities Conference (aka URC). Research projects that have been conducted under the supervision of a faculty member or professional in any field eligible. The conference is designed to acquaint undergraduates with the process and academic rigors of presenting research in a scholarly manner.

The Conference usually consists of four oral presentation sessions in the morning in Wellman Hall and one poster session in the afternoon in Freeborn Hall.

