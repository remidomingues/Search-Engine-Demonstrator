wiki:WikiPedia:United_States_Marine_Corps United States Marine Corps  (established as the Continental Marines on November 10, 1775 in Philadelphia, Pennsylvania). This is largest military organization designated as Marine in the world, and the majority of the worlds Marine troops are American.

There are several future Marines, current Marines, and former Marines living in Davis.  A large number of Semper Fi stickers and Marine emblems can be seen throughout town.  They seem to be especially popular in The Arbors.

The film http://www.imdb.com/title/tt0418763/ Jarhead is about a US Marine snipers time in wiki:WikiPedia:Operation_Desert_Storm Operation Desert Storm. The film is based on a book of the same name written by UC Davis Famous UC Davis Alumni alumnus Anthony Swofford.


