The http://www.city.davis.ca.us/meetings/agenda.cfm?c19 Unitrans Advisory Committee reviews and makes recommendations regarding any proposed changes to Unitrans service. It is a City Council committee for the City of Davis. It includes members of several city commissions, ASUCD representatives, and members of the public.

