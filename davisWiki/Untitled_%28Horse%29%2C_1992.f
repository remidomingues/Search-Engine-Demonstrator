
This piece is not as public as other Campus Art pieces because its not an outdoor fixture. The horse sculpture is located in the Walter A. Buehler Alumni & Visitors Center Lobby. It was made by UC Davis alumna Deborah Butterfield (B.A. 1971, M.F.A. 1973).

Butterfield commented on the piece: I wanted to create a piece that would humanize the space, that would give it a scale that was more recognizable, a more intimate feeling. The building needed a piece that was larger than life.
