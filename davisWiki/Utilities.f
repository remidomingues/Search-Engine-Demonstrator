
Utilities in the City of Davis include:

  Electricity  PG&E
  Natural Gas  PG&E
  Water  Public Works
  Sewer  Public Works
  Drainage  Public Works
  Refuse  Davis Waste Removal
  Telephone  AT&T, SureWest

On Campus there are also
  Steam
  Chilled Water
  Distilled Water

Remember to always call the Underground Service Alert (http://www.usanorth.org/) before digging.
