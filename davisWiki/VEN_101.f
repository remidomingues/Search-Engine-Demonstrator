Viticulture and Enology 101

The VEN 101 Series is a three course series that educates students in many of the more modern aspects of Viticulture.

  VEN 101A is offered every Fall, and is taught by Andy Walker.
  VEN 101B is offered every Winter, and is also taught by Andy Walker.
  VEN 101C is offered every Spring, and is taught by Dave Smart.
