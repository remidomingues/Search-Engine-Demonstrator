

The Unitrans V Bus Line runs back and forth between the Unitrans Hutchison Bus Terminal to West Village in West Campus. Along the way it passes Health Sciences District, and the University Extension Center.

Due to constant expansion , a Double Decker has been put on the VLine to accommodate the increasing population in West Village.

Check out the schedule and route map at the http://unitrans.ucdavis.edu/v Unitrans website for more information.

Destinations on the V Line
  University Extension Center
  Sacramento City College Davis Center at UC Davis West Village

Apartments Served

  The Ramble Apartments
  The Viridian
