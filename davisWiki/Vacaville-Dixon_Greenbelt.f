The VacavilleDixon Greenbelt is a 1,003acre agricultural buffer between the cities of Vacaville and Dixon right around Interstate 80. It is open space, mostly private Farms farms, with easements prohibiting Developers development. For more information see http://www.solanolandtrust.org/.

The two cities split the $2.5 million cost, unlike the DavisDixon Greenbelt, which was mostly funded by the state and federal governments. The VacavilleDixon Greenbelt Agency was formed in 1996 to monitor the land.


