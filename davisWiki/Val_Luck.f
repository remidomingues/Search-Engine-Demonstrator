
Val Luck (born: 17 Sept. 1917, Des Moines, Iowa; died: 18 July, 2001, Davis, California) was an elementary music teacher in the Davis schools for 12 years.  He earned college degrees from http://en.wikipedia.org/wiki/Grinnell_College Grinnell College and http://en.wikipedia.org/wiki/Columbia_University Columbia University.  He served in the U.S. Army during WW II, reaching first lieutenant.  He is most remembered for teaching elementary music out of a gray school bus that traveled among Davis schools.

