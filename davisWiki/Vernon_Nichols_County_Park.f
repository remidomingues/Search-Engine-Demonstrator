
On the way to Rumsey, about 3045 minutes out of Davis, along Highway 16 you will find Vernon Nichols County Park, which has a nice little swim hole and a couple of beaches.

Heres a picture of a beach right near the bridge and the hosted camp ground. This was taken at the end of September, 2009, so the water level is probably about as low as it will normally get. (before the rain starts) The water still got neck deep in spots.


If youre lucky you might get to see some interesting creatures, like this crawdaddy.



