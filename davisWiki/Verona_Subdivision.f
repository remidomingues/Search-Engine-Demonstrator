The Verona Subdivision is a proposal for developers development of 8.55 acres just east of Mace Ranch Park at the intersection of Fifth Street and Alhambra Drive. The land is owned by Dan Ramos of West Sacramentos Ramco Enterprises. Regis Homes of Sacramento proposes to build 83 single family homes; 10% of the land will be a greenbelt. A public hearing on this development was held in City of Davis Davis on May 28, 2008.

