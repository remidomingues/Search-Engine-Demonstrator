

Run by Gabriella Engelhard, this small outfit made all the awesome chocolate truffles that you may find at Mishkas and Common Grounds coffee shops. She also made other desserts and wedding cakes. There was no retail store but you could order by phone.

If you want similar tasty tasty confections, she now works for Konditorei.

