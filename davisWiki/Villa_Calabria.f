

Villa Calabria is a rental housing community developed and managed by Community Housing Opportunities Corporation CHOC for very low, low and moderate income households. Villa Calabria also provides a resident services program which provides residents with professional staff to assist them in enhancing the quality of life within their residential communities and achieving individual personal goals.

In March 1992 Villa Calabria, a 6unit senior housing complex, was built across the street from Tuscany Villas Apartments. The complex offers one and twobedroom units with a washer and dryer in each unit. Villa Calabria serves residents at or below 50% and 60% of Yolo Countyâ€™s area median income.

