
This Community Center for Village Homes is used regularly for daycare and events.

Events
  Harvest Festival  Annual fall potluck. Held in midSeptember in the Village Homes Community Center.
  Performers Circle  A monthly folk music gathering in the Village Homes Community Center.
  Timna Tal Music  A concert series of Middle Eastern, Mediterranean, Eastern European, and Central Asian music held at the Village Homes Community Center.
