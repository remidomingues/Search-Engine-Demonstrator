

Village Park is a neighborhood park in South Davis. Several houses look out over it, and it integrates nicely with the rest of the neighborhood with its trees helping to blend it in. In the warmer months when the trees have foliage, a majority of the playground equipment is shaded throughout the day. It appears that the citys open container ordinance does not apply to this park.

