Villanova Drive is an extension of 14th Street in the middle of what is now Central Davis.  Originally 14th Street 14th stopped at Oak Avenue, I believe, and eventually it was cut through to make one continuous street from F Street to Sycamore Lane.  It is part of the college streets in the town street name themes.

Villanova House, one of the Davis houses focused on music, is located here.

Accidents
At one time there was a serious traffic problem at the curve just after Redwood Lane and it seemed that at least one major accident a year happened.


In 1976, a speeding car skidded while making the jog at Villanova and Toyon Pl., ran through our neighbors yard, taking out two large sections of fence, hit our car, which was parked in the driveway, spun it around and into the wall of our living room.  The speeding car ended up impaled on our newly planted tree.

The following year, after another car went out of control, smashed into a home, coming to rest at the foot of a sleeping young girls bed, and setting the house on fire (killing the family cat), a committee had several meetings to discuss the safety factor on the street.

Ultimately it was decided that if it had a more residential feel to it, rather than looking like a race track from Davis Senior High School the high school to Anderson Road, it might slow things down. So a divider was placed in the middle of the street, with trees planted on it.

There hasnt been a major accident on that street since.
