




Vision Dance Troupe is a student dance group at UC Davis that performs at various events around campus throughout the year.  We incorporates Contemporary, Jazz, Salsa, Tap, Ballet, Hip hop, Lyrical, and more styles of dance into our performance pieces.  In the past we have performed at various events ranging from student celebrations, fashion shows, sports games, and picnic day celebrations.  We practice twice a week on Tuesdays and Thursdays and perform at different events around campus.  Our practices this year will be focused primarily on our 2014 Showcase in the Spring, in addition to other performances opportunities throughout the academic year.




