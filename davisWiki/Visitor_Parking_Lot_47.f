


Visitor Parking Lot 47 is a large campus parking lot located off of La Rue and behind the Tercero Tercero Dormitories. VP 47 accepts both A and C permit types and has several visitor permit dispensors throughout the lot. It is divided into two sections, VP 47a on the west side bordered by Dairy Road and VP 47sw on the eastern side bordered by Bioletti Way. The western half is going to be removed to make way for the planned  Tercero South Phase II dorm expansion.

