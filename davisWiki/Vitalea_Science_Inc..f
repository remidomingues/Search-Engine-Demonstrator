

Atom Counter Changes Pharmaceutical Drug Development

Vitalea Science is technology driven business that provides  bioanalytical measurements for drug development and biological research using Accelerator Mass Spectrometry, a highly specialized atom counter that quantifies isotopes of carbon at unprecedented levels of sensitivity and precision.   Vitalea collaborates with the Swiss Federal Institute of Technology to develop advanced instrumentation to better serve its customer base, Pharmaceutical and Biotechnology companies.

Vitalea has worked with many of the major global Pharmaceutical Companies as well as internationally distributed biotechnology companies.  Its mission is to accelerate early drug development through enabling key decision making data on human drug metabolism early in drug development programs.

Many of its principal founders arose out of UC Davis and Lawrence Livermore National Laboratory.
