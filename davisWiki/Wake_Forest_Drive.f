Wake Forest Drive is a strange little street on the west side of Sycamore Lane that connects to Hawthorne Lane on the bottom (near Russell) and 8th Street West 8th. at the top.  It is part of the college streets in the town street name themes.

Some attractions on Wake Forest:

  Cuarto residence halls
  Wake Forest Apartments
  Sherwood & Forest Arms Apartments

It is served by the following buses:
  B Bus Line
  C Bus Line

For more information about making your way around the town, see Navigating Davis.
