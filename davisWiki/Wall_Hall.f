

Wall Hall is one of three new Residence Halls dorm buildings in Tercero South Phase II Tercero South II. Located Next to Laben Hall and Kearney Hall, Wall mimics Laben and Kearney in design, and shares a courtyard with its new neighbor halls, Campbell Hall and Potter Hall.

  Wall hall will be the new home for incoming students who with to live in the dorms and undertake the Davis Honors Challenge!
  Wall is also the hall which connects to both the Tercero main quad, and the glasswalled lounge which acts as the main study for all of Tercero II.


