Walter G. Read (February 21^st^, 1854  April 17^th^, 1907) was an inventor and businessman. He worked to secure water rights for the University Farm.

He was highly regarded in the almond industry. He invented almond hullers and cot pit crackers.

His wife was Jennie Drummond Lillard who had a son Lewis Craig Lillard.

