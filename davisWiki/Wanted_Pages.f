We call a page wanted when there are links made to it but the page does not yet exist.  By creating a wanted page youre adding something that others had probably desired to see on the wiki.

Theyre the opposite of Orphaned Pages, which are existing pages with no links to them.

This list is only advisory: just because a page is listed here does not make it necessary. Look carefully at the pages that link to it and use your judgement  if its not worth making a page, remove all the links to it instead.  Of course, many of the best pages on this Wiki started out as some of the most hot sauce worthless pages.

