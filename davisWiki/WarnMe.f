After the Virginia Tech shootings, the UC Davis administration went looking for ways to get word out if tragic events something bad was happening on campus. WarnMe was the result. The system contacts people through phone, email, and text messages.

Sisweb will bug you to verify that your contact information is still good after 120 days or so.

