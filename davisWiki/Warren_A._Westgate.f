Warren Adelbert Westgate (born Plainfield, NH, 12 September 1904, died Davis, CA, 20 November 1989) was a local businessman and genealogist. He and his wife Dorothy Esther Small (24 February 190521 August 1992) are both buried in the Plainfield Cemetery, Plainfield, NH, with his parents and many ancestors and cousins.

He donated his genealogical library to the Davis Genealogy Club.

He was Davis Citizen of the Year in 1972
