The Warren G. Roberts Western Redbud Redbud Collection is a grove of Western redbud, small multitrunked trees that produce clouds of magentapink blossoms in early spring. Redbud is native to the California foothills and is an important plant to Native Californians, who harvest young shoots for basketry. The grove was named to honor Warren G. Roberts, superintendent of the UC Davis Arboretum for more than 35 years.





