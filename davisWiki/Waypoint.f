

  It is a ministry of First Baptist Church that reaches out to the greater community in the Davis area, many members go to various churches in Davis and the surrounding area.  Dinner is served at 6:30 PM and the actual study begins at 7:00 PM.  If you are looking for fellowship and want to dive into the Word, this is a great place to do just that.
  We are led by Jonnalee Henderson, Rebecca Wilson, and Moises De La Torre.  Many members attend FBC, including the leaders.  If anyone has an issue they would like to address outside the group, the FBC leadership is available to provide counsel, oversight, and accountability.

