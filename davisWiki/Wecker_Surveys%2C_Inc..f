

Wecker Surveys is a local business that does Business to Business Services surveying and mapping.  They had a hiatus from November 2008 through midFebruary 2009, but are currently back in business.

According to their website, Wecker Surveys is a surveying and mapping company, providing a wide variety of services including, but not limited to ALTA/ACSM Land Title Surveys, Subdivision/Parcel Maps, Topographic Surveys, Lot Line Adjustments/Mergers, and Legal Descriptions. In the year 2007 we completed over 220 ALTA surveys in 23 California counties.

