The term weed refers to a plant deemed unwanted by someone, somewhere. Grasses popping up in a garden plot may be considered weeds by a horticulturist, while those same prized garden plants popping up in a grassy pasture may be considered weeds by the farmer. In natural areas, weeds are often called invasive species.

One of the most common and invasive weeds in Davis is http://en.wikipedia.org/wiki/Bermuda_grass Bermuda Grass.


UC Davis is very active in weed research. A useful site with weed information is its http://wric.ucdavis.edu/ Weed Research and Information Center.

Weed is also common slang for Marijuana as well as a http://www.weedchamber.com/ town in northern California.
