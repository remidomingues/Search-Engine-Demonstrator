Weed Day is an annual gathering at UC Davis hosted by the Weed Research & Information Center, which is part of UC Cooperative Extension. Weed Day brings together scientists, students, farm regulators and advisors to share information on the latest in landbased and aquatic weed control. The event has taken place annually for over 50 years.

