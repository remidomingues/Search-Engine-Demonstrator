
Located on Hutchison Dr. next to the Life Sciences Addition, the  structure will be six stories high and will be the largest parking structure on the UC Davis Campus campus.

The West Entry Parking Structure project for the UC Davis campus will hold 1,453 spaces and include a 20,800squarefoot office building to house the Transportation And Parking Services and UC Davis Police Department public safety programs, including a new emergency operations center.  The parking structure has been proposed to accommodate recent increases in student enrollment, faculty and staff employment and campus visitors. University Construction Construction will begin in January 2004 with completion expected in November 2005. (http://www.dateline.ucdavis.edu/020703/dl_regents.html source)

