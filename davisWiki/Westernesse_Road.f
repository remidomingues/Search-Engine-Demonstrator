Westernesse Road is one of the many Navigating Davis streets in Davis Village Homes neighborhood that are named after places or characters in J.R.R. Tolkiens books, The Hobbit and The Lord of the Rings.

Both Overhill Lane and Poppy Lane branch off from Westernesse Road.

The Westernesse is the ancestral homeland of the humans in Lord of the Rings. Located far away over the ocean, it is the source of much magic. When Pippin the hobbit battles the Lord of the Nazgul, he uses a sword from the barrow wights tomb, which is the only weapon able to harm the Nazgul.
