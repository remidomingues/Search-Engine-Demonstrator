
The Solano Mall, built in the 1970s, was bought by the Westfield Group in 1998 and renamed to Westfield Solano. It has five anchor stores (six if you include the Edwards movie theaters Movie Theater) which are Sears, Mervyns, JC Penny, Best Buy and Macys.

Although considerably small compared to other Westfield Malls, such as the one off of Union Square in wiki:sf San Francisco, this mall serves all of Solano County and arguably part of Yolo County. Not to mention, it is the only mall with a Hollister Co. store until Concord, CAs Sun Valley Mall in the West and roseville Rosevilles Galleria in the East.

Santa Claus will also be making appearances in the mall between November 10th and December 24th.

