The Weston A. Price Foundation is a NonProfit Organizations nonprofit organization dedicated to restoring nutrientdense foods to our tables.

The Yolo County Chapter of the WAPF works to promote traditional, nurturing food ways and sustainable farming at the local level. At
http://www.wapfyolo.ning.com, youll find local sources for grassfed livestock, pastureraised eggs, raw milk from grassfed cows, naturally leavened bread, as well as many other traditional foods. This local facebookstyle website also offers information on nearby farms that offer CSAs (Community Supported Agriculture), current events in Davis and the surrounding areas, photos, videos, and a community forum to discuss any and all things WAPF Yolo County.


