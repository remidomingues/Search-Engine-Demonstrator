Crime
The Davis Enterprise http://www.davisenterprise.com/localnews/crimefirecourts/violentcrimesatwholeearthfestivalleadtodancevenueshutdown/ reports that a series of violent crimes at the 2011 Whole Earth Festival will lead to the shutdown of the DJ stage in the pit of Wellman Hall.  Among the reported crimes are the rape of a 16yearoldgirl on Friday night, sexual battery against a 14yearoldgirl, the beating of a 19yearold man, and the assault and robbery of a UC Davis student Saturday morning.  Arrests have been made for the beating and the robbery, but not for the sexual crimes.

Vice Chancellor for Student Affairs Fred Wood announced Thursday that the DJ stage wont be back because it has created an unsafe environment.

