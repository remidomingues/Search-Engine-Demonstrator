YoLoCa! Local Food Booth

Monticello, Joy & Taylors Raw Chocolate Joy and Taylors Raw Chocolates & Pachamama Coffee http://www.pachamama.coop have teamed up to create the Whole Earth Festivals first local food booth, dubbed YoLoCa!

We will be on the east quad across the street from North and South Hall under the cork oak selling Monticellos market salads, veggie sandwiches, adult Smores with housemade Pachamama coffee marshmallows & graham crackers & Joy & Taylors chocolate, Terra Firma strawberry shortcake with fresh whipped cream, & lavender lemonade. Joy & Taylors Raw Chocolates will be serving frozen organic bananas with chocolate sauce, Terra Firma strawberries with chocolate sauce, chocolate shots, & truffles. Pachamama will serve fresh brewed hot & iced coffee & mochas.

If you are going to the festival, please stop by to say hello and support the first Whole Earth Festival local food booth. If you arent going to the festival, support our local businesses by patronizing Monticello restaurant & Davis Farmers Market Picnic in the Park Food Booth; purchasing Joy & Taylors Raw Chocolates in the chocolate aisle and/or the living foods section at the Davis Food Coop; and purchasing Pachamama coffee at the Davis Food Coop and at their booth at the Davis Farmers Market.

For more information (and for this years program) go to http://wef.ucdavis.edu.
