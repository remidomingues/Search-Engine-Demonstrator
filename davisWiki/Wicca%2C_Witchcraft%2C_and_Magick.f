

Wicca, Witchcraft, and Magick is a free course taught through the Experimental College. You can sign up inperson or online.

Forget double double toil and trouble, Harry Potter, and devil worship; learn about real witchcraft. This course is for everyone from experienced practitioners looking for a different perspective, to people brand new to witchcraft. Discussion and handson activities will feature prominently in this course. We will start with the history and theory behind paganism and the Craft, seeing where science, faith, and ethics intersect. From this we will progress into drawing a circle, calling quarters, deities, divination, and ritual itself.

