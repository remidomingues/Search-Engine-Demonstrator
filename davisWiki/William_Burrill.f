William Burrill served as Trustee for the Davis JUSD School Board from 1973 to 1977.  He was also minister at the The Episcopal Church of St. Martin Episcopal Church of St. Martin from 1962 to 1981.  After that he took other positions in the Episcopal Church in Northern California, Rochester, New York, and Arizona.

