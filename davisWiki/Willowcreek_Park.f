

In the park there is a lot of open grassy area, two play structures, a circular lawn near the basketball area with five trees in it, and one of the spill over drinking fountains. The Willowcreek Greenbelt greenbelt that heads south from here is nice to ride along, and to go from the Dave Pelz Bike Overpass to the bike path along the creek you have to pass through this park. There is also the Goldstein Memorial Bench. It appears that the citys open container ordinance does not apply to this park.

