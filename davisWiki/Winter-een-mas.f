Wintereenmas is a holiday for videogaming video gamers invented in the comic strip wiki:WikiPedia:Ctrl+Alt+Del.  It is the only gaming holiday recognized by EB Games, and is growing in popularity in the real world.  It has made appearances in Davis on campus in 2007. Wintereenmas is the last week in January, the 25th through the 31st.


