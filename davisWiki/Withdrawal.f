When a UC Davis student for some reason cannot continue their studies, they can withdraw from UC Davis the university.

It is a fairly painless process; you just have to fill out a simple form with your personal information and reason for leaving.

The Registrar is fairly anal about keeping the forms in their office, though. If you would rather fax or mail in the form it is available online. http://registrar.ucdavis.edu/html/office_of_the_registrar_forms.html Cancelation/Withdrawal Form

