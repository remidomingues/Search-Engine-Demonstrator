Womens Music Night is a concert series featuring touring female singer/songwriters as well as an openmic session for Music Scene local artists. Duval Speck hosts and performs in the shows, which take place at Davis Cafes coffee houses. Currently, Common Grounds is home to WMN but they have taken place at Cafe Roma in the past. There is a $4$10 sliding cover charge. Shows are allages.

