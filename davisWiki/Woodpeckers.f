Woodpeckers are fairly common in the Arboretum. The most common species are the Nuttalls Woodpecker (Picoides nuttallii) and the Northern Flicker (Colaptes auratus), although Downy Woodpecker (Picoides pubescens) and others may be seen as well.  They use their chisellike beaks to peck holes in tree bark, looking for insects to eat.

