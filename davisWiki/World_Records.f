David Phillips
David Phillips, also know as pudding guy probably holds the record for most amount of frequent flier miles redeemed from food purchases. http://www.time.com/time/magazine/article/0,9171,996450,00.html See Time magazine story

Lis Harvey
Folk musician Lis Harvey, holds the http://lisharvey.com/lis/50state.php Guinnessconfirmed record for Fastest Solo Female Musician to Play A Concert in All 50 States.  Lis played 52 shows in 50 US states in 60 days in 2002. Lis currently resides in Davis, and has played locally at Bistro 33, Delta of Venus and Cafe Roma.

