Davis is host to a number of World Records, some of them registered with wiki:WikiPedia:Guinness_World_Records Guinness.  On a smaller scale, there are a lot of interesting Davis Records records within Davis borders.


 Guinness 
These are the records that are to be confirmed, or have been confirmed by http://www.guinnessworldrecords.com/ Guinness authorities.

Worlds Largest Bicycle Parade 
Saturday, March 3rd, 2007 the Aggie Pack organizes the Worlds Largest Bicycle Parade with around 900 bicycles snaking singlefile through campus.

Worlds Largest Flashmob 
Thursday, February 1, 2007, Zack Crockett organizes the Worlds largest flashmob which applauded the people at the Memorial Union ATMs when the MU bell tower chimed noon.

 Longest paperclip chain 
February 13, 2004, Dan Meyer puts together 54,030 paper clips to create a 5,340foot paper clip chain.

