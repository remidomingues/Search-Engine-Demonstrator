
Houses the Department of Theatre and Dance, and is named after Celeste Turner Wright, whose name adorns the address(front of the building, 38.539106, 121.747917).  Wright was the first female English professor at UC Davis.  The building houses the UC Davis Main Theatre which is also its most prominent feature.  The Eggheads Yin and Yang are in the courtyard in front of the Building.

When new, Wright Hall was a promenant feature on campus, visible from as far away as the Hickey Gym, as seen in this 1966 Ansel Adams photo, taken the year it was built.  After 1967 the facade was hidden by the planting of a redwood grove.

There is a Costume Shop costume shop in the basement and the happy smiley tulips at the back of the building.

