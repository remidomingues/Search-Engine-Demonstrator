Yerba maté is a shrub that is often chopped up and turned into a tisane (herbal tea), and sometimes fermented. It contains caffeine and drugs similar to caffeine. It is a very popular drink in South America, and traditionally drunk with a bombilla (metal straw) that filters the solids out, in a gourd that slowly grows a certain character, especially if the mate is allowed to ferment in it.

