
The YoloSolano Air Quality Management District (also known by the memorable acronym YSAQMD) is a public health agency with the mission of protecting human health and property from the harmful effects of air pollution. The District serves all of  Yolo County and the northeastern portion of Solano County, including the cities of Vacaville, Dixon and Rio Vista.

