
ARES organizations provide emergency communication support using the Amateur Radio Service when other communication mechanisms fail.  The Yolo County Amateur Radio Emergency Service is the local Yolo County chapter of ARES.

The Yolo ARES chapter holds inperson meetings once a month at 7:00PM on the third Tuesday of each month in Woodland at the Yolo County Red Cross office.

They also hold weekly nets on monday nights from 8:008:30PM on the http://www.bark2m.com/ bark repeater.
