
The Yolo County Elections office is responsible for city, county, state and national elections for Yolo County, including elections for board of education boards of education and community colleges community college governance boards.  The County Clerk Reporter, Ms. Freddie Oakley, oversees the office.

The office is not involved with ASUCD Elections.

 2008 Presidential Election Early Voting 
The office is open for early voting for Yolo County registered voters until Monday, November 3rd, on weekdays from 8am to 5pm.



