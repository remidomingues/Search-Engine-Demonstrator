The Yolo County Homeless and Poverty Action Coalition is an affiliation of organizations throughout the county whose mission involves directly addressing and providing services related to homelessness. Members include:

Nonprofit organizations:

  Community Housing Opportunities Corporation
  Davis Community Meals
  Food Bank of Yolo County
  Sexual Assault and Domestic Violence Center
  Short Term Emergency Aid Committee
  United Christian Centers (Broderick Christian Center)
  Woodland Youth Services
  Yolo Community Care Continuum
  Yolo Crisis Nursery
  Yolo Wayfarer Center Christian Mission

Partners:
  City of Davis
  City of West Sacramento
  City of Winters
  City of Woodland
  Yolo County County of Yolo

They conduct a census of homeless people every two years in order to get federal funding.
