

The Yolo County SmokeFree Apartments Project is part of the countys Public Health Education Tobacco Prevention Program. The SmokeFree Apartments Projects goal is to reduce exposure to secondhand smoke. They work with multiunit housing owners to encourage designation of some apartments as smokefree. Contact them for a copy of the list of complexes that have smokefree units.

