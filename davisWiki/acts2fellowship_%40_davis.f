

acts2fellowship @ davis was a Student Organizations student Christian Spiritual Organizations fellowship at UC Davis that fostered a warm, friendly environment where any student with questions about Christianity could find answers, and those who were already Christians could grow deeper in their faith. At acts2fellowship, they were inspired to create a community described in Acts chapter 2 of the Bible. It’s an example of ordinary folks creating an extraordinary community. It’s what they wanted to imitate.

During the academic year they met on the UC Davis campus.
