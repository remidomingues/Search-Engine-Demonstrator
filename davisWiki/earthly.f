earthly is a Music Scene band formed in Davis through the collaboration of Josiah Of the Future & Leif Bauer, with Alex Seratti on Drums.  Occasionally Kevin Deacon plays Trumpet at live shows.  They have a http://earthly.bandcamp.com/ website.

Recordings include demo, 3arthly demo 2wo, & demo Remaster

(Remaster by Matigus)

earthly has played at Delta of Venus, Armadillo Music, and International House, as well as all over the streets of Davis.

Leif & Josiah met at the Little Prague Open Mic in 2009(?) and have played together previously as Raging Squirrels.

They currently play shows all over Davis, CA.
