
Izzy
A.k.a. Izzy Bell
A.k.a. The Itty Bity Izzy Girl

Hi! My name is Izzy.  I like to play and run and watch the birdies and play with rolly things and jump and chase and then I like to cuddle.  I have a brother, Maxipoo Max, and he is big and lazy but he is cozy and warm.  I like food left on the table and sometimes I find yummies in the compost pile.  Some people call me the carb kitty because I like starchy things from the compost like tortillas and bread and crackers and yummy yummy burnt toast.  Okay, bye.
