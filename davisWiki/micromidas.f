

Micromidas is a biotechnology company founded by UC Davis alumni that focuses on the converting the carbon in organic wastewater into biodegradable plastic.

From the company website: Micromidas technology will close the biomass cycle by enabling the production of myriad products from highly diverse, biological feedstocks. We convert the carbon in organic wastewater into bioplastics that can be used just like conventional plastics. Micromidas process generates high yields of bioplastic while substantially reducing the amount of waste that has to undergo treatment.

