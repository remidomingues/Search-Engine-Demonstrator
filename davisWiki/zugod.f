
ZUGOD stands for http://www.zope.org Zope Users Group of Davis, but most of the time these meetings focus on http://www.plone.org Plone.  Zope and Plone are free, opensource software programs used for managing website content.  Similar to the software that drives this wiki, theyre used to empower computer users to edit their own pages.

Once a month local Ploneistas get together to exchange notes and advice on local and UCD use of Plone/Zope/Python.  Usually someone presents an interesting topic, then there is plenty of time for questions and socializing.

